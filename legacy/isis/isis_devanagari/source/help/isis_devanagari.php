<h2>Instructions for Use</h2>

<p>The system is phonetic in the true sense, so that's how you get the standalone consonants and vowels.</p>

<ul>
<li>For long vowels, press the concerned vowel key twice.</li>
<li>To get an aspirated consonant, press <span class='keys'>h</span> after its unaspirated counterpart.</li>
<li>Try typing a vowel after a consonant.</li>
<li>Experiment with Shift+any key.</li>
<li>To get a joined consonant (yukta varna), press <span class='keys'>/</span> (once or twice) after typing the constituent consonants.</li>
<li>To break up a joined consonant, press <span class='keys'>Ctrl+/</span>.</li>
<li>Vocalic R ऋ can be typed with <span class='keys'>;r</span>; press <span class='keys'>/</span> for the subscript form ृ</li>
<li>Vocalic RR ॠ is typed with <span class='keys'>;rr</span>; press <span class='keys'>/</span> for the subscript form ॄ</li>
</ul>

