<h2>Additional Notes</h2>

<ul>
  <li>The system is phonetic in the true sense, so that's how you get the standalone consonants and vowels.
  <li>For long vowels, press the concerned vowel key twice.
  <li>To get an aspirated consonant, press h after its unaspirated counterpart.
  <li>Try typing a vowel after a consonant.
  <li>Experiment with Shift+any key.
  <li>To get a joined consonant (yukta varna), press / (once or twice) after typing the constituent consonants.
  <li>To break up a joined consonant, press Ctrl+/.
</ul>