<h2>Keyboard Layout</h2>

<p>The keyboard layout is described in terms of a chart rather than a keyboard. This is because many base characters are typed as a sequence of a letter followed by one of =, ~ or ^. Details of the 
keyboard layout may be found in the <span><a href='Greek Poly Comp Kbrd Layout.pdf'> document in the section on 
Keyboard Assignments.</a> (PDF, requires <a href='http://www.adobe.com/reader/' target='_blank'>Adobe Reader</a>)</span></p>

<h2>Keyboard Installation </h2>

<p>Keyman 6 or later is needed for using the keyboard. Keyman's latest version is available from <span><a href=https://keyman.com/downloads/>here</a></span>. If you will be writing or modifying keyboards, you will need the Developer version. Otherwise, get the regular version. </p>

<h3>Keyman Install</h3>

<ol>
1.	Uninstall any previous versions of Keyman<br>
2.	Install Keyman, following the installation instructions provided with Keyman.<br>
</ol>

<h2>Using the keyboard</h2>

<h3>Using Keyman Selector</h3>

<ol>
1.	Click on the Keyman keyboard selector in the taskbar (bottom right of screen). <br>
2.	Select the keyboard you want from the menu.
</ol>

<h3>Using Keyman keyboards in place of Windows keyboards</h3>

<ol>
1.	Start Keyman Configuration. <br>
2.	Select the Languages tab. <br>
3.	In the table that appears, select the language for which you wish to always use a Keyman keyboard. <br>
4.	Click in the last column or press F2 to select the Keyman keyboard. <br>
5.	Click the OK button to save your selection. <br>
6.	Click the Windows language selector, and select the language you just associated with your keyboard. 
</ol>


<h2>Fonts</h2>

<p>SIL International has produced several Unicode fonts that have Greek characters. These fonts are:</p>

