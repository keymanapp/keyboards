<p class=MsoNormal>This keyboard is for use with <span class=SpellE>Tavultesoft</span>
<span class=SpellE>Keyman</span> Desktop, and should function on both Windows
keyboards as well as Mac keyboards running Windows under an emulator.<span
style='mso-spacerun:yes'>  </span>It was originally developed for Mac running
Windows, and used <span class=SpellE>OPTION+key</span> combinations to output
the special characters for the <span class=SpellE>Bukawa</span> language.<span
style='mso-spacerun:yes'>  </span>However, because the Option and Control keys
on Windows computers are somewhat different to Mac keyboards it has been
revised so that the sequence used is ‘[‘+key (left square bracket + key) to
output the same characters.</p>

<p class=MsoNormal><o:p>&nbsp;</o:p></p>

<p class=MsoNormal>Windows users should use just the <span class=SpellE>deadkey</span>
‘[‘</p>

<p class=MsoNormal>Mac users can use either the <span class=SpellE>deadkey</span>
‘[‘ or the OPTION key. </p>

<p class=MsoNormal>(For Mac users running on Mac only, I suggest developing a
keyboard using the program <span class=SpellE><i style='mso-bidi-font-style:
normal'>Ukelele</i></span>: http://scripts.sil.org/cms/scripts/page.php?site_id=nrsi&amp;item_id=ukelele<b><span
style='font-size:14.0pt;font-family:"Arial","sans-serif";color:#208C10'> <br>
</span></b><span class=GramE>Please</span> contact the author if you would like
further information or a <span class=SpellE>Bukawa</span> keyboard for use with
<span class=SpellE>Ukelele</span>.)</p>

