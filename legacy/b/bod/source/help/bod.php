<p>1. 绝大多数藏文字符的输入可通过直接按下相应的键位来实现。例如：直接按下字母ར和ག所在的键位可输入字符རྒ。
</p><p>
2. 输入字母ར、ཝ或者ཡ时，不必考虑它们是否发生形变。例如，直接按下字母ར、ག和ཡ对应的键位即可输入字符རྒྱ。其它纵向叠加字符（例如：ལྔ、རྒྱ等）可类似输入。
</p><p>
3. 依次按下ག、ཨ和ར对应的键位，可输入གར，即通过插入ཨ键来阻止字母ག和ར的纵向叠加。用户输入རག、གར、ལག、གལ时，可类似操作。
	</p>


