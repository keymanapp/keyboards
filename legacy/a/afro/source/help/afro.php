    <p>La touche morte se tape toujours en premier,
    puis le caract&#232;re de base non modifi&#233;, puis
    &#233;ventuellement le caract&#232;re modificateur. Par exemple
    '&nbsp;&#596;&#768;&nbsp;' se tape d'abord '&nbsp;,o&nbsp;' ce
    qui donne '&nbsp;&#242;&nbsp;' suivi de la touche modificatrice
    '&nbsp;&lt;&nbsp;' qui donnera le caract&#232;re voulu.</p>

    <p>On pr&#233;cisera qu'on a fait le choix de la
    d&#233;composition des voyelles &#224; tons haut, bas et
    haut-descendant (circonflexe) plut&#244;t que celui des
    caract&#232;res pr&#233;-compos&#233;s (pour ceux qui existent)
    par souci d'homog&#233;n&#233;it&#233; de la transcription. Les
    voyelles accentu&#233;es fran&#231;aises accessibles
    directement sur le clavier restant elles des caract&#232;res
    pr&#233;-compos&#233;s. Il est &#233;vident que si Unicode
    comportait les caract&#232;res sp&#233;ciaux
    pr&#233;-compos&#233;s avec les diacritiques, ce dernier choix
    aurait &#233;t&#233; pr&#233;f&#233;r&#233; pour des raisons de
    qualit&#233; de repr&#233;sentation graphique et
    d'homog&#233;n&#233;it&#233; des graph&#232;mes.</p>
