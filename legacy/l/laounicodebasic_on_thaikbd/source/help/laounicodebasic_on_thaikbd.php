and currency symbols. Reordering rules maintain consistent underlying sequence
of diacritic vowels and tones, and other rules prevent entry of many illegal
vowel and tone sequences and beep to alert the typist.<span
style='mso-spacerun:yes'>  </span><o:p></o:p></span></p>

