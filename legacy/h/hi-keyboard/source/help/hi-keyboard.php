<h1 style='margin-bottom: 2px'>Hawaiian Unicode and ANSI Keyboards</h1>
<p style='margin-top: 2px'><i>Created by Tavultesoft Pty Ltd</i></p>
<p>This package includes two keyboards for typing Hawaiian: <b>Hawaiian Unicode</b> and <b>Hawaiian ANSI</b>.
The Hawaiian Unicode keyboard should be used in all circumstances except when editing old text that was written originally in the ANSI format.</p>

<h2>Fonts</h2>

<p><b>HI Kakuhihewa Unicode</b>, <b>HI Keawe Unicode</b>, <b>HI Manokalanipo Unicode</b> and <b>HI Pi'ilani Unicode</b> fonts have been
installed with this keyboard package and are recommended for use with Hawaiian.</p>

<p>These fonts are all Copyright &copy; 2002 Hale Kuamo'o and are distributed with this keyboard with permission from Hale Kuamo'o.</p>

