<h3>Typing additional characters</h3>

<ol>
	<li>Vowels with a circumflex: type the vowel then the <em>left square bracket</em> key, e.g. ( a[   yields  â )</li>
	<li>Characters with a dot above: type the vowel then the <em>right aquare braket</em> key, e.g. ( s]  yields  ṡ )</li>
	<li>The <em>back slash</em>key is used for typing the modifier letter apostrophe.</li>
</ol>

