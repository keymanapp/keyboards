<li>Right Alt Key + X > &#x0621; - Arabic letter Hamza</li>
<li>Right Alt Key + C > &#x0624; - Arabic letter Waw with Hamza Above</li>
<li>Right Alt Key + M > &#x0629; - Arabic letter Teh Marbuta</li>
<li>Right Alt Key + W > &#x064B; - Arabic Fathatan</li>
