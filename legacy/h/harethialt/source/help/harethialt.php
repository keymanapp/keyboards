<h3>Typing additional characters</h3>

<ol>
	<li>Press a consonant key to obtain the version of the character belonging to series 6 (no inherit vowel)</li>
	<li>Press a consonant followed by a vowel key (a, e, i ,o, u, \) to obtain the other series.</li>
	<li>To type an indepentant vowel type the <strong>`</strong> key followed by a vowel key.</li>
	<li>To type the long vowel marker (◌́), press the pipe <strong>|</strong> key.</li>
</ol>

