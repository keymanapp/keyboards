<p>The “<strong>\</strong>” key has a number of functions, it:</p>
<ol>
<ol>
	<li>indicates that the preceding consonant is an isolated consonant rather than part of a digraph, e.g. <strong>hêgʼna</strong> (<strong><span class="ethiopic">ሔ́ግነ</span></strong> - replace);</li>
	<li>distinguishes between a double digraph and a consonant followed by digraph, <strong>magʼgna</strong> (<strong><span class="ethiopic">መግኘ</span></strong> - throwing);</li>
	<li>distinguishes between a consonant and an isolated vowel <strong>Gaz’i</strong> (<strong><span class="ethiopic">ገዝኢ</span></strong> - God); or</li>
	<li>following a vowel it indicates that the vowel is stressed <strong>raʼyi</strong> (<strong><span class="ethiopic">ረእዪ</span></strong> - idea) or <strong>usuʼ</strong> (<strong><span class="ethiopic">ኡሱእ</span></strong> - people). </li> 												
</ol></ol>


