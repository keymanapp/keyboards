<h1>The Tunisian Spoken Arabic Keyman Keyboard Package</h1>


<h2>Keyboard</h2>
<p style="text-align: justify;">
One keyboard is included in this package. This Keyman keyboard is based (with slight modifications) on the Microsoft &ldquo;Arabic (Tunisia)&rdquo; keyboard. It is not a mnemonic keyboard. It includes an on screen keyboard which can be viewed by clicking on the Keyman icon (in your taskbar) and selecting the <b>On Screen Keyboard</b> menu item. The keyboard will be associated with the Arabic (Saudi Arabia) language.</p>

<h2>Manual</h2>
