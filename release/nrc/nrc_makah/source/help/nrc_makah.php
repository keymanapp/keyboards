<h2> Fonts </h2>
<p> As of this writing (June 7, 2021), many fonts have poor support for Makah
letters. There are a few letters that can be used to diagnose whether a font
supports writing in Makah: </p>

<h3> Common problem letters </h3>
<p>Many fonts struggle with the following characters: </p>
<ul>
  <li><strong>ƛ̓</strong> — the glottal mark above often sits too close to ƛ as to be indistiguishable.  </li>
  <li><strong>x̌</strong> — the &#x25CC;&#x030C; appears off to the side of the x. </li>
  <li><strong>k̓</strong> — the &#x25CC;&#x0313; appears off to the side of the k. </li>
  <li><strong>ɫ</strong> — the font may not contain this letter, so a different font is substituted. </li>
  <li><strong>ʔ</strong> — the font may not contain this letter, so a different font is substituted. </li>
</ul>

<h3> Fonts that work well for typing Makah on a computer: </h3>
<ul>
  <li>Andika: <q style="font-family: Andika">ʔux̌u·ʔaƛa·k,  weʔič̓   šuʔuk̓ʷaƛ̓  ɫ, serifax̌</q></li>
  <li>Arial: <q style="font-family: Arial">ʔux̌u·ʔaƛa·k,  weʔič̓   šuʔuk̓ʷaƛ̓  ɫ, serifax̌</q></li>
  <li>Times New Roman: <q style="font-family: Times New Roman">ʔux̌u·ʔaƛa·k,  weʔič̓   šuʔuk̓ʷaƛ̓  ɫ, serifax̌</q></li>
