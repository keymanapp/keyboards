<p>
This keyboard was developed for use by BTL (Bible Translation & Literacy) for language teams in Kenya.
</p>

<p>This keyboard makes use of modifier keys. Typing the modifier key before the base character will output the new or combined character. For example:</p>
	<p>[c (Open Bracket and c) will produce c (available on cC eE ?? gG iI jJ oO sS uU).</p>
	<p>]o (Close Bracket and o) will produce ö ( available on oO uU).</p>
	<p>]s (Close Bracket and s) will produce s .</p>
	<p>`c (Back quote and c) will produce ç (available on sS).</p>
	<p>'c (Quote and c) will produce c' (available on cC kK qQ pP sS tT).</p>
	<p>< and < will produce «.</p>
	<p>> and > will produce ».</p>
