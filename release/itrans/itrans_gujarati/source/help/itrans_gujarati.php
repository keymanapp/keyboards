<h3><span style="color: chocolate;">Gujarati Phonetic (ITRANS) </span>Keyboard</h3>
<p>This is a phonetic (ITRANS) keyboard layout for Gujarati. It is based on <strong><a href="https://www.aczoom.com/itrans/">ITRANS</a></strong> transliteration scheme with some modifications. You can phonetically type using the modified ITRANS trasliteration scheme on a regular QWERTY keyboard and get Devanagari Hindi output. 
</p>
<p>For example, the keys <kbd>s</kbd> <kbd>a</kbd> <kbd>M</kbd> <kbd>s</kbd> <kbd>k</kbd> <kbd>R</kbd> <kbd>t</kbd> <kbd>a</kbd> will output the word <strong>संस्कृत</strong>, <kbd>l</kbd> <kbd>a</kbd> <kbd>x</kbd> <kbd>m</kbd> <kbd>I</kbd> will output the word <strong>लक्ष्मी</strong>.</p>

