<h2>Gujarati Phonetic (ITRANS)</h2>
    <h2>
    <span style="color: chocolate;">Gujarati Phonetic (ITRANS)</span> Keyboard Version 1.0.0</h2>
    <p>This is a phonetic (ITRANS) keyboard layout for Gujarati. It is based on 
    <strong>
      <a href="https://www.aczoom.com/itrans/">ITRANS</a>
    </strong> transliteration scheme with some modifications. You can phonetically type using the modified ITRANS scheme on a regular QWERTY keyboard and get Gujarati
    output.</p>
    <p>For example, the keys 
    <kbd>s</kbd>     <kbd>a</kbd>     <kbd>M</kbd>     <kbd>s</kbd>     <kbd>k</kbd>     <kbd>R</kbd>     <kbd>t</kbd>     <kbd>a</kbd> will output the word 
    <strong>સંસ્કૃત</strong>, 
    <kbd>l</kbd>     <kbd>a</kbd>     <kbd>x</kbd>     <kbd>m</kbd>     <kbd>I</kbd> will output the word 
    <strong>લક્ષ્મી</strong>.</p>
