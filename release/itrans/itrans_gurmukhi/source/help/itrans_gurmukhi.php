<h3>Conjuncts</h3>
<p>
Consonant conjuncts are automatically formed, e.g. <kbd>k</kbd> <kbd>t</kbd> produces 
<samp>ਕ੍ਤ੍‌</samp>. The inherent <strong>a</strong> is only applied when followed by 
<kbd>space</kbd>. e.g. <kbd>k</kbd> <kbd>t</kbd> <kbd>a</kbd> or <kbd>k</kbd> <kbd>t</kbd> 
<kbd>space</kbd> produces <samp>ਕ੍ਤ</samp>. To force an explicit virama at end of word, use 
<kbd>^</kbd> e.g. <kbd>k</kbd> <kbd>t</kbd> <kbd>^</kbd> <kbd>space</kbd> produces 
<samp>ਕ੍ਤ੍‌</samp> .</kbd>
</p>
<p>A full (live) consonant with visible virama in middle of words can be typed using 
<kbd>^</kbd>, e.g. <kbd>k</kbd> <kbd>^</kbd> <kbd>S</kbd> produces 
<samp>ਕ੍‌ਸ਼</samp>.</p>

<h3>Zero Width Non Joiner (ZWNJ)</h3>
<p>To type a Zero Width Non Joiner, <kbd>_</kbd> can be typed between the letters, for example: 
<kbd>a</kbd> <kbd>_</kbd> <kbd>i</kbd> will generate <samp>ਅ‌ਇ</samp>,
<kbd>k</kbd> <kbd>_</kbd> <kbd>S</kbd> will generate <samp>ਕ੍‌ਸ਼</samp>
.</p>

<h3>Zero Width Joiner (ZWJ)</h3>
<p>To type a Zero Width Joiner, <kbd>+</kbd> can be typed between the letters, for example: 
<kbd>k</kbd> <kbd>+</kbd> <kbd>S</kbd> will generate <samp>ਕ੍‍ਸ਼</samp> while 
<kbd>k</kbd> <kbd>S</kbd> will generate <samp>ਕ੍ਸ਼</samp> .</p>

