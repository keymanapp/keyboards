<h2>Panjabi/Gurmukhi Phonetic (ITRANS)</h2>
<h2>
<span style="color: chocolate;">Gurmukhi Phonetic (ITRANS)</span> Keyboard Version 1.0.0</h2>
<p>This is a phonetic (ITRANS) keyboard layout for Punjabi written in Gurmukhi script. It is based on 
<strong><a href="https://www.aczoom.com/itrans/">ITRANS</a></strong> transliteration scheme 
with some modifications. You can phonetically type using the modified ITRANS scheme on a 
regular QWERTY keyboard and get Gurmukhi Unicode output.</p>
<p>For example, the keys 
<kbd>s</kbd><kbd>a</kbd><kbd>M</kbd><kbd>s</kbd><kbd>k</kbd><kbd>R</kbd><kbd>t</kbd><kbd>a</kbd> will output the word 
<strong>ਸਂਸ੍ਕ੃ਤ</strong>, <kbd>l</kbd><kbd>a</kbd><kbd>x</kbd><kbd>m</kbd><kbd>I</kbd> will output the word 
<strong>ਲਕ੍ਸ਼੍ਮੀ</strong>.</p>

<h3>Optional Sanskrit Mode</h3>

<p>The optional Sanskrit mode can be activated by pressing 
<kbd>RCTRL</kbd><kbd>RALT</kbd><kbd>0</kbd> keys. In this mode, 
the implicit <strong>a</strong> is not automatically applied after the word ending consonant. 
e.g. <kbd>rAm</kbd> produces <samp>ਰਾਮ੍</samp> not <samp>ਰਾਮ</samp>.<p>

<p>`Panchama varna` <samp>ਙ੍</samp> and <samp>ਞ੍</samp> as part of consonant conjuncts 
can also be typed as <kbd>n</kbd> for convenience, e.g. <kbd>mangaLa</kbd> 
will output the word <strong>ਮਙ੍ਗਲ਼</strong>, <kbd>chanchala</kbd> will 
output the word <strong>ਚਞ੍ਚਲ</strong>.  </p>

<p>The default mode can be reactivated by pressing 
<kbd>RCTRL</kbd><kbd>RALT</kbd><kbd>1</kbd> keys.</a>

<h3>Keyboard layouts</h3>
<p>The default and shifted layouts cover digits, consonants, vowels and other commonly used characters.</p>

