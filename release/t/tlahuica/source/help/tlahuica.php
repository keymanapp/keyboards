<p>El saltillo se encuentra a la derecha de la tecla 0 y a la izquierda del ¿.<br>
Para obtener las vocales con barra  del tlahuica, se presionan éstas y la tecla Alt Gr al mismo tiempo.<br>
Alt Gr también sirve para escribir signos como el apóstrofo, el arroba, el signo de exclamación de apertura y, para quienes no cuenten con un teclado de 102 teclas, los signos de mayor que y menor que.<br>Estos son los caracteres que se consiguen apretando Alt Gr:</p>
<img src="alt_layer.png">
<p>Al presionar Shift/mayúsculas se obtienen las letras mayúsculas (incluyendo el saltillo) y los siguientes signos:</p>
<img src="shift_layer.png">
<p>Esto también aplica para las vocales barradas. Por lo que si se presionan las vocales, Alt Gr y Shift/mayúsculas al mismo tiempo se obtendrán las vocales barradas en mayúsculas.</p>
<img src="shift_alt_layer.png">
<p>Los botones de acento agudo, diéresis y acento grave funcionan como lo harían en un teclado del español y el acento agudo puede aplicarse a las vocales barradas (minúsculas y mayúsculas) si se quiere usar para marcar tonos.<br>
