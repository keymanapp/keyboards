<p>
    taichow keyboard for romanized goetian Ng-Yüih-Nyü.台州蘇州寧波吳越語羅馬字輸入法
</p>
<p> typing specific letters by doubletapping and combination 雙擊咾字母組合輸入特殊字母</p>
<p> examples: oo > ô, oe > ö, uu > ü, ae > æ, aw > ă, aa > ă, uw > ŭ </p>
<p> adding aspiration marks "‛" by adding h after consonants 垃拉輔音後頭輸入h字母，添加送氣音符號"‛"</p>
<p> examples: ph > p‛, th > t‛, kh > k‛, tsh > ts‛, ch > c‛, kwh > kw‛, hh > ‛, kyh > ky‛, kwh > kw‛, khy > ky‛, khw > kw‛， gh > ‛ </p>
<p> Typing 'gh', to generate voiced sound h "‛", 輸入gh，產生濁音h "‛"</p>
<p> examples: gh > ‛ </p>
<p> Typing 'h' after 'l''m''n', to generate voiced sound 'l̆''m̆''ň', 垃拉輔音lmn後頭輸入h，產生濁音"l̆""m̆""ň"</p>
<p> examples: lh > l̆, mh > m̆, nh > n̆ </p>
<p> Typing 'w' twice, to generate un-voiced sound 'w̆', 雙擊w，產生清音"w̆"</p>
<p> examples: ww > w̆ </p>
<p> typing rising and falling tone marks by adding "r" "q" and "x" 通過添加字母r、q咾x輸入上聲咾去聲音調符號 </p>
<p> examples: aq > á, ax > à. </p>
<p> automatically generate ông and w̆ by typing in Wu association romanization 使用吳語協會拼音輸入方式，自動產生ông咾w̆ </p>
<p> examples: aon > ông, ua > w̆a. </p>
<p> automatically predict “yi” combinatiön 自動預知“yi”搭配 </p>
<p> examples: ki > kyi, ni > nyi. </p>
<p> cancelling letter tranformation and tone marks by typing the tranformation key again</p>
<p> 再撳一記轉化鍵，取消字母轉化咾音調符號 </p>
<p> examples: aqq > aq, axx > ax, ooo > oo. </p>
