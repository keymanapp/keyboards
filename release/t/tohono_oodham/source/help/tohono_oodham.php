<h2>Help</h2>
<p>
This keyboard enables people to type in the Tohono O'odham language on their Android smartphone, Android tablet, iPhone, or iPad. Tohono O'odham is ISO639-3 language code OOD. It is spoken by about 15,000 people primarily in south-central Arizona, USA and northern Sonora, Mexico. This keyboard should work with any Unicode font.
</p>

<h2>Usage</h2>
<p>
Once you have installed the keyboard on your device (see documentation for <a href="https://help.keyman.com/products/iphone-and-ipad/10.0/">iOS</a> and <a href="https://help.keyman.com/products/android/10.0/">Android</a> devices) you will be able to use it to enter text. The keyboard layout is much like a normal qwerty keyboard, but certain keys support a long-press to activate a pop-up menu that selects alternative characters. Keys that support a long-press menu are marked with a dot in the upper right hand corner.
</p>
<p>
The main long-press keys used to type Tohono O'odham specific characters are:
<ul>
	<li><b>i</b> : menu allows input of i or ĭ</li>
	<li><b>s</b> : menu allows input of s or ṣ</li>
	<li><b>d</b> : menu allows input of d or ḍ</li>
	<li><b>n</b> : menu allows input of n, ñ, or ŋ</li>
</ul>
</p>

