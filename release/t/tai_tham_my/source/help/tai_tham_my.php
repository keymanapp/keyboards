		<p style="text-align: justify;">
		The Tai Tham MY keyboard was developed for typing Tai Lue language in Tai Tham script. The keyboard is available for computers as well as mobile devices.</p>

	<h2>Keyboard Layouts for Computer</h2>
		<p>There are 3 layouts in the keyboard map: “Default” layout; “Shift” layout and “Right Alt” layout.</p>
	<h3>Computer - Default</h3>
		<p>The Default layout is shown below.</p>
		<ol>
			<li>To type the character you want, press the appropriate key. For example, <kbd>A</kbd> gives <span class="taitham">“ᨼ”</span>; <kbd>B</kbd> gives <span class="taitham">“ᩥ”</span> etc.</li>
			<li>The vowels  <span class="taitham">ᩮ ᩯ ᩱ ᩰ </span> and the medial consonant <span class="taitham"> ᩕ</span> are typed after the desired consonant. For example, <span class="taitham">ᨠ + ᩱ </span> gives <span class="taitham"> ᨠᩱ </span></li>
			<li>The <span class="taitham"> ᩠ </span>character has a special function: it allows you to create a subjoined character. Having typed the  <span class="taitham">᩠</span> character, just type the character you want to be subjoined. For example, <span class="taitham">ᨴ +ᩱ + ᩠ + ᨿ </span>gives <span class="taitham"> ᨴᩱ᩠ᨿ </span> </li>
		</ol>
		<p><a href="desktop_layout_default.png"><img width="90%" class="keyboard" src="desktop_layout_default.png" alt="Default (unshifted) state" /></a></p>

	<h3>Computer - Shift</h3>
		<p>The Shift part is shown below. To type the character you want, press <kbd>Shift</kbd> + the appropriate key (at the same time). For example, <kbd>Shift</kbd> + <kbd>A</kbd> gives <span class="taitham">ᩀ</span>.</p>
		<p><a href="desktop_layout_shift.png"><img width="90%" class="keyboard" src="desktop_layout_shift.png" alt="Shift state" /></a></p>

	<h3>Computer - Right Alt</h3>
		<p>The Right Alt part is shown below. To type the character you want, press Right <kbd>Alt</kbd> + the appropriate key (at the same time). For example, Right <kbd>Alt</kbd> + <kbd>1</kbd> gives <span class="taitham">᪑</span>.</p>
		<p><a href="desktop_layout_altgr.png"><img width="90%" class="keyboard" src="desktop_layout_altgr.png" alt="AltGr state" /></a></p>


