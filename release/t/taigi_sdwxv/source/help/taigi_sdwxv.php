<p>
    This IME selected "sdwxv" for representing tone marks
</p>

<p>
    rules: <br>
    <li> 2=s, 3=d, 5=w, 7=x, 8=x, 9=v, ⁿ=nn, o͘=oo</li>
    examples: <br>
    <li> as > á, ad > à, ax > ā, aw > â, ahx > a̍h</li>
    shortcut: <br>
    <li> aa > â, ee > ê, uu > û, ii > î</li>
    doubletaping leads to generate original letters: <br>
    <li> example: fix = fixx, did = didd, kann = kannn, law = laww, bee = beee

</p>

