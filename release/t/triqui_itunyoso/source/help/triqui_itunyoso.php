<h2>Instrucciones para el Teclado de Triqui de Itunyoso</h2>

<p>
   Escribiendo con el Teclado de Triqui de Itunyoso:
   </p>
   <p>
El Teclado de Triqui de Itunyoso es similar al teclado de un teléfono móvil en español. Las principales diferencias son la disposición de las teclas y la forma de escribir los acentos. Triqui no usa todas las letras del alfabeto español y algunos sonidos triqui se escriben usando más de una letra y las claves se cambian para reflejar eso. Triqui también usa tanto el acento agudo como el acento grave, y usa muchos más acentos que el español, por lo que hay algunas formas de escribir acentos más rápidamente. El Teclado de Triqui de Itunyoso también cuenta con texto predictivo de palabras triqui.
</p>
<p>
Cada clave corresponde a un fonema en itunyoso triqui. Algunos fonemas como /kw/, /ng/, /ch/, /chr/ se escriben usando más de una letra y al presionar esas teclas se ingresarán dos o tres letras. Por ejemplo, al presionar [kw] se escribirá /kw/.
</p>
<h2>
Consonantes:
</h2>
<p>
No todos los fonemas consonánticos tienen claves propias; algunos se escriben utilizando varias teclas.
</p>
<p>
Las consonantes glotalizadas (hr, hy, hl, hb, hn, hm, hng) se escriben simplemente escribiendo [h] y luego la consonante. Por ejemplo, /hng/ se escribe usando [h][ng]. También se pueden encontrar manteniendo pulsada [h], o la tecla de consonante normal correspondiente (por ejemplo, [ng]).
</p>
<p>
Las consonantes dobles (kkw, tt, yy, kk, ll, cch, cchr, bb, nn, mm) se escriben tocando dos veces la consonante simple. En particular, /kkw/, /cch/ y /cchr/ se escriben usando [kw][kw], [ch][ch], [chr][chr]. También se pueden encontrar consonantes dobles manteniendo presionada la tecla de consonante normal correspondiente.
</p>
<p>
No hay ninguna clave para /cn/; Se puede acceder a /cn/ manteniendo presionado [n] o usando el acceso directo [ch][n].
</p>
<p>
No hay ninguna clave para /ngw/; Se puede acceder a /ngw/ manteniendo presionado [ng] o usando el acceso directo [ng][kw].
</p>
<p>
El teclado no tiene teclas para /q/, /w/, /d/, /g/, /z/, /c/ o /v/, ya que esas letras no se utilizan para los fonemas triqui. Puede acceder a ellos manteniendo presionado o tocando dos veces el botón del globo [🌐], a la izquierda de la barra espaciadora, para cambiar a una distribución de teclado QWERTY. Luego puede volver al teclado Triqui tocando el mismo botón nuevamente.
</p>
<p>
 
</p>
<h2>
Vocales:
</h2>
<p>
Hay cuatro formas de escribir vocales con acentos.
</p>
<p>
En primer lugar, se puede acceder a las vocales con tilde manteniendo pulsada la tecla vocal correspondiente. Esto mostrará la versión de acento agudo y grave de la vocal, así como los tonos de contorno de dos letras. Por ejemplo, mantener presionado [a] le permitirá acceder a [à][á][áa][aa][aà][a].
 
</p>
<p>
También puedes introducir acentos agudos y graves deslizando hacia arriba o hacia abajo la tecla de vocales. Desliza hacia arriba sobre [a] para [á], desliza hacia abajo sobre [a] para [à].
</p>
<p>
En segundo lugar, las vocales con acento se pueden escribir usando las teclas de acento agudo [´] y grave [`] en el lado derecho del teclado. Para escribir una vocal acentuada, escriba la clave muerta y luego la vocal. Por ejemplo, [`][a] da /à/.

</p>
<p>
En tercer lugar, está el texto predictivo. Si escribe una palabra sin acentos, como [kw][e][n][t][a][a], el texto predictivo sugerirá los patrones de acento más comunes para esa palabra. Para palabras comunes, a menudo puedes escribirlas sin acentos y confiar en el texto predictivo para ingresar los acentos.
</p>
<p>
 
En cuarto lugar, existe el "modo atrás", que puede cambiar los acentos de las vocales de palabras que ya se han escrito. Este modo es útil cuando se utiliza texto predictivo para palabras menos comunes (ver más abajo). Después de escribir una palabra, antes de presionar la barra espaciadora, presione [Shift] para ingresar al modo hacia atrás. Luego, puedes usar las teclas de acento agudo y grave, así como la tecla "sin acento" [◌] en la esquina superior derecha, para asignar acentos a cada vocal.
 
</p>
<p>
Después de presionar mayús, presione la tecla de acento correspondiente al tono deseado en la primera letra vocal, o si no tiene acento, presione [◌] para omitirlo. Luego haz lo mismo con la segunda vocal y así sucesivamente con cada vocal de la palabra. Cuando hayas asignado un acento (o ningún acento) a la última vocal, el teclado volverá a estar en minúsculas, indicando que a todas las vocales se les han asignado sus acentos. Por ejemplo, /kínihìnj/ se puede escribir [k][i][n][i][h][i][n][j] [shift] [`][◌][´].
</p>
<p>
</p>
<p>
Es importante utilizar la tecla [◌] en modo atrás; de lo contrario, el teclado no sabrá qué vocales omitir. Si quisiera escribir /kubìj/, y simplemente escribiera [k][u][b][i][j] [shift] [`], obtendría /kùbij/, ya que el teclado no sabría omitir la /u/. En su lugar, debería escribir [k][u][b][i][j] [shift] [◌][`].
</p>
<p>
Para las vocales de tono de contorno que utilizan dos letras vocales, deberá asignar un acento (o ningún acento) a cada letra individualmente.
</p>
<p>
Por ejemplo, quiero escribir /kwentàa/, escribo [kw][e][n][t][a][a] [shift] [◌][`][◌].
</p>
<p>
Para escribir vocales nasales o vocales con /j/ o /h/, simplemente escriba la vocal seguida de la tecla [n], [j] o [h].
</p>
<p>
También puedes ingresar acentos en "modo atrás" tocando dos veces las teclas de acento agudo y grave, o deslizando hacia arriba y hacia abajo la tecla "sin acento" (desliza hacia arriba para acento agudo, hacia abajo para acento grave).

</p>
<h2>
Texto predictivo:
</h2>
<p>
A medida que escribe una palabra, el teclado sugerirá automáticamente palabras similares. Para usar una sugerencia, tóquela en la barra sobre el teclado. El teclado sugerirá primero las palabras más comunes que coincidan con lo que ha escrito hasta ahora.
</p>
<p>
(Se insertará automáticamente un espacio después de la palabra. Si desea escribir puntuación, puede presionar la tecla de puntuación y se colocará automáticamente antes del espacio).
</p>
<p>
El texto predictivo ignora los acentos; /a/, /à/ y /á/ se tratan de la misma manera como sugerencia. Si escribe [kw][e], el texto predictivo sugerirá todas las palabras que comiencen con /kwe/, /kwè/ y /kwé/.
</p>
<p>
Por este motivo, puede escribir palabras con vocales sin acento y el texto predictivo sugerirá patrones de acento comunes para esas palabras. Hay dos situaciones en las que es posible que desee utilizar el "modo atrás" (ver arriba) para asignar acentos con texto predictivo:
</p>
<p>
Primero, si escribe una palabra completa, pero no se sugiere el patrón de acento correcto. En este caso, puedes utilizar el modo atrás para asignar acentos a cada vocal.
</p>
<p>
Por ejemplo, quiero escribir /kuù/. Si escribo [k][u][u], el texto predictivo sugerirá /kùhnàj/, /kùyàh/ y /kuyanj/, pero no /kuù/. Entonces, escribo [shift][◌][`], y el /kuu/ que ya escribí se convierte a /kuù/.
</p>
<p>
En segundo lugar, si escribe parte de una palabra, el modelo puede sugerir una palabra completa con las letras correctas pero con los acentos incorrectos. En este caso, puedes tocar esa palabra y modificar los acentos a los de la palabra que deseas usar.
</p>
<p>
Por ejemplo, quiero escribir /kìhyáj/. Escribo [k][i][h] y el texto predictivo sugiere /kihyaj/, /kìhyaj/ y /kihyòh/. Selecciono /kihyaj/ y luego escribo [shift][`][´]. Se asignan los acentos y obtengo /kìhyáj/.
</p>
<p>
(También podría haber seleccionado /kìhyaj/ [shift][`][´] y el resultado sería el mismo; los acentos en la sugerencia se sobrescriben completamente en modo atrás, por lo que solo importan las letras).
</p>
<h2>
Diseño QWERTY:
</h2>
<p>
Además de la distribución Triqui, el teclado también se puede cambiar a una distribución de teclado QWERTY. Para cambiar de diseño, mantenga presionado o tocando dos veces el botón del globo [🌐], a la izquierda de la barra espaciadora. Para volver al diseño Triqui, presione el botón [kwerty].
</p>
<p>
Este teclado solo tiene texto predictivo triqui y sugerirá palabras triqui incluso en la capa QWERTY. Para volver al español, presione el botón del globo [🌐] una vez. Esto volverá al teclado predeterminado del teléfono.

</p>

<h1>Itunyoso Triqui Keyboard Instructions</h1>

<p>
Typing with the Itunyoso Triqui Keyboard:
</p>
<p>
The Itunyoso Triqui Keyboard is similar to a Spanish mobile phone keyboard. The main differences are the layout of the keys and the way accents are typed. Triqui does not use all the letters of the Spanish alphabet and some Triqui sounds are typed using more than one letter and the keys are changed to reflect that. Triqui also uses both the acute and grave accents, and uses many more accents than Spanish, so there are a few ways to type accents more quickly. The Itunyoso Triqui Keyboard also features predictive text for Triqui words.
</p>
<p>
Each key corresponds to a phoneme in Itunyoso Triqui. Some phonemes such as /kw/, /ng/, /ch/, /chr/ are written using more than one letter and pressing those keys will enter two or three letters. For example, pressing [kw] will type /kw/.
</p>
<h2>
Consonants:
</h2>
<p>
Not all consonant phonemes have keys of their own; some are written using multiple keys.
</p>
<p>
Glottalized consonants (hr, hy, hl, hb, hn, hm, hng) are written by simply typing [h] and then the consonant. For example, /hng/ is written using [h][ng]. They can also be found by holding down [h], or the corresponding regular consonant key (e.g. [ng]).
</p>
<p>
Double consonants (kkw, tt, yy, kk, ll, cch, cchr, bb, nn, mm) are written by tapping the single consonant twice. In particular, /kkw/, /cch/, and /cchr/ are written using [kw][kw], [ch][ch], [chr][chr]. Double consonants can also be found by holding down the corresponding regular consonant key.
</p>
<p>
There is no key for /cn/; /cn/ can be accessed by holding down [n] or by using the shortcut [ch][n].
</p>
<p>
There is no key for /ngw/; /ngw/ can be accessed by holding down [ng] or by using the shortcut [ng][kw].
</p>
<p>
The keyboard does not have keys for /q/, /w/, /d/, /g/, /z/, /c/, or /v/, as those letters are not used for Triqui phonemes. You can access them by holding down or double-tapping the globe button [🌐], to the left of the space bar, to switch to a QWERTY keyboard layout. You can then switch back to the Triqui keyboard by tapping the same button again.
</p>
<p>

</p>
<h2>
Vowels:
</h2>
<p>
There are four ways to type accented vowels.
</p>
<p>
First, accented vowels can be accessed by holding down the corresponding vowel key. This will display the acute and grave accent version of the vowel, as well as the contour tones of two letters. For example, holding down [a] will allow you to access [à][á][áa][aa][aà][a].

</p>
<p>
You can also enter acute and grave accents by swiping up or down on the vowel key. Swipe up on [a] for [á], swipe down on [a] for [à].
</p>
<p>
Second, accented vowels can be typed using the acute [´] and grave [`] accent keys on the right side of the keyboard. To type an accented vowel, type the dead key and then the vowel. For example, [`][a] gives /à/.

</p>
<p>
Third, there is predictive text. If you type a word without accents, such as [kw][e][n][t][a][a], predictive text will suggest the most common accent patterns for that word. For common words, you can often type them without accents and rely on predictive text to enter the accents.
</p>
<p>

Fourth, there is "backward mode," which can change the accents on vowels in words that have already been typed. This mode is useful when using predictive text for less common words (see below). After typing a word, before pressing the space bar, press [Shift] to enter backward mode. You can then use the acute and grave accent keys, as well as the "no accent" [◌] key in the upper right corner, to assign accents to each vowel.

</p>
<p>
After pressing shift, press the accent key corresponding to the desired pitch on the first vowel letter, or if it has no accent, press [◌] to skip it. Then do the same for the second vowel, and so on for each vowel in the word. When you've assigned an accent (or no accent) to the last vowel, the keyboard will revert to lowercase, indicating that all vowels have been assigned their accents. For example, /kínihìnj/ can be written as [k][i][n][i][h][i][n][j] [shift] [`][◌][´].
</p>
<p>
</p>
<p>
It's important to use the [◌] key in backspace mode; otherwise, the keyboard won't know which vowels to skip. If you wanted to type /kubìj/, and you simply typed [k][u][b][i][j] [shift] [`], you'd get /kùbij/, since the keyboard wouldn't know to skip the /u/. Instead, you'd type [k][u][b][i][j] [shift] [◌][`].
</p><p>
For contour tone vowels that use two vowel letters, you will need to assign an accent (or no accent) to each letter individually.
</p>
<p>
For example, I want to type /kwentàa/, I type [kw][e][n][t][a][a] [shift] [◌][`][◌].
</p>
<p>
To type nasal vowels or vowels with /j/ or /h/, simply type the vowel followed by the [n], [j], or [h] key.
</p>
<p>
You can also enter accents in "back mode" by double-tapping the acute and grave accent keys, or by swiping up and down on the "no accent" key (swipe up for acute accent, down for grave accent).

</p>
<h2>
Predictive text:
</h2>
<p>
As you type a word, the keyboard will automatically suggest similar words. To use a suggestion, tap it in the bar above the keyboard. The keyboard will first suggest the most common words that match what you've typed so far.
</p>
<p>
(A space will automatically be inserted after the word. If you want to type punctuation, you can press the punctuation key and it will automatically be placed before the space.)
</p>
<p>
Predictive text ignores accents; /a/, /à/, and /á/ are treated the same as a suggestion. If you type [kw][e], predictive text will suggest all words that begin with /kwe/, /kwè/, and /kwé/.
</p>
<p>
For this reason, you can type words with unaccented vowels and predictive text will suggest common accent patterns for those words. There are two situations where you might want to use "back mode" (see above) to assign accents with predictive text:
</p>
<p>
First, if you type a whole word, but the correct accent pattern is not suggested. In this case, you can use back mode to assign accents to each vowel.
</p>
<p>
For example, I want to type /kuù/. If I type [k][u][u], predictive text will suggest /kùhnàj/, /kùyàh/, and /kuyanj/, but not /kuù/. Then, I type [shift][◌][`], and the /kuu/ I already typed is converted to /kuù/.
</p>
<p>
Second, if you type part of a word, the model may suggest a whole word with the correct letters but the wrong accents. In this case, you can tap that word and change the accents to those of the word you want to use.
</p>
<p>
For example, I want to type /kìhyáj/. I type [k][i][h] and predictive text suggests /kihyaj/, /kìhyaj/, and /kihyòh/. I select /kihyaj/ and then type [shift][`][´]. The accents are assigned and I get /kìhyáj/.
</p>
<p>
(I could have also selected /kìhyaj/ [shift][`][´] and the result would be the same; the accents in the suggestion are completely overwritten in backspace mode, so only the letters matter.)
</p>
<h2>
QWERTY layout:
</h2>
<p>
In addition to the Triqui layout, the keyboard can also be changed to a QWERTY keyboard layout. To switch layouts, long press or double tap the globe button [🌐], to the left of the space bar. To return to the Triqui layout, press the [kwerty] button.
</p>
<p>
This keyboard only has Triqui predictive text and will suggest Triqui words even on the QWERTY layer. To return to Spanish, press the globe button [🌐] once. This will return to the phone's default keyboard.

</p>
