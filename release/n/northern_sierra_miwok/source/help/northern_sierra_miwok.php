<h1>Keyboard Layout</h1>

<p>The Northern Sierra Miwok alphabet uses a number of special symbols that are not available on a standard keyboard.</p>

<p>With the Northern Sierra Miwok Keyboard enabled, you can type these symbols on a physical keyboard device by holding RIGHT ALT/OPTION or RIGHT ALT/OPTION+SHIFT and pressing the corresponding key.</p>

