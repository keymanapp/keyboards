This is a basic keyboard with with little modification to accomodate the Nias characters ö and ŵ. Typing [ produces ö and ] gets you ŵ. To produce the []{} characters please use ALT-RIGHT and ALT-RIGHT+SHIFT combinatio.<br /><br />

Ini adalah keyboard umum dengan huruf ö dan ŵ. Tekan tombol [ untuk menghasilkan huruf ö dan ] menghasilkan huruf ŵ. Untuk menghasilkan tanda [ dan ] tekan tombol ALT-Kanan+[ dan ALT-Kanan+]. Demikian juga untuk menghasilkan { dan } tekan tombol SHIFT+ALT-Kanan+[ atau ].
