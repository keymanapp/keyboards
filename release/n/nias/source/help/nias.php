<p>
This is a basic keyboard with little modification to accomodate the Nias characters ö and ŵ. Typing [ will produce ö and ] will produce ŵ. To type the []{} characters please use ALT-RIGHT and ALT-RIGHT+SHIFT combination.<br /><br />

Ini adalah keyboard umum dengan huruf ö dan ŵ. Tekan tombol [ untuk menghasilkan huruf ö dan ] menghasilkan huruf ŵ. Untuk menghasilkan tanda [ dan ] tekan bersama-sama tombol ALT-KANAN+[ dan ALT-KANAN+]. Demikian juga untuk menghasilkan { dan } tekan bersama-sama tombol SHIFT+ALT-KANAN+[ atau ].
