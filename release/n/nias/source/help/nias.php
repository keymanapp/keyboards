<p>This is a basic keyboard with little modification to accomodate the Nias characters ö and ŵ.</p>

<p>On PC: press together CTRL-RIGHT+o to produce ö and CTRL-RIGHT+w to produce ŵ. Add SHIFT to that to get the capital letters.</p>

<p>On tablets/smartphones: ö and ŵ keys are already available on the keyboard.</p>

<p>Di laptop/desktop: tekan bersama-sama tombol CTRL-KANAN+o untuk mengetik ö dan tombol CTRL-KANAN+w untuk menghasilkan ŵ. Tambahkan SHIFT ke kombinasi tsb. untuk mengetik huruf besar.</p>

<p>Di tablet/smartphone: huruf ö dan ŵ telah tersedia di keyboard.</p>

