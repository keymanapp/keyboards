<p> Use the PERIOD key [.] to get a dot above/below or a ring below a character, e.g. &nbsp r [.] > ṛ &nbsp ṛ [.] > r̥ &nbsp n [.] > ṅ<br>ṅ [.] > ṇ &nbsp m [.] > ṁ &nbsp ṁ [.] > ṃ &nbsp t [.] > ṭ &nbsp g [.] > ġ etc.
<br>Use the HYPHEN key [-] to get a macron above or a line below a character or pair of characters, e.g. &nbsp a [-] > ā
<br>l [-] > ḻ &nbsp t [-] > ṯ &nbsp y [-] > ȳ &nbsp kh [-] > k͟h.
<br>Use the TILDE key [~] to get a tilde or Chandrabindu ̐ above a character, e.g. &nbsp a [~] > ã &nbsp n [~] > ñ &nbsp m [~] > m̐.
<br>Use the CARET key [^] to get a circumflex, caron, breve or inverted breve on a character, e.g. &nbsp e [^] > ê &nbsp o [^] > ô &nbsp r [^] > r̆ &nbsp h [^] > ḫ.
<br><b>All the transliteration symbols, except k͟h and K͟H may also be obtained by pressing [=] after typing a <br> base character, e.g. &nbsp h [=] > ḥ, &nbsp ḥ [=] > ẖ, &nbsp ẖ [=] > h̤, &nbsp h̤ [=] > ḫ.
</p>
