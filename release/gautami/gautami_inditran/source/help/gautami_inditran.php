<h2>Transliteration Keymap</h2>

<p><a href="translit.png"><span style='text-decoration:none'><img border=3 img width=95% src="translit.png" alt="Transliteration Keymap"></span></a></p>

<p>Use the PERIOD key [.] to get a dot above/below or a ring below a character, e.g. r&nbsp[.]&nbsp>&nbspṛ&nbsp, ṛ&nbsp[.]&nbsp>&nbspr̥&nbsp, n&nbsp[.]&nbsp>&nbspṅ&nbsp, ṅ&nbsp[.]&nbsp>&nbspṇ&nbsp, m&nbsp[.]&nbsp>&nbspṁ&nbsp, ṁ&nbsp[.]&nbsp>&nbspṃ&nbsp, t&nbsp[.]&nbsp>&nbspṭ&nbsp, g&nbsp[.]&nbsp>&nbspġ etc.</p>
<p>Use the HYPHEN key [-] to get a macron above or a line below a character or pair of characters, e.g. a&nbsp[-]&nbsp>&nbspā&nbsp, l&nbsp[-]&nbsp>&nbspḻ&nbsp, t&nbsp[-]&nbsp>&nbspṯ&nbsp, y&nbsp[-]&nbsp>&nbspȳ&nbsp, kh&nbsp[-]&nbsp>&nbspk͟h.</p>
<p>Use the TILDE key [~] to get a tilde or Chandrabindu ̐ above a character, e.g. a&nbsp[~]&nbsp>&nbspã&nbsp, n&nbsp[~]&nbsp>&nbspñ&nbsp, m&nbsp[~]&nbsp>&nbspm̐.</p>
<p>Use the CARET key [^] to get a circumflex, caron, breve or inverted breve on a character, e.g. e&nbsp[^]&nbsp>&nbspê&nbsp, o&nbsp[^]&nbsp>&nbspô&nbsp, r&nbsp[^]&nbsp>&nbspr̆&nbsp, h&nbsp[^]&nbsp>&nbspḫ.</p>
<p><b>All the transliteration symbols, except k͟h and K͟H, may also be obtained by pressing [=] after typing a base character, e.g. h&nbsp[=]&nbsp>&nbspḥ&nbsp, ḥ&nbsp[=]&nbsp>&nbspẖ&nbsp, ẖ&nbsp[=]&nbsp>&nbsph̤&nbsp, h̤&nbsp[=]&nbsp>&nbspḫ.</b></p>

<h2>Transcription Keymap</h2>

<p><b>To access IPA transcription symbols press [\] after typing any base character, e.g. a [\] > ə, &nbsp ə [\] > ɑ, &nbsp ɑ [\] > ä.</b></p>

<p><a href="transcrip.png"><span style='text-decoration:none'><img border=3 img width=95% src="transcrip.png" alt="Transcription Keymap"></span></a></p>
<p><b>Pressing down and holding the Right-Alt key switches the keyboard back to its original QWERTY configuration</b>, e.g. [o~]&nbsp>&nbspõ, but typing o and then holding down the Right-Alt key while typing ~ yields o~.</p>
