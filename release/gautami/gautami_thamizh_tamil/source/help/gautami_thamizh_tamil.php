<h2>Vowels</h2>

<p><a href="vowels.png"><span style='text-decoration:none'><img border=3 img width="95%" src="vowels.png" alt="Vowels"></span></a></p>

<p>The keys with superscripts are toggle keys. The superscript numbers indicate the position of the akṣara in the toggle cycle. Press each of these keys several times in a row to see how it works.</p>
<p>The key x serves three functions: (1) it breaks a toggle cycle and moves the cursor on to the next akṣara, e.g. ii&nbsp>&nbspஈ, but ixi&nbsp>&nbspஇஇ; (2) it prevents a Pulli from combining two contiguous akṣaras into a single composite akṣara, e.g. k.S&nbsp>&nbspக்ஷ but k.xS&nbsp>&nbspக்‌ஷ; and (3) on being pressed twice it inserts the composite akṣara க்ஷ at the cursor location, e.g. xx&nbsp>&nbspக்ஷ.</p>
<p><b>Pressing down and holding the Right-Alt key switches the keyboard back to its original QWERTY configuration</b>, e.g. [o]&nbsp>&nbspஒ, but hitting [o] while holding down the Right-Alt key yields o.</p>

