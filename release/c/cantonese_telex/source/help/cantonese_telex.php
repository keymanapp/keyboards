  <p>每個句子嘅第一個字母要大寫</p>
  <p>The first letter in a proper noun such as the name of a particular person and place should be capitalized</p>
  <p>專有名詞譬如話人同地方嘅第一個字母要大寫</p>
  <p>The first letter in each syllable of a Cantonese origin or Sino-cantonese origin proper noun such as the name of a person and a place should be capitalized</p>
  <p>粵語或者漢粵詞詞源嘅專有名詞嘅每一個音節嘅第一個字母要大寫</p>
