<p>Layout keyboard digital terdiri dari 3 layer: 
<ol>
<li>Default = Latin lowercase, angka Arab, alofon vokal Jawa, dan simbol paling umum;</li>
<li>Shift/Caps = Layout Standar Aksara Jawa layer 1; aksara-aksara di Layout Standar Aksara Jawa layer 2 dapat diakses melalui longpress (tekan tombol lama)</li>
<li>Right Alt = Latin uppercase dan simbol lainnya</li>
</ol></p>
