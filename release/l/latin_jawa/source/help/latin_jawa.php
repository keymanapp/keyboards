<h2>Layout Huruf Latin dan Aksara Jawa</h2>
<p>&nbsp;</p>
<p>Layout Huruf Latin dan Layout Standar Aksara Jawa hasil keputusan KAJ I 24 Maret 2021</p>
<p>Dirancang dengan tujuan sebagai pembelajaran / adaptasi untuk menggunakan layout standar aksara Jawa. Diharapkan layout ini dapat menjembatani para pengguna layout QWERTY untuk dapat mengingat-ingat letak aksara Jawa yang diletakkan di layer Shift. (Dengan beberapa catatan)</p>
<p>Definisi layout keyboard standar berdasar: <a href="https://docs.google.com/document/d/1whGh6a1spMo8gBCw5x4AKGu8-uoAYapbCIFC08Xq8-s">Definisi tata letak papan tombol standar Aksara Jawa sesuai hasil keputusan KAJ 1 24 Maret 2021</a>
<p>&nbsp;</p>
<p>Changelog:</p>
<p>Versi 1.0:</p>
<p>Layout keyboard fisik terdiri dari 4 layer: 
<ol>
<li>Default = Latin lowercase, angka Arab, alofon vokal Jawa, dan simbol paling umum;</li>
<li>Shift/Caps = Layout Standar Aksara Jawa layer 1;</li>
<li>Right Alt = Layout Standar Aksara Jawa layer 2;</li>
<li>Shift+Right Alt / Caps+Right Alt = Latin uppercase dan simbol lainnya</li>
</ol></p>
