<p>Kedua layout telah diselaraskan. Perbedaannya hanya terletak pada keyboard digital, yang dapat ditambahi fungsi longpress (keyboard fisik tidak), sehingga dapat menghemat satu layer, yang tersembunyi (tidak tertampil),  namun semua aksara Jawa dapat dihasilkan semua.</p>
<p>Yang perlu diperhatikan
<ul>
<li>Semua tombol aksara nglegena dan sandhangan swara di layer shift menjadi huruf Latinnya di layer default</li>
<li><b>PENTING</b>: Posisi huruf Latin 'a' dan 'ng' di layout fisik layer default dibalik! ( = susunan NgWERTY. Huruf 'ng' tidak kompatibel dengan posisi aksara Nga di layer shift, tapi huruf 'a' dan 'A' kompatibel dengan posisi aksara 'A-kara'). Hal ini disebabkan posisi kelingking kiri lebih sering mengetikkan huruf 'a' di keyboard fisik. Namun posisi di layout digital layer default tidak dibalik ( = susunan AWERTY. Huruf 'ng' masih kompatibel dengan posisi aksara Nga di layer shift)</li>
<li>Fisik: Tombol 'pangkon' di layer shift menjadi Tombol 'ng' di layer default. Tombol 'aksara nga' di layer shift menjadi Tombol 'a' di layer default.<br />
Digital: Tombol 'pangkon' di layer shift menjadi Tombol 'a' di layer default</li>
<li>Tombol 'pepet' di layer shift menjadi Rotasi Tombol 'z', 'x', dan 'q' di layer default.</li>
<li><small>Rotasi tombol adalah tombol yang dapat diketik/dipencet berulang kali untuk menghasilkan hasil yang berbeda</small></li>
<li>Tombol 'pada lingsa' menjadi Rotasi Tombol ',', '?', dan '!'</li>
<li>Tombol 'pada adeg-adeg' menjadi Rotasi Tombol '/', '(', dan ')'</li>
<li>Tombol 'cecak' menjadi Tombol 'è' (alofon bunyi taling)</li>
<li>Tombol 'layar' menjadi Tombol 'é' (alofon bunyi taling)</li>
<li><small>Catatan: Tombol 'e' di layer default menandakan alofon suara Pepet (tidak kompatibel dengan tombol Taling di layer shift). Sementara untuk suara Taling, silakan pilih Tombol è atau é</small></li>
<li>Tombol fungsi PV1 menjadi Tombol Rotasi ', :, dan "</li>
<li>Tombol 'pada pangkat' menjadi Tombol Rotasi å, ɔ, (alofon bunyi a jejeg) atau ā (bunyi a miring)</li>
<li>Tombol 'cecak telu' menjadi Tombol Rotasi í dan ɪ (alofon bunyi i miring)</li>
<li>Tombol 'panyangga' menjadi Tombol Rotasi ū dan ʊ (alofon bunyi u miring)</li>
<li>Mayoritas simbol Latin di layer Latin uppercase kompatibel dengan layout keyboard QWERTY, namun ada beberapa perkecualian yang perlu dipelajari ulang, misalnya simbol ` - = { }. Tombol simbol \ dan | merupakan Rotasi Tombol.</li>
<li>Catatan untuk beberapa longpress: bagi para pengguna layout jawa-standar, ada beberapa perbedaan minor di pilihan longpress dengan layout latin-jawa di tombol deret angka, dan tombol-tombol paling kanan di baris 2, 3, dan 4.</li>
<li>Catatan untuk CAPSLOCK: "Layer" Caps Lock mirip dengan layer Shift, namun hanya meliputi 26 tombol saja (huruf A-Z di layout QWERTY). Selain dari 26 tombol itu harus diketik menggunakan Shift. Hal yang sama untuk Caps Lock + Right Alt.</li>
</ul>
</p>

