<p>
    To add a Lamkaang acute accent or dot to any letter, after typing the letter, press the backtick (`) key, normally at the top left corner of the keyboard.</p>
<p>e.g. Typing <strong>a`</strong> produces <strong>á</strong>. Typing<strong> t`</strong> produces<strong> ṭ</strong>.</p>
<p>To add the ring accent mark, press the backtick key twice. </p>
<p>e.g. Typing <strong>u``</strong> produces <strong>ů</strong>.</p>
