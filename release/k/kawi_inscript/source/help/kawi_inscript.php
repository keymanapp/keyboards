<p>For ease in typing, the RALT and RALT+SHIFT layers produce <i>conjunct</i> forms (conjoiner plus consonant). The <i>conjoiner</i> can also be typed separately on the "\" key.</p>

<p>This keyboard is distributed with the <b>Noto Sans Kawi</b> font. Although the keyboard supports a <i>nukta</i> (on the "]" key) the font does not have a <i>nukta</i> character. It will display as a square box.</p>

