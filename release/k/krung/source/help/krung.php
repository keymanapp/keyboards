<h2>Presyllable Configuration</h2>
<p>
Words with a presyllable can be typed in one of the following ways. Presyllables
are underlined.
</p>
<ul>
<li>Consonant + Vowel<br>
<span style="text-decoration:underline;">ca</span>caa		> ចចា		‘to eat’<br>
<span style="text-decoration:underline;">ga</span>nvt		> ឝនឹត		‘valuable’
<li>Consonant + Consonant + Vowel<br>
<span style="text-decoration:underline;">cr</span>heew		> ច្រហើវ	‘young unmarried man’<br>
<span style="text-decoration:underline;">gra</span>ngzt	> ឝ្រង៉ត់	‘sad’
<li>“h” or “q” + nasal<br>
<span style="text-decoration:underline;">hm</span>puet	> ហ្មពួត		‘to sell<br>
<span style="text-decoration:underline;">qm</span>bvng	> អ្មប៊ឹង		‘area behind house’
</li>
</ul>
