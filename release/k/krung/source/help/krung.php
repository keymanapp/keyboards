<h2>Consonant</h2>
<p>
Not all consonants in Krung language are mapped to a key. Certain consonants are mapped, while
others are automatically converted to their corresponding counterparts. Krung
language uses Khmer characters for their writing. Unicode codepoints and names
are given for your information.
