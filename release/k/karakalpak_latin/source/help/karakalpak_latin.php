<h2>Desktop Keyboard Layout</h2>

<p>This keyboard utilizes a deadkey on the backslash key (\). To get the desired character, press the deadkey
    and the character to the next shown in Key combination row in the below table. 
</p>
<p>For example, if you want to get letter 'á', you need to first type '\', then 'a'.</p>

<p>To access the "deadkey" itself (\), type a space after it, or double tap it.</p>

