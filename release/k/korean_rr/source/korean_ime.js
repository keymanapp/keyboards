/* Test if a selection key has been pressed, or space (to convert the jamo to hangul) */
this.selectHanja = function(t,e)
{
  var k;
  if(this.suggestions == null) return false;
  
  var ei=KeymanWeb.KKI(e),x,c=KeymanWeb.KC(12,12,t),cLen,k,g;
  if(ei.code > 96 && ei.code < 106) x = ei.code-97; else x = ei.code - 49;
  if(x >= 0 && x < this.suggestions.length)
  {
    h = this.suggestions.charAt(x+this.offset);
    g = this.tips[x+this.offset];
    /* Find the part of the context matching hangul for the selected list entry */
    cLen=c.length;
    for(k=1; k<=cLen; k++)
    {
      if(this.getHangul(c.substr(cLen-k)) == g) break;
    }  
    if(k <= cLen) KeymanWeb.KO(k,t,h);
    this.hide();
  }  
  return true;
}
/* Display the IME with a list of hanja suggestions for (up to four) hangeul */
/* h is a string of hanja characters, g is an array of strings with the hangeul for each hanja */
this.showIME = function(t)
{
  /* Get the pending jamo and at most three more hangul, and convert to a hangul string */
  this.hangul=this.getHangul(KeymanWeb.KC(12,12,t)); 

  /* List of hanja for last four hangul, last three, last two, then final hanja */ 
  if(this.hangul != '') 
  {    
    var h='',g=[],y,z,j=0;
    for(var k=this.hangul.length; k>0; k--)
    {
      y = this.hangul.substr(this.hangul.length-k); z = this.hanja[y];
      if(z != null) 
      { 
        h += z;
        for(var i=0; i<z.length; i++,j++) g[j] = y; 
      }
    }      
    this.show(g,h);
  }
  else
  {
    this.hide();
  }
  return true;
}
  
this.hideIME = function(t)
{
  this.hide();
  return true;
}  

/* Convert an arbitrary string of hangul and/or jamo to (up to 4) hangul */ 
this.getHangul = function(s)
{
  var sLen = s.length,j,jLen,h,q;
  if(sLen < 2) return '';
  
  j = this.getJamo(s);
  jLen = j.length;
  if(jLen > 3) j = j.substr(jLen-3);
  jLen = j.length;
  h = this.convertJamo(j);
  if(h == '') return '';
  for(var i=0; i<3; i++)
  {
    q = s.charCodeAt(sLen-jLen-i-1);
    /* Test if preceding character is hangul */
    if(q < 0xAC00 || q > 0xD7A3) break;
    h = s.charAt(sLen-jLen-i-1) + h;
  }
  return h;
}

/* Extract up to three jamo from the end of a string */      
this.getJamo = function(s) 
{
  var x,i,sLen=s.length;
  for(i=1;i<=sLen && i<4;i++)
  {
    x = s.charCodeAt(sLen-i);
    if(x < 0x1100 || x > 0x11ff) break;
  }
  if(i > 1) return s.substr(sLen-i+1); else return '';
}      

/* Convert 2 or 3 jamo to hangul */  
this.convertJamo = function(c)
{
  var n=0,TIndex=0,VIndex,LIndex,LBase=0x1100,VBase=0x1161,TBase=0x11A7,SBase=0xAC00,LCount=19,VCount=21,TCount=28;

  if(c.length < 3) n = 0;
  else if(c.charCodeAt(2) >= TBase && c.charCodeAt(2) < TBase+TCount) TIndex = c.charCodeAt(2) - TBase;
	else n = 1;

	if(c.charCodeAt(n) >= LBase && c.charCodeAt(n) < LBase+LCount &&
	   c.charCodeAt(n+1) >= VBase && c.charCodeAt(n+1) < VBase+VCount)
	{
		LIndex = c.charCodeAt(n) - LBase;
		VIndex = c.charCodeAt(n+1) - VBase;

		return String.fromCharCode((LIndex * VCount + VIndex) * TCount + TIndex + SBase);
	}
  return '';
}

/* Same function, called directly from keyboard.  Note that if matched length is less than two, it will simply output a space */
this.jamoCombine = function(t)
{
  var k=KeymanWeb,c=k.KC(3,3,t),n=0,TIndex=0,VIndex,LIndex,LBase=0x1100,VBase=0x1161,TBase=0x11A7,SBase=0xAC00,LCount=19,VCount=21,TCount=28,cLen=3;
  if(c.length < 2) { /* k.KO(0,t,' '); */ return true; }
  cLen = c.length;
  if(c.length < 3) n = 0; 
  else if(c.charCodeAt(2) >= TBase && c.charCodeAt(2) < TBase+TCount) TIndex = c.charCodeAt(2) - TBase;
  else { n = 1; cLen--; }
  
  if(c.charCodeAt(n) >= LBase && c.charCodeAt(n) < LBase+LCount &&
  c.charCodeAt(n+1) >= VBase && c.charCodeAt(n+1) < VBase+VCount)
  {
    LIndex = c.charCodeAt(n) - LBase;
    VIndex = c.charCodeAt(n+1) - VBase;
    
    syl = String.fromCharCode((LIndex * VCount + VIndex) * TCount + TIndex + SBase);
    k.KO(cLen,t,syl);
  }
  else { /* k.KO(0,t,' '); */ return true; }
  this.hide();
  return true;  
}

