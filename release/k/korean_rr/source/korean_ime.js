this.clickCallback = function(elem)
{
  var t = window.keyboard_Korean_RR;
  for(var i=0; i < 11; i++)
    if(t.boxes[i] == elem)
    {
      if(i == 0)
      {
        t.offset = t.offset - 9;
        if(t.offset < 0) t.offset = 0;
        t.renderSuggestions(t.IME);
      }
      else if(i == 10)
      {
        t.offset = t.offset+9;
        if(t.offset >= t.suggestions.length) t.offset = t.suggestions.length-1;
        this.renderSuggestions(t.IME);
      }
      else
      { 
        t.insertCallback(t.tips[i+t.offset-1],t.suggestions.charAt(i+t.offset-1),elem);
      }
      break;        
    }
};  


this.scrollHanja = function(t,e)
{
  var ei=KeymanWeb.KKI(e);
  if(this.suggestions == null) return false;
  if(this.suggestions.length == 0) return false;
  if(ei.code == 34)
  {
    this.offset = this.offset + 9;
    if(this.offset > this.suggestions.length) this.offset = this.suggestions.length-1; 
  }
  else if(ei.code == 33)  
  {
    this.offset = this.offset - 9;
    if(this.offset < 0) this.offset = 0;
  }
  this.renderSuggestions(this.IME);

  return true;
}

this.insertCallback = function(tip,chr,elem)
{ 
  var c,cLen,j,k=window.keyboard_Korean_RR,a;

  if(chr != null) 
  { 
    a = KeymanWeb.GetLastActiveElement();        
    KeymanWeb.KT(null);    /* KT will refocus the active element in the case of a mouse click */
    c = KeymanWeb.KC(12,12,a); 
    cLen = c.length;
 
    /* Find the part of the context matching hangul for the currently displayed list */
    for(j=1; j<=cLen; j++)
    {
      if(k.getHangul(c.substr(cLen-j)) == tip) break;
    }
    if( j <= cLen) KeymanWeb.KO(j,a,chr); 
  }
  k.hide();
}

