<h1>The Osage Keyman Keyboard Package</h1>

<h2>Font</h2>
<p>
The Osage Keyman keyboard is designed to work any Osage script font. It comes with the Pawhuska
font. This and other Osage fonts are available from <A href="http://osagelanguagetools.appspot.com/downloads/" target=_blank>Osage Nation</A>. 
</p>


<h2>Keyboard</h2>
<p>
This keyboard layout is designed for the Osage language in the Osage script. It includes 
an on screen keyboard which can be viewed by clicking on the Keyman icon 
and selecting the On Screen Keyboard menu item.     </p>


