<h2>Touch screen keyboard </h2>
<p><b>Long press keys:</b> Touch and hold the key, more options will pop up for that letter with diacritics. For example, if you need a high tone ó, hold down the o button, or if you need a glottalized t̓, hold down the t button.</p>
<p><b>New keys:</b> Some of the letters that are not used in Ó tissi have been replaced with the aspirated letters, syllable dot and punctuation symbols.</p>


