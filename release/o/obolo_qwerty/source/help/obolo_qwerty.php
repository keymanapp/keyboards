<p>
Obolo Qwerty is a simple Unicode keyboard for the Obolo language, using the QWERTY layout.
<p>
This is an alternative to the Obolo Chwerty Keyboard. This layout is for those who prefer to type Obolo using the English (US) QWERTY keyboard layout.
<p>
 In this language, note that:
 <p>(1) You cannot place more than one accent on a letter.
 <p>(2) Some accents are optional and are not required. They are only used in primers and for showing pronounciations.
 <p>(3) Some letters do not accept some accents.
 <p>(4) Some letters do not accept accents at all. 
<p> 
Except for the optional accents which are not required, the keyboard will notify you when you inadvertently go against the spelling rules. 
<p>
