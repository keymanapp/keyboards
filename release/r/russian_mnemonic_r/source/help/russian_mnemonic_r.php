<p>
Russian Mnemonic R is a rewrite of the <a href="https://help.keyman.com/keyboard/basic_kbdrum/">Russian - Mnemonic Basic</a> keyboard with improved dead-keys processing, and enriched with special and math symbols.
</p>

<p style='margin: 16px 0 0 0'>
A font must be installed to support this keyboard. There are number of fonts which support the <b>Cyrillic</b> script.
</p>
<p>
If square boxes are displayed instead of characters when using this keyboard (and in the keyboard layouts below), please read our <a href="/troubleshooting/#boxes">troubleshooting guide</a>.
</p>

<p>
Visit <a href="https://github.com/dotland/mnemonic-kb-ru/blob/main/README.md">GitHub.com/dotland/mnemonic-kb-ru</a> for extensive documentation on using the keyboard.
</p>

<p>This keyboard uses eight dead keys, such as: <kbd class="key">S</kbd> <kbd class="key">s</kbd> <kbd class="key">C</kbd> <kbd class="key">c</kbd> <kbd class="key">J</kbd> <kbd class="key">j</kbd> <kbd class="key">\</kbd> <kbd class="key">/</kbd>, where <kbd class="key">S</kbd> is <kbd class="key">Shift</kbd>+<kbd class="key">s</kbd> or just <kbd class="key">s</kbd> with <kbd class="key">Caps Lock</kbd> turned on. Press one of these keys and then another key to produce the desired output. For instance, press <kbd class="key">S</kbd> and then the <kbd class="key">Space</kbd> to get cyrillic С; press <kbd class="key">S</kbd> and then <kbd class="key">h</kbd> to get cyrillic Ш etc. The complete combinations are as follows.</p>

