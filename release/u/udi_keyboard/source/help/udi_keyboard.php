<p>
This keyboard is designed for the <a
href="https://en.wikipedia.org/wiki/Udi_language">Udi</a> language of Azerbaijan, Georgia and Russia.
</p>

	<p>This keyboard makes use of modifier keys. Typing the modifier key before the base character will output the new or combined character. For example:</p>
	<p>[c (Open Bracket and c) will produce č (available on cC eE əƏ gG iİ jJ oO sS uU).</p>
	<p>]o (Close Bracket and o) will produce ö ( available on oO uU).</p>
	<p>]s (Close Bracket and s) will produce ś .</p>
	<p>`c (Back quote and c) will produce ç (available on sS).</p>
	<p>'c (Quote and c) will produce cʹ (available on cC kK qQ pP sS tT).</p>
	<p>&lt; and &lt; will produce «.</p>
	<p>&gt; and &gt; will produce ».</p>

<p>If square boxes are displayed instead of characters when using this keyboard (and in the keyboard layouts below), please read our <a
href="https://help.keyman.com/troubleshooting/#boxes">troubleshooting guide.</a> </p>
 
<p>
Эта клавиатура предназначена для языка <a
href="https://en.wikipedia.org/wiki/Udi_language">уди</a> Азербайджана, Грузии и России. 
</p>

<p>Эта клавиатура использует клавиши-модификаторы. Ввод клавиши-модификатора перед базовым символом приведет к выводу нового или комбинированного символа. Например:
</p>
	<p>[c (Открытая скобка и c) произведет č (доступно на cC eE əƏ gG iİ jJ oO sS uU).</p>
	<p>] o (Закрывающая скобка и o) будет производить ö (доступно на oO uU).</p>
	<p>] s (Закрывающая скобка и s) будет производить ś (доступно на sS).</p>
	<p>`c (обратная кавычка и c) будет производить ç (доступно на cC).</p>
	<p>‘c (Кавычка и c) будет производить cʹ (доступно на cC kK qQ pP sS tT).</p>
	<p>&lt; и &lt; будет производить «.</p>
	<p>&gt; и &gt; будет производить ».</p>

