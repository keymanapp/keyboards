<h2>Typing Nasal Vowels</h2>
<p>To type nasal vowels, press the tilde key "~" and then type "a, e, i, or u". The tilde should appear directly above the vowel.</p>
<br><br>

<h2>Typing Long Vowels</h2>
<p>To type long vowels, press the backtick key "`" and then type "a, e, i, or u". The accent should appear directly above the vowel.</p>
