<p>This keyboard consists of four layers. The above layout is structured as follows:</p>
<ul>
   <li>
      <b>Left Bottom :</b> Unshifted
   </li>
   <li>
      <b>Left Top :</b> Shifted
   </li>
   <li>
      <b>Right Bottom :</b> AltGr (Right Alt)
   </li>
   <li>
      <b>Right Top :</b> AltGr (Right Alt)+Shift
   </li>
</ul>
