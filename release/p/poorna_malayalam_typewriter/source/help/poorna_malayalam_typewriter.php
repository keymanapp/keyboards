<h2>Example</h2>
<p>In the case of key <kbd>d</kbd> : </p>
<ul>
   <li>
      <kbd>d</kbd> → ക (U+0D15 : MALAYALAM LETTER KA)
   </li>
   <li>
      <kbd>Shift</kbd> + <kbd>d</kbd> → ഖ (U+0D16 : MALAYALAM LETTER KHA)
   </li>
   <li>
      <kbd>AltGr</kbd> + <kbd>d</kbd> → ഽ (U+0D3D : MALAYALAM SIGN AVAGRAHA)
   </li>
   <li>
      <kbd>AltGr</kbd> + <kbd>Shift</kbd> + <kbd>d</kbd> → ഁ (U+0D01 : MALAYALAM SIGN CANDRABINDU)
   </li>
</ul>
