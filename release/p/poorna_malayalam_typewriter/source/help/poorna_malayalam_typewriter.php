<p>
   <b>Poorna Malayalam Typewriter</b> keyboard is a complete Malayalam unicode character set keyboard. The key mapping of this keyboard is an extended version of the Malayalam Typewriter layout. Visit <a href="https://poorna.smc.org.in" target="_blank">https://poorna.smc.org.in</a> for more information.
</p>
