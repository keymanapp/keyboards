<body>
<p>The Pukapuka keyboard has been designed to type the Pukapuka language of the Cook Islands of the South Pacific. 
It can be used on physical keyboards (laptops, etc.) as well as hand-held touch devices (such as smart phones, tablets, etc.).</p>

<h2>Touch Layout</h2>

<h3>Default layer</h3>

<p>Note that the darker punctuation keys ` ; ː and ^ provide a way of adding the
diacritics to vowels. So pressing <span style='color:blue'>e ;</span> would result
in <b><span style='font-size:14.0pt;mso-ansi-language:#0400'>ē</span></b><span
style='mso-ansi-language:#0400'> and pressing <span style='color:blue'>u :</span>
would result in </span><b><span style='font-size:14.0pt;mso-ansi-language:#0400'>ú</span></b><span
style='mso-ansi-language:#0400'> and so on.<o:p></o:p></span></p>

<p><img width=659 height=254 id="_x0000_i1029"
src="pukapuka_touch_layout_default.png"></p>

<h3>Shift layer</h3>

<p><img width=659 height=250 id="_x0000_i1030"
src="pukapuka_touch_layout_shift.png"></p>

<h3>Numeric layer</h3>

<p><img width=660 height=251 id="_x0000_i1031"
src="pukapuka_touch_layout_numeric.png"></p>

<p>The touch layout has some additional functionality of providing short-cuts
for commonly used combinations of characters. </p>

<p>For example, holding down (long-press) a vowel key will show various vowels
with the diacriticsː</p>

<p><img width=396 height=175 id="_x0000_i1027"
src="pukapuka_longpress_vowel_sample.png"></p>

<p>Similarly some consonants have long-press shortcuts to commonly-used
combinations of charactersː</p>

<p><img width=391 height=196 id="_x0000_i1028"
src="pukapuka_longpress_sample.png"></p>

