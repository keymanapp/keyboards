<p>
  This keyboard is designed for typing Kurdish Sorani Behdini in the Arabic script. It is based on a phonetic layout.
</p>

<h2>Desktop Keyboard Layout</h2>

<p>There are a few keys which are only available through two keystrokes:</p>
<ul>
  <li><b>RALT+COMMA</b> produces &#x203A;, and pressing <b>RALT+COMMA</b> a second time will produce &#x00BB;</li>
  <li><b>RALT+PERIOD</b> produces &#x2039;, and pressing <b>RALT+PERIOD</b> a second time will produce &#x00AB;</li>
  <li><b>RALT+SHIFT+COMMA</b> produces &#x2019;, and pressing <b>RALT+SHIFT+COMMA</b> a second time will produce &#x201D;</li>
  <li><b>RALT+SHIFT+PERIOD</b> produces &#x2018;, and pressing <b>RALT+SHIFT+PERIOD</b> a second time will produce &#x201C;</li>
</ul>  

