<h3>Full Documentation</h3>
		<!-- The guts of the documentation: reasoning behind the keyboard layout; detailed instructions on typing & editing; instructions on special characters or keyboard behaviour, etc.; complete character/input chart or link to chart; -->


<p>The Sabdalipi Assamese keyboard uses a phonetic-style layout, so that the key sequence
for each Assamese (Unicode) character is usually the similar sounding English character, for example:</p>

<p style='margin-left:30px'><kbd>k</kbd> is <span class='lang2'>ক</span>, <kbd>g</kbd> is <span class='lang2'>গ</span>, <kbd>p</kbd> is <span class='lang2'>প</span> and so on.</p>
<p>Similarly uppercase characters are used for the deep sounding characters, for example:</p>
<p style='margin-left:30px'> <kbd>K</kbd> is <span class='lang2'>খ</span>, <kbd>G</kbd> is <span class='lang2'>ঘ</span>, <kbd>P</kbd> is <span class='lang2'>ফ</span> and so on.</p>

<p>The plus '<kbd>+</kbd>' sign is used to create juktyakhars, for example:</p>

<p style='margin-left:30px'>typing '<kbd>l</kbd><kbd>+</kbd><kbd>p</kbd>' would result in <span class='lang2'>ল্প</span><br />
typing '<kbd>j</kbd><kbd>+</kbd><kbd>j</kbd><kbd>+</kbd><kbd>b</kbd>' would result in <span class='lang2'>জ্জ্ব</span></p>

<p>A special key is reserved for all kaar jogs, for example:</p>
<p style='margin-left:30px'><kbd>a</kbd> stands for Aakaar<br />
<kbd>u</kbd> stands for Ukaar, i.e <kbd>p</kbd><kbd>u</kbd> gives <span class='lang2'>পু</span>, <kbd>r</kbd><kbd>u</kbd> gives <span class='lang2'>ৰু</span> and <kbd>g</kbd><kbd>u</kbd> gives <span class='lang2'>গু</span><br />
<kbd>y</kbd> stands for rhokaar, i.e <kbd>g</kbd><kbd>y</kbd> is <span class='lang2'>গ্ৰ</span>, <kbd>t</kbd><kbd>y</kbd> is <span class='lang2'>ত্ৰ</span> and <kbd>k</kbd><kbd>y</kbd> is <span class='lang2'>ক্ৰ</span></p>

