<h2 lang="es">Como usar <kbd>AltGr</kbd>+<kbd>N</kbd></h2>

<p lang="es">Si no tienes una tecla <kbd>Ñ</kbd> en tu teclado, hay que
presionar las teclas <kbd>AltGr</kbd> y <kbd>N</kbd> a la misma vez, después
soltar ambas teclas y luego presionar la letra en que quieres que colocar la
virgulilla (~).</p>

<p lang="es">N. B. la <kbd>AltGr</kbd> es la tecla <kbd>Alt</kbd> al lado
derecho de la barra espaciadora.</p>

<p lang="es">N. B. usuarios de macOS deben presionar la tecla
<kbd>⌥ Opción</kbd> en lugar de <kbd>AltGr</kbd>.</p>

