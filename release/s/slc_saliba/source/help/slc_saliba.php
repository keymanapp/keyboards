<a id="slc_saliba-en"></a>

<p>The Sáliba keyboard allows you to type ã/ẽ/ĩ/õ/ũ/ñ on your keyboard.</p>

<h1>Characters</h1>

<p>In this keyboard <kbd>Ñ</kbd> becomes a “dead key” which means pressing
it once will not type any letter. You must press the <kbd>Ñ</kbd> key, and
then follow it up with a second key to type the letter you want. This is similar
to the way that <kbd>&nbsp;&#x301;</kbd> is used to type an stress mark on
vowels in Spanish.</p>

<p>For keyboards that lack a <kbd>Ñ</kbd> key, you can use
<kbd>Alt</kbd>+<kbd>N</kbd>.</p>

