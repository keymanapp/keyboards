<a id="slc_saliba-en"></a>

<p>The Sáliba keyboard allows you to type ã/ẽ/ĩ/õ/ũ/ñ of the Saliban alphabet
on your existing keyboard. All of the other letters used in Sáliba can already
be typed on a typical keyboard (for example, on the Latin American keyboard
included with most computers in Colombia), and those letters have the same
keys on this keyboard layout.</p>

<h1>Characters</h1>

<p>In this keyboard <kbd>Ñ</kbd> becomes a “dead key” which means pressing
it once will not insert any letter (as opposed to the Latin American keyboard,
where pressing this key will insert the letter ñ); rather, you
must press the <kbd>Ñ</kbd> is a placeholder, and it
must be followed up with a second key to type one of the letters mentioned
above. This is similar to the way that the accent key (´) is used to type an
stress mark over vowels in Spanish.</p>

<p>For keyboards that lack a dedicated <kbd>Ñ</kbd> key, you can use the
combination <kbd>AltGr</kbd>+<kbd>N</kbd> (Windows/Linux) or
<kbd>⌥ Option</kbd>+<kbd>N</kbd> (macOS).</p>

