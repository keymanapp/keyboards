<h2>How to use <kbd>Alt</kbd>+<kbd>N</kbd></h2>

<p>In this style, you must press <kbd>Alt</kbd> and <kbd>N</kbd> at the same
time, let go, and then press the letter that the ~ will go
over.</p>

<p>Note: macOS users will press <kbd>⌥ Option</kbd> instead of <kbd>Alt</kbd>.</p>

