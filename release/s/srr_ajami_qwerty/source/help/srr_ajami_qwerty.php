<h1>Start Using Seereer Ajami (QWERTY)</h1>

<p>
    Seereer Ajami (QWERTY) keyboard developed for use for the Seereer language of Senegal. 
</p>

<h1>Keyboard Layout</h1>
<p>This keyboard makes use of rotas for a few characters. For example: typing » twice will produce ›. It also works with turning <i>fatha</i>, <i>kasra</i>, and <i>damma</i> into <i>fathatan</i>, <i>kasratan</i>, and <i>dammatan</i> respectively.</p>
<p>It may be difficult to tell, but kashida is on the SHIFT+7 key, and the hyphen is available on the SHIFT+- key.</p>
