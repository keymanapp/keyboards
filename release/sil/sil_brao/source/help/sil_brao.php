<p>
    Brao (SIL) keyboard is designed for typing Brao Lave language spoken in north-eastern Cambodia. Khmer script is used for this keyboard. The desktop layout is similar to that of the Khmer NiDA keyboard, but there are less vowel characters. Only two subscripts are used in this language, <span style="font-family:Khmer Mondulkiri;">្រ</span> and <span style="font-family:Khmer Mondulkiri;">្ល</span>.
</p>
<p>
	Four extra characters are used in this language: 
	<ul>
	<li><span style="font-family:Khmer Mondulkiri;">អ្យ</span> on <kbd>o</kbd> key,</li> 
	<li><span style="font-family:Khmer Mondulkiri;">ឝ</span> on Shift <kbd>o</kbd> key,</li>
	<li><span style="font-family:Khmer Mondulkiri;">ឞ</span> on Shift <kbd>v</kbd> key and</li>
	<li><span style="font-family:Khmer Mondulkiri;">៝</span> on <kbd>e</kbd> key.</li>
	</ul>
</p>

