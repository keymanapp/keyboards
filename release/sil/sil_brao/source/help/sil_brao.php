<p>
    Brao (SIL) keyboard is designed for typing Brao Lave language spoken in north-eastern Cambodia. Khmer script is used for this keyboard. The desktop layout is similar to that of the Khmer NiDA keyboard, but there are less vowel characters. Only two subscripts are used in this language, ្រ and ្ល.
</p>
<p>
	Four extra characters are used in this language: 
	<ul>
	<li>អ្យ on <kbd>o</kbd> key,</li> 
	<li>ឝ on Shift <kbd>o</kbd> key,</li>
	<li>ឞ on Shift <kbd>v</kbd> key and</li>
	<li>៝ on <kbd>e</kbd> key.</li>
	</ul>
</p>

