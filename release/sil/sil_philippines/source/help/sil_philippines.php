<body>
<p>This document describes the use of the Keyman keyboard designed for typing Philippine languages with a Unicode font.</p>

<h2>Key to Reading the Chart</h2>
<p>The characters are presented in several tables, starting with the alphabetic characters and followed by various other
categories. At the top of each table there is a header row to help keep track of which information is
in which column:</p>

<ol>
  <li><b>Character</b> is the character you want displayed on your screen.</li>
  <li><b>Keying Sequence</b> is the keying sequence that will produce this character.</li>
  <li><b>Access Code</b> is the Unicode access code for the character.</li>
  <li><b>Character Name</b> is the official Unicode name of the character.</li>
</ol>

<p>Here are some examples of how the keying sequences are to be understood:</p>
<p><b>&apos;a</b> means press the <b>Apostrophe</b> key followed by the <b>A</b> key.</p>
<p><b>&apos;A</b> means press the <b>Apostrophe</b> key followed by the <b>A</b> key while holding down the <b>Shift</b> key.</p>
<p><b>&amp;[ ]</b> means press the <b>Ampersand/7</b> key while holding down the <b>Shift</b> key, followed by the <b>space</b> key.</p>

<h2>Additional Info</h2>

<p>All of these characters may be displayed correctly with the <a target='_blank' href='https://software.sil.org/doulos/'>Doulos SIL</a>, 
	<a target='_blank' href='https://software.sil.org/charis/'>Charis SIL</a>, and 
	<a target='_blank' href='https://software.sil.org/gentium/'>Gentium Plus</a> fonts, as well as with more
common Windows fonts like Times New Roman, Arial, Calibri, and Cambria.</p>

