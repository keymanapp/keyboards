<p>
  Este teclado normaliza la entrada de texto para más de 60 idiomas indígenas 
  hablados en Colombia y Venezuela (Wayuunaiki, familias Tucano, Arawak, etc.).
</p>

<h2>Métodos de Escritura</h2>

<h3>1. Rotación</h3>
<p>Escriba la letra base y presione la tecla <strong>]</strong> (o <strong>+</strong>) repetidamente.</p>
<p>Ejemplo: a + ] &rarr; á + ] &rarr; ã...</p>

<h3>2. Construcción Visual</h3>
<p>Use las teclas de corchetes y puntuación para agregar diacríticos:</p>
<ul>
  <li><strong>[</strong> = Tilde (´)</li>
  <li><strong>Shift + [</strong> = Diéresis (¨)</li>
  <li><strong>AltGr + ]</strong> = Nasal (~)</li>
  <li><strong>Shift + ]</strong> = Barra (*)</li>
</ul>

<h3>3. Móviles</h3>
<p>Mantenga presionada la tecla base para ver las variantes.</p>

