	<h2>Overview</h2>
	<p>This keyboard pairs with two other keyboards to cover the expected needs of most of Nigeria's languages. The three keyboards are identical 
		except that the vowels are treated differently. The vowels on this keyboard can have an underline below them as well as the standard tone markings. 
		The keyboard also includes standard "hook" (implosives), nasality, tone, and the Naira sign.
</p>
	<h3>Basic Function</h3>
	<p>This keyboard supports basic Latin characters on the standard keyboard. Other special keys are accessed through "deadkeys". 
