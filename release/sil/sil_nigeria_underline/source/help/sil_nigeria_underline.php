<p><b>The same result is achieved even when typed with the opposite sequence of deadkeys (<kbd>'</kbd><kbd>;</kbd> instead of <kbd>;</kbd><kbd>'</kbd>). The following sequences are possible:</b></p>
<p><kbd>'</kbd><kbd>;</kbd> or <kbd>;</kbd><kbd>'</kbd> followed by the vowel (such as "o") will give <samp>ó̱</samp></p>
<p><kbd>-</kbd><kbd>;</kbd> or <kbd>;</kbd><kbd>-</kbd> followed by the vowel (such as "o") will give <samp>ō̱</samp></p>
<p><kbd>`</kbd><kbd>;</kbd> or <kbd>;</kbd><kbd>`</kbd> followed by the vowel (such as "o") will give <samp>ò̱</samp></p>
<p><kbd>^</kbd><kbd>;</kbd> or <kbd>;</kbd><kbd>^</kbd> followed by the vowel (such as "o") will give <samp>ô̱</samp></p>
<p><kbd>&</kbd><kbd>;</kbd> or <kbd>;</kbd><kbd>&</kbd> followed by the vowel (such as "o") will give <samp>ǒ̱</samp></p>
<p><kbd>!</kbd><kbd>;</kbd> or <kbd>;</kbd><kbd>!</kbd> followed by the vowel (such as "o") will give <samp>ő̱</samp></p>
<p><kbd>~</kbd><kbd>;</kbd> or <kbd>;</kbd><kbd>~</kbd> followed by the vowel (such as "o") will give <samp>õ̱</samp></p>

<p><b>In order to achieve nasalization with tone, the following sequences must be typed in order:</b></p>
<p><kbd>~</kbd><kbd>'</kbd> followed by the vowel (such as "o") will give <samp>ṍ</samp></p>
<p><kbd>~</kbd><kbd>-</kbd> followed by the vowel (such as "o") will give <samp>ȭ</samp></p>
<p><kbd>~</kbd><kbd>`</kbd> followed by the vowel (such as "o") will give <samp>õ̀</samp></p>
<p><kbd>~</kbd><kbd>^</kbd> followed by the vowel (such as "o") will give <samp>õ̂</samp></p>
<p><kbd>~</kbd><kbd>&</kbd> followed by the vowel (such as "o") will give <samp>õ̌</samp></p>
<p><kbd>~</kbd><kbd>!</kbd> followed by the vowel (such as "o") will give <samp>õ̋</samp></p>

<p><b>In order to achieve nasalization with tone on underlined vowels, the following sequences must be typed in order:</b></p>

<p><kbd>;</kbd><kbd>~</kbd><kbd>'</kbd> followed by the vowel (such as "o") will give <samp>ṍ̱</samp></p>
<p><kbd>;</kbd><kbd>~</kbd><kbd>-</kbd> followed by the vowel (such as "o") will give <samp>ȭ̱</samp></p>
<p><kbd>;</kbd><kbd>~</kbd><kbd>`</kbd> followed by the vowel (such as "o") will give <samp>õ̱̀</samp></p>
<p><kbd>;</kbd><kbd>~</kbd><kbd>^</kbd> followed by the vowel (such as "o") will give <samp>õ̱̂</samp></p>
<p><kbd>;</kbd><kbd>~</kbd><kbd>&</kbd> followed by the vowel (such as "o") will give <samp>õ̱̌</samp></p>
<p><kbd>;</kbd><kbd>~</kbd><kbd>!</kbd> followed by the vowel (such as "o") will give <samp>õ̱̋</samp></p>


