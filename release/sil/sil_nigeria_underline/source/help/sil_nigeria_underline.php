	<h3>On Screen Keyboard</h3>
	<p>This keyboard includes an On Screen Keyboard view for easy reference. The On Screen Keyboard works best when associated with a QWERTY US layout.</p>
	<h3>Fonts</h3>
	<p>This is a Unicode keyboard and works with any Unicode font which has support for extended Latin characters. To find fonts on your computer which support the keyboard, use the Keyman Desktop Font Helper.</p>
