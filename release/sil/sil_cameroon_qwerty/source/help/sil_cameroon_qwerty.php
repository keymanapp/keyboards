<h2>Keyboard layout</h2>
<p>This keyboard supports the Standardized Alphabet for Cameroonian Languages for a QWERTY (US Keyboard).</p>

<div><img src='keylayout.png' alt='Keyboard Layout' /></div>

<div><img src='keyinfo.png' alt='Keyboard Legend' /></div>

<p>This chart describes the keystrokes used in Cameroon QWERTY keyboard layout.</p>

<p>Use the <kbd>;</kbd> (semicolon) key for the Cam key and the <kbd>:</kbd> (colon) key for the special (Spcl) key.</p>

<p>A non-combining version of any tone/diacritic marker may be inserted by itself into a text by typing the desired tone or diacritic key, followed by <kbd>+</kbd>. Note that this feature is not available for the four complex tones, dot-under, or tilde-under.</p>

<p>When adding nasal or tone diacritics, the typing sequence is always: Nasal ( &#x25CC;&#x0303; &#x25CC;&#x0327; ) + Tone ( &#x25CC;&#x0300; &#x25CC;&#x0301; &#x25CC;&#x0302; &#x25CC;&#x0304; etc.) + Vowel.</p>

