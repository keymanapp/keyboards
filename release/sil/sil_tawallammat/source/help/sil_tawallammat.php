  <p>This keyboard was developed to input characters for Tifinagh scripts.
  You can use this keyboard with any Tifinagh Unicode font.</p>
  
  <p>For typing in the Tawallammat Tamajaq language, we highly recommend 
  the Tagmukay font at 
  <a href="http://software.sil.org/tagmukay/" target=_blank>http://software.sil.org/tagmukay</a>. 
  This font has the required characters and bi-consonant ligatures for Tawallammat.</p>

<h2>Layout</h2>
<p>The three states of the Tawallammat Tifinagh Keyman keyboard for the Tagmukay font are illustrated
below. The black characters show characters found in the Tagmukay font. For bi-consonant ligatures,
type the "consonant joiner" between two consonants. For example, press the following keys in
sequence: <img src="2d54.png">, <img src="2d7f.png">, and <img src="2d5c.png"> to get the <img src="2d54_2d5c.png"> ligature. Support for bi-consonants will be dependent on the font you use.</p>

