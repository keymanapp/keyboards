<h2>ရှင်းပြချက်</h2>

<p>မြန်မာဖောင့်အဟောင်းသည် အက္ခရာတချို့၏ ကွဲပြားသောပုံစံအမျိုးမျိုးနှင့် အခြားသော သင်္ကေတတို့ လိုအပ်လေ့ရှိသည်။ ယခုအခါ တော်တော်များများမှာ ထိုကဲ့သို့  မလိုအပ်တော့ပါ။ အဘယ်ကြောင့်ဆိုသော် ဤဖောင့်သည် အက္ခရာတစ်လုံးစီအတွက် သင့်တော်သော မှန်ကန်သော ပုံစံကို ရွေးချယ်ထားသည်။ ဆိုလိုသည်မှာ control (သို့) alt တို့နှင့်တွဲပြီးရိုက်နှိပ်ခြင်းများ မလိုအပ်တော့ပါ။ </p>

