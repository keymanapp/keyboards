<h2 class="color" xml:lang="en">Physical Keyboard (Windows, Mac, Linux, also Android and iOS with external keyboards) </h2>
<div>
<p xml:lang="en">This section covers the use of the Cameroon <span class="az">AZERTY</span> with a physical keyboard, harnessing the power of multiple fingers to type more easily.</p>
<h3 class="color" xml:lang="en">Layout Overview</h3>
<p xml:lang="en">Below is the layout of the Cameroon <span class="az">AZERTY</span> Keyboard. Once you have learned to use the <span class="keycap">CAM</span> key and <span class="keycap">Shift</span> keys, this image can be used as a quick reference to find available characters.</p>
<img src="AzertyColor.png" class="full az" />
<blockquote xml:lang="en">Note: If your computer has <span class="az">a QWERTY or QWERTZ</span> Keyboard or something similar, consider removing Cameroon <span class="az">AZERTY</span> and try the Cameroon
<span class="az">QWERTY</span> Keyboard.
</blockquote>
<h3 class="color" xml:lang="en">Combinations</h3>
<img class="center" src="e_chart.png" xml:lang="en" />
<p xml:lang="en">As shown above, each key on the Cameroon Keyboard can be used to produce multiple characters (letters).</p>
<ol style="list-style-type:decimal; ">
<li xml:lang="en">If you tap the <span class="keycap">E</span> key once, you will get a lowercase "e".<br />Sometimes you may need to use two keys to get the letter you want. The SHIFT key is a key on a computer keyboard. It is used to type capital letters (like this: "ABC") instead of lowercase ("abc"). </li>
<li xml:lang="en">If you hold the <span class="keycap">Shift</span> key and tap the <span class="keycap">E</span> key once, you will get a capital "E".<br />The Cameroon Keyboard is special, and it is possible to type other characters not printed on your keyboard using the "CAM" (Cameroon) key. On your Cameroon <span class="az">AZERTY</span> Keyboard, the CAM key is the <span class="az">exclamation point</span> key (<span class="keycap az">!</span>). Each time you need the CAM key, just press <span class="keycap az">!</span>. To get the special characters shown on the right side of each key in the chart (like this: "əɔɓ ") instead of common characters (like "eob"), you need to use the CAM key. </li>
<li xml:lang="en">If you tap the <span class="keycap az">!</span> key once, and then press the <span class="keycap">E</span> key, you will get schwa "ə". </li>
<li xml:lang="en">If you tap the <span class="keycap az">!</span> key once, then hold <span class="keycap">Shift</span> and type <span class="keycap">E</span>, you will get capital schwa “Ə”.
<br />In review, if you look at the keyboard chart, you'll see that most keys on the Cameroon keyboard can be used to produce up to four different characters in this way. Use the chart to find the letters you need for your language, and use a combination of <span class="keycap">Shift</span> and the CAM Key (<span class="keycap az">!</span>) to type them.
</li>
<li xml:lang="en">Unlike French, Cameroonian diacritics (accents) are typed after the letter. For example, you would type: <span class="keycap az">!</span>
<span class="keycap">e</span>
<span class="keycap az">ù</span> to get "ə̀"
</li>
</ol>
