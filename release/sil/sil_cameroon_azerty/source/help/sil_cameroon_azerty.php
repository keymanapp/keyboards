<h3 class="color" xml:lang="en">Verrouillage des majuscules</h3>
<p>Double appui vite sur la touche shift pour activer le verrouillage des majuscules. Toutes les lettres tapées seront en majuscules jusqu'à ce que l'on appuie à nouveau sur la touche shift pour désactiver le verrouillage des majuscules.</p>
