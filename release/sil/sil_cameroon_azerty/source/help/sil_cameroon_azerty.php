<p class="tagline" xml:lang="fr">Vous permettant de saisir du texte dans votre langue maternelle !</p>
<p xml:lang="fr">Bienvenue au Clavier camerounais <span class="az">AZERTY</span> pour Keyman. Cette page explique comment saisir du texte dans les langues vernaculaires du Cameroun à l'aide du clavier <span class="az">AZERTY</span> du Cameroun.</p>
<p xml:lang="fr">Le clavier camerounais <span class="az">AZERTY</span>, un des claviers virtuels maintenus par l'équipe de Technologie linguistique de <a href="https://silcam.org/">SIL Cameroun</a>, vous permet de saisir toutes les langues qui suivent <a href="https://langtechcameroon.info/gacl/">l'Alphabet général des langues camerounaises</a>. L'Ethnologue recense 279 langues vivantes au Cameroun, dont 275 sont autochtones. Pour une liste de ces langues, verrez <a href="https://www.ethnologue.com/country/CM/">Cameroun dans l'Ethnologue.</a>
</p>
<hr />
<p xml:lang="fr">Pour profiter des caractéristiques uniques de chaque appareil, le clavier camerounais <span class="az">AZERTY</span> se comporte différemment sur les claviers physiques et les écrans tactiles, veuillez choisir votre interface ci-dessous :</p>
<!--Physical Keyboard-->
<h2 class="color" xml:lang="fr">Clavier physique (Windows, Mac, Linux, aussi Android et iOS avec un clavier externe) </h2>
<div>
<p xml:lang="fr">Cette section explique comment utiliser le clavier Cameroun <span class="az">AZERTY</span> avec des claviers physiques, exploitant la puissance de plusieurs doigts pour taper plus facilement.</p>
<h3 class="color" xml:lang="fr">Survol sur la disposition</h3>
<p xml:lang="fr">Voici la disposition du clavier Cameroun <span class="az">AZERTY</span>. Une fois que vous avez appris à utiliser les touches <span class="keycap">CAM</span> et <span class="keycap">Shift</span>, cette image peut être utilisée comme référence rapide pour trouver les caractères disponibles.</p>
<img src="AzertyColor.png" class="full az" />
<h3 class="color" xml:lang="fr">Comment faire les combinaisons de touche</h3>
<img class="center" src="e_tableau.png" xml:lang="fr" />
<p xml:lang="fr">Chaque touche du Clavier camerounais peut vous servir à produire des caractères (lettres) différents. </p>
<ol style="list-style-type:decimal; ">
<li xml:lang="fr">Si vous tapez la touche <span class="keycap">e</span> une fois, vous obtiendrez un « e » en minuscule. </li>
<li xml:lang="fr">Parfois, il faut utiliser plusieurs touches sur le clavier pour obtenir la lettre souhaitée. La touche <span class="keycap">MAJ</span> (SHIFT) est une touche sur le clavier d’un ordinateur. Elle est utilisée pour saisir les lettres en majuscule (par exemple : « ABC ») au lieu de lettres en minuscule (« abc »). Si vous appuyez la touche <span class="keycap">Maj</span> et tapez la touche <span class="keycap">E</span> une fois, vous obtiendrez l'« E » en majuscule. </li>
<li xml:lang="fr">Le Clavier Camerounais est spécial, et il est possible de saisir d’autres caractères non imprimés sur votre clavier à l’aide de la touche « CAM » (Cameroun). Sur votre Clavier Camerounais
<span class="az">AZERTY</span>, la touche CAM est le <span class="az">point d' exclamation</span> (<span class="keycap az">!</span>). Chaque fois que vous avez besoin de la touche CAM, tapez la touche
<span class="keycap az">!</span>. Pour obtenir les caractères spéciaux situés sur le côté droit de chaque touche dans la disposition du clavier (par exemple : « əɔɓ ») au lieu de caractères simples (comme « eob »), vous devez utiliser la touche CAM. Si vous tapez la touche <span class="keycap az">!</span> une fois, puis tapez la touche <span class="keycap">E</span>, vous obtiendrez le schwa « ə ».
</li>
<li xml:lang="fr">Parfois, il faut utiliser trois touches pour obtenir le caractère souhaité. Si vous tapez la touche <span class="keycap az">!</span> une fois, puis appuyez sur le <span class="keycap">Shift</span> en tapant le <span class="keycap">E</span>, vous obtiendrez le schwa en majuscule « Ə ».</li>
</ol>
