<h2 class="color" xml:lang="en">Touch Keyboard (Android, iOS)</h2>
<p xml:lang="en">The Cameroon keyboard has been adapted to use touch-based features when used on a touch device, such as an <span class="Android">Android</span> or <span class="iOS">iOS</span> phone or tablet. The following sections will explain use of the touch features of the Cameroon Keyboard. If you plug in an external keyboard to your <span class="Android">Android</span> or <span class="iOS">iOS</span> device, the keyboard will respond to the keyboard as it would on a desktop or laptop computer. </p>
<h3 class="color" xml:lang="en">Keyboard Use</h3>
<p xml:lang="en">The Cameroon Keyboard works like most Android keyboards. Tap a key to type the displayed letter. Long-press a key marked with a dot to access hidden characters. </p>
<span class="az">
<img src="longpress-azerty.png" height="300" />
</span>
<h3 class="color" xml:lang="en">Special Keys</h3>
<p xml:lang="en">Or use a special key to access other characters.</p>
<span class="az">
<img src="modkeys-en-a.png" height="300" xml:lang="en" />
</span>
<h3 class="color" xml:lang="en">Caps Lock</h3>
<p>Double tap quickly on the shift key to activate Caps Lock. All letters typed will be capital until the shift key is pressed again to deactivate Caps Lock.</p>
<div class="keyman">
<h3 class="color" xml:lang="en">Switching to Another Keyman Keyboard</h3>
<p xml:lang="en">To switch to another Keyman keyboard, for example <span class="az">Cameroon QWERTY</span>, follow these steps: </p>
<ol>
<li xml:lang="en">With the keyboard visible, touch the globe key: <br />
<img src="globe.png" />
<br />This will bring up a list of all currently installed languages.<br />If you have already downloaded additional languages, they will appear here. Simply select them and the keyboard will reappear with the new language.
</li>
</ol>
<p xml:lang="en">If you have not yet installed other keyboards, click the + button in the top-right corner of your screen.</p>
<ol>
<li xml:lang="en">A list of all available languages will appear. Scroll through this list until you find the language you want to install. Some languages may have multiple keyboards - for example we have 10+ different Tamil keyboards. Select the language and keyboard you want to install - a popup will ask for confirmation, hit the Download button.</li>
<li xml:lang="en">Once the download is complete, the new keyboard will be selected. Touch the Return button on the top left corner of the popup and the new keyboard will become active.</li>
</ol>
<blockquote xml:lang="en">Note: If you are more familiar with a<span class="az"> QWERTY</span> keyboard layout, consider switching to the Cameroon <span class="az">QWERTY</span> Keyboard.</blockquote>
</div>
<h3 class="color" xml:lang="en">Switching to Other <span class="keyman">Non-Keyman</span> Keyboards</h3>
<p xml:lang="en">You can switch between several keyboards. On most phones, the process is:</p>
<ol>
<li xml:lang="en">Tap in a text area to make sure the keyboard is showing.</li>
<li xml:lang="en">Swipe from the top of the screen down to the bottom.</li>
<li xml:lang="en">Choose the button 'Select Keyboard'.</li>
<li xml:lang="en">Choose the desired keyboard.</li>
</ol>
<h3 class="color" xml:lang="en">Physical Keyboards</h3>
<p xml:lang="en">If you choose to connect a physical keyboard (i.e. through Bluetooth or USB) to your mobile device, the physical keyboard will respond like a desktop keyboard. See the Physical Keyboard Section of this document for instructions and combinations.</p>
<blockquote xml:lang="en">Note: If your physical keyboard uses a <span class="az">QWERTY or QWERTZ</span> keyboard layout, consider switching to the Cameroon <span class="az">QWERTY</span> Keyboard.
</blockquote>
