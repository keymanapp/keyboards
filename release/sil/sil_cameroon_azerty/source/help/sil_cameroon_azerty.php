<p xml:lang="fr">Les ordinateurs achetés dans différents pays ont tendance à avoir des claviers physiques différents. Lorsque vous utilisez des claviers virtuels, il est BEAUCOUP plus facile d'apprendre le clavier si les touches sont à la même place que les lettres que vous voulez taper.</p>
<p xml:lang="fr">Le clavier Cameroun <span class="az">AZERTY</span> est conçu pour les ordinateurs équipés des claviers physiques suivants :</p>
<ul xml:lang="fr" class="az">
<li>Français (AZERTY) : <a href="azerty-fr.php">Voir toutes les combinaisons.</a></li>
</ul>
<blockquote xml:lang="fr">Note: Si votre ordinateur a un clavier <span class="az">QWERTY ou QWERTZ</span> ou quelque chose de similaires, envisagez de retirer le clavier Cameroun <span class="az">AZERTY</span> et installez le clavier Cameroon <span class="az">QWERTY</span>.</blockquote>

<h2 class="color" xml:lang="fr">Clavier tactile (Android, iOS)</h2>
<p xml:lang="fr">Le Clavier camerounais est spécialement adapté pour une utilisation sur les appareils tactiles comme un téléphone mobile ou une tablette <span class="Android">Android</span> ou <span class="iOS">iOS</span>. Vous pouvez utiliser le Clavier camerounais comme n'importe quel autre clavier virtuel. Il faut noter que si vous branchez ou connectez un clavier à l'appareil, le Clavier camerounais fonctionnera comme un claver sur un ordinateur desktop ou portable.</p>
<h3 class="color" xml:lang="fr">Mode d'emploi du clavier</h2>
<p xml:lang="fr">Le clavier camerounais fonctionne comme la plupart des claviers Android ou iOS. Tapez sur une touche pour saisir la lettre affichée. Appuyez longuement sur une touche marquée d'un point blanc pour accéder aux caractères cachés.</p>
