<h3 class="color" xml:lang="fr">Changez vers un autre clavier Keyman</h2>
<ol>
<li xml:lang="fr">Le clavier étant visible, appuyez sur la touche globe : <br />
<img src="globe.png" />
<br />Une liste de toutes les langues actuellement installées s'affichera.<br />Si vous avez déjà téléchargé d'autres langues, elles apparaitront ici. Il suffit de les sélectionner et le clavier réapparaitra avec la nouvelle langue.
</li>
</ol>
<p xml:lang="fr">Si vous n'avez pas encore installé de langues, appuyez sur le bouton + dans le coin supérieur droit de votre écran.</p>
<ol>
<li xml:lang="fr">Une liste de toutes les langues disponibles apparaitra. Faites défiler cette liste jusqu'à ce que vous trouviez la langue que vous souhaitez installer. Certaines langues peuvent avoir plusieurs claviers - par exemple, nous avons plus de 10 claviers tamouls différents.  Sélectionnez la langue et le clavier que vous voulez installer - un popup vous demandera confirmation, cliquez sur le bouton Télécharger.</li>
<li xml:lang="fr">Une fois le téléchargement terminé, le nouveau clavier sera sélectionné. Appuyez sur le bouton Retour dans le coin supérieur gauche de la fenêtre contextuelle et le nouveau clavier deviendra actif.</li>
</ol>
<blockquote xml:lang="fr">Note : Si vous êtes plus familier avec la disposition d'un clavier <span class="az">QWERTY ou QWERTZ</span>, pensez à passer au clavier Cameroun <span class="az">QWERTY</span>.</blockquote>
