<p style="text-align: justify; line-height:1.3">
នាវមឞើសមានទើល៝ត់រាខាច់ងគ្លែៈទុត ល៝ត់ <kbd>Spacebar</kbd> ទូត៝ង់គៃមាគិតទូនាវងើយ ល៝ត់ ២ត៝ង់គៃមា​អ្វាញ់ឆៃងខាស​ងាយ។ គៃមាតឹមងខាសជេសតេៈរាញាងកេងល៝ត់ <kbd>Ctrl</kbd>+<kbd>Shift</kbd>+<kbd>Spacebar</kbd>។
<br/>
សេចក្ដីពន្យល់សម្រាប់គ្រាប់ចុចដកឃ្លា៖ ចុច <kbd>Spacebar</kbd> ម្ដងដើម្បីសម្គាល់ចុងពាក្យ [ដំណកឃ្លាមើលមិនឃើញ (ZWSP)] ហើយចុចពីរដងដើម្បីបានដំណកឃ្លាធម្មតា។ ដើម្បីដកឃ្លាស្ដើង សូមចុច <kbd>Ctrl</kbd>+<kbd>Shift</kbd>+<kbd>Spacebar</kbd>។
<br/>
Explanation of the final Rota rule: Press <kbd>Spacebar</kbd> once to mark the end of a word [invisible space (ZWSP)] and twice for a visible space.
</p>

