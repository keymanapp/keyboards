<h2>Keyboard Instructions</h2>

<h3>English</h3>
<p>Each of the number keys from 1 to 8, plus five punctuation keys are each set up as a &quot;rota&quot;, which means
that pressing the key multiple times cycles through the
symbols for that key. For example, the rota for the 3 key is: <font face="Doulos SIL Cipher"><span style="font-style: normal">3 3&#x00307;
3&#803; 3&#824; 3&#824;&#775; 3&#824;&#803;</span></font>, which means that
pressing 3 once outputs &quot;3&quot;, pressing it twice 
produces &quot;3&#775;&quot;, and so forth. (Technically, the
diacritics are separate unicode characters; so, for example, 3&#824;&#775;
is actually three characters prepackaged in the proper sequence.)</p>

