<p>
The Cipher music notation is used throughout Indonesia and China for all kinds of music. The font is not intended for general orthographic use, although it is usable for song lyrics in simple Roman writing systems. This solution is primarily intended for fairly straightforward songbooks for singers, not for complex instrumental music. It can handle harmony lines fine, but not a lot of intricacies or subtleties (e.g. no staccato).
</p>

<p>The default for this keyboard is to type Indonesian cipher (<i>kepatihan</i>) 
	where overlines are used. If you wish to use Chinese cipher (<i>jianpu</i>) 
	that is also possible with this keyboard. Go to <b>Keyman Configuration</b>, 
	select the keyboard, click on <b>Keyboard options</b> and choose <b>Chinese cipher</b>. 
	You may need to exit Keyman configuration before the default behavior changes.</p>
<p>It is also possible to switch from <i>kepatihan</i> to <i>jianpu</i> using the RALT HYPHEN sequence (RALT+-). 
	Switch back to <i>kepatihan</i> using the RALT EQUAL sequence (RALT+=)</p>


<p>This keyboard and the Doulos SIL Cipher font (which will be installed
with this keyboard) work best with applications using the Graphite 
rendering technology, such as LibreOffice.</p>
<h2>English</h2>
<p>These are instructions for typing
cipher-style music (where numbers represent notes) using a unicode
font and a software keyboard for typing the characters covered by
that font.</p>
<p>Apart from the special symbols below,
the font also includes ordinary text characters inherited unchanged
from Doulos SIL, in order to support typing text and
punctuation--this single font can be used for both music and lyrics.
Using one font for both is recommended for ease of use, and also
because changing the font size should then preserve horizontal
alignment.</p>
<p>Because the keyboard needs to allow
both music and lyrics to be typed, it does not &quot;hijack&quot; any
of the standard text keys' characters by completely replacing them
with other characters. Alas, this means that typing most musical
symbols takes multiple keystrokes, but on the plus side, lyrics can
be touch-typed at full speed without switching keyboards or fonts.</p>
<p>By
default, rows of text typed using this font will be quite high, in
order to ensure that there will be enough room for all diacritics. If
you don't want this, you can use Format, Paragraph to set the
line-spacing to a fixed height.</p>
<p>The
font targets Indonesian cipher (<i>kepatihan</i>), but a mode
for Chinese cipher (<i>jianpu</i>)
is also provided via the &quot;slur&quot; font feature. The purpose
and scope of this project, and downloads, are provided here:</p>
