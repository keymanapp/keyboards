      <h1>Key Sequences</h1>
      <h4>The SHIFT key</h4>
      <p>Pressing the shift key (in yellow), and then one of those white keys in the layout (below) provides access to the symbols shown in the top right-hand area of each key. e.g. the shift key, then the 'e' key will output schwa /ə/.</p>
    
      <h4>Diacritics</h4>
      <p>To type diacritics you will first need to type the letter to which you want to add a diacritic. For example, to type the consonant marker below 'l', 'm', 'n', or 'r', type one of these aforementioned letters and then the BACKTICK key (in yellow). This will produce the syllabic consonant marker below the character: l̩, m̩, n̩, ɹ̩.</p>
