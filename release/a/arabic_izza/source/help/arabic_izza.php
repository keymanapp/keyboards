<h1>Arabic Izza: The pleasure of typing Arabic.</h1>
<h1>استرجاع لهيبة العربية في عصر المعلوملتية</h1>

<p>In order to type letters with Hamza proceed as follows: </p>
<p>قواعد كتابة الهمزة</p>

<br/>

<p>Isolated Hamza(ء), press letter Hamza(ء)once.</p>
<p>الهمزة لوحدها، الضغط على رمز الهمزة مرة واحدة</p>
<br/>

<p>Hamza fouk alif(أ), press letter alif(ا)twice.</p>
<p>الهمزة فوق ألف، الضغط على رمز الألف مرتين</p>
<br/>

<p>Hamza taht alif(إ), press letter Hamza(ء)twice.</p>
<p>الهمزة تحت ألف، الضغط على رمز الهمزة مرتين</p>
<br/>

<p>Hamza fouk waw(ؤ), press letter waw(و)twice.</p>
<p>الهمزة فوق واو، الضغط على رمز الواو مرتين</p>
<br/>

<p>Hamza fouk nabira(ئ), press nabira twice(ى).</p>
<p>الهمزة فوق نبرة، الضغط على رمز النبرة مرتين</p>
<br/>

<p>Hamzat madd(آ), press Isolated Hamza(ء)followed by Alif(ا).</p>
<p>همزة المد، الضغط على الهمزة ثم الألف</p>
<br/>

<p>To type Al taarif(ال), just press one key.</p>
<p>الـ التعريف لها زر مستقل</p>
<br/>

<p>Harakat(Damma, Fatha, Kasra)and Shadda are on the first level without Shift.
To make things even simpler, you find Harakat and hamza under their corresponding names:
Shadda under letter Shin for example, using Shift.</p>
<p>الحركات و الشدة لها أزرار مستقلة على المستوى الأول
توجد كذلك على المستوى الثاني تحت الحروف الأولى لأسمائها
على المستوى الثاني توجد كذلك كل الرموز لكتابة العربية بدقة و احترافية</p>

