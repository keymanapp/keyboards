<h1>Arabic Izza: The pleasure of typing Arabic.</h1>
<h1>استرجاع لهيبة العربية في عصر المعلوملتية</h1>

<p>In order to type letters with Hamza proceed as follows: </p>
<p>قواعد كتابة الهمزة</p>

<ul>

<li>Isolated Hamza(ء), press letter Hamza(ء)once.
<li>الهمزة لوحدها، الضغط على رمز الهمزة مرة واحدة

<li>Hamza fouk alif(أ), press letter alif(ا)twice.
<li>الهمزة فوق ألف، الضغط على رمز الألف مرتين

<li>Hamza taht alif(إ), press letter Hamza(ء)twice.
<li>الهمزة تحت ألف، الضغط على رمز الهمزة مرتين

<li>Hamza fouk waw(ؤ), press letter waw(و)twice.
<li>الهمزة فوق واو، الضغط على رمز الواو مرتين

<li>Hamza fouk nabira(ئ), press nabira twice(ى).
<li>الهمزة فوق نبرة، الضغط على رمز النبرة مرتين

<li>Hamzat madd(آ), press Isolated Hamza(ء)followed by Alif(ا).
<li>همزة المد، الضغط على الهمزة ثم الألف

<li>To type Al taarif(ال), just press one key.
<li>الـ التعريف لها زر مستقل

</ul>

<p>Harakat(Damma, Fatha, Kasra)and Shadda are on the first level without Shift.
To make things even simpler, you find Harakat and hamza under their corresponding names:
Shadda under letter Shin for example, using Shift.</p>
<p>الحركات و الشدة لها أزرار مستقلة على المستوى الأول
توجد كذلك على المستوى الثاني تحت الحروف الأولى لأسمائها
على المستوى الثاني توجد كذلك كل الرموز لكتابة العربية بدقة و احترافية</p>

