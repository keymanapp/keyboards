<h1>Keyboard Layout</h1>
<h2>Desktop</h2>
<p><b>Unshifted</b></p>
<img src='me_enU_.png' alt='Keyboard Layout'>
<p>To get à (low tone a) please type  a`	To get á (high tone a) please type a``</p>   
<p>To get ɛ̀ (low tone ɛ) please type ɛ`	To get ɛ́ (high tone ɛ) please type ɛ``</p>
<p>and so on... </p>
<p>The various quote characters are typed by pressing the ’ key, which is also used for the consonant character ’ (appearing in the word Mɛ’ɛn for example). </p>
<p>So to get the various single quote characters please press ’ once, twice, or three times respectively. </p>
<p>To get the various double quote characters please press SHIFT ’ once, twice, three times respectively.</p> 
<p>(For realizing straight quotes (single or double) please ensure to adapt autotype/autocorrect settings accordingly in your word processor.)</p>
<p><b>Shifted</b></p>
<img src='me_enU_S.png' alt='Keyboard Layout'>
<p>To get À (low tone capital a) please type A`    To get Á (hight tone capital a) please type A``</p>   
<p>To get Ɛ̀ (low tone capital ɛ) please type Ɛ`    To get Ɛ́ (hight tone capital ɛ) please type Ɛ``</p>
<p>and so on... </p>
<p>In order to get € please press the combination for $ twice.</p>
<p>In order to get £ please press the combination for $ three times.</p>
