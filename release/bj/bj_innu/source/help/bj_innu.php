  <p>This keyboard is designed for the Innu language of Canada. 
  	The keyboard follows a standard US QWERTY keyboard layout. 
  	To access the superscript u and U, type the consonant followed by a "w". 
  	For example "kw" becomes "kᵘ, "mw" becomes "mᵘ" "shw" becomes "shᵘ" and "tw" becomes "tᵘ". 
  	These are also available for uppercase. If "kᵘ" is subsequently followed by "a", "e", "i", or "u", 
  	then the raised "u" becomes a "w" again ("kwa" remains "kwa").</p>
<p>On the mobile layout, the same keystrokes will work. However, the superscript "u" is also available as a longpress on the "u" key.</p>
