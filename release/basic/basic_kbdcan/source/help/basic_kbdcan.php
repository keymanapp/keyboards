<p>This keyboard makes extensive use of deadkeys. Typing the deadkey before the base character will output the combined character. For example:</p>
<p>^a will produce â (available on aAcCeEgGhHiIjJoOsSuUwWyY).</p>
<p>¨a will produce ä (available on aAeEiIoOuUy).</p>
<p>`a will produce à (available on aAeEiIoOuU).</p>
<p>´a will produce á (available on aAcCeEiIlLnNoOrRsSuUyYzZ).</p>
<p>¯a will produce ā (available on aAeEiIoOuU).</p>
<p>~n will produce ñ (available on aAiInNoOuU).</p>
<p>¸c will produce ç (available on cCgGkKlLnNrRsStT).</p>
