<p style='margin: 16px 0 0 0'>
This keyboard layout is designed for Tibetan (People's Republic of China). The keyboard layout follows the Windows 10 Tibetan (People's Republic of China) layout.</p>

<p>The Windows keyboard on which this one is based had 3 characters on the RALT SHIFT K, RALT SHIFT M, and RALT SHIFT O keys. Because these are reserved for other purposes in Keyman, we have changed those characters to be typed using RALT K, RALT M and RALT O instead.</p>
<p>The "m" key is a deadkey. When typed in conjunction with other keys a similar subjoined character will be formed. For example, if you type "m" + "d" (U+0F51 ད) then U+0FA1 (ྡ) is formed.</p>
<p>If square boxes are displayed instead of characters when using this keyboard (and in the keyboard layouts below), please read our <a href="/troubleshooting/#boxes">troubleshooting guide</a>.
</p>
