<h1>Keyboard Layout</h1>
<p>The keyboard layouts are listed below. However, this keyboard also utilizes "deadkeys" which are accessed with RALT 0/2/3/4/5/6/8/9/-/=. Thus, type:</p>
<p> RALT+0+o to get "ő". RALT+0 can be used with o, O, u, and U. </p>
<p> RALT+2+c to get "č". RALT+2 can be used with c, C, d, D, e, E, l, L, n, N, r, R, s, S, t, T, z, and Z. </p>
<p> RALT+3+a to get "â". RALT+3 can be used with a, A, i, I, o, and O. </p>
<p> RALT+4+a to get "ă". RALT+4 can be used with a and A. </p>
<p> RALT+5+u to get "ů". RALT+5 can be used with u and U. </p>
<p> RALT+6+a to get "ą". RALT+6 can be used with a, A, e, and E. </p>
<p> RALT+8+z to get "ż". RALT+8 can be used with z and Z. </p>
<p> RALT+9+a to get "á". RALT+9 can be used with a, A, c, C, e, E, i, I, l, L, n, N, o, O, r, R, s, S, u, U, y, Y, z, and Z. </p>
<p> RALT+-+a to get "ä". RALT+- can be used with a, A, e, E, o, O, u, and U. </p>
<p> RALT+=+c to get "ç". RALT+= can be used with c, C, s, S, t, and T. </p>

