<p>
This keyboard layout is designed for Latvian.  It includes an on screen keyboard which can be viewed 
by clicking on the Keyman icon and selecting the On Screen Keyboard menu item. The keyboard layout 
follows the Windows 10 Latvian (QWERTY) layout.
</p>

<p>This keyboard makes use of "deadkeys" which are listed below:</p>

<p>~o will produce õ (available on oO).</p>
<p>°a will produce å (available on aA).</p>
<p>°e will produce ė (available on eEgzZ).</p>
<p>´c will produce ć (available on cCeEnNoOsSzZ).</p>
<p>¨a will produce ä (available on aAoOuU).</p>

