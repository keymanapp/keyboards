<p>This keyboard makes use of SHIFT+RALT keys which are set by default when Keyman is installed. To use this keyboard, you must go to <b>Configuration.../Hotkeys</b> and select the hotkey for "Switch Keyman Desktop Off". Either change the hotkey or turn it off. Do the same for "Open Keyboard Menu" and for "Open Language Switcher".</p>

<p>This keyboard utilizes deadkeys:</p>
<ul>
	<li>Press ◌̀ (on the tilde key) and then any vowel key (aeiou) to get: àèìòù.</li>
	<li>Press ◌́ (by press and hold the Shift key and the tilde key) and then any vowel key to get: áéíóú.</li>
	<li>Press ◌̄ (by press and hold the Shift key and the numeric key 6) and then any vowel to get: āēīōū.</li>
	<li>Press ◌̀ and then the spacebar to get: ` </li>
	<li>Press ◌́ and then the spacebar to get: ~</li>
</ul>
