<p>Halang Telex Style keyboard for the Halang language of Vietnam.</p>
<br>
<p>â : Type "a" vowel twice to produce the vowel with a circumflex above it.</p>
<p>ê : Type "e" vowel twice to produce the vowel with an e circumflex above it.</p>
<p>ô : Type "o" vowel twice to produce the vowel with an o circumflex above it.</p>
<br>
<p>ă : Type "a", plus a "w" or "q" to produce the "a" with an inverted breve above it.</p>
<p>ĕ : Type "e", plus a "q" to produce the "e" with an inverted breve above it.</p>
<p>ĭ : Type "i", plus a "q" to produce the "i" with an inverted breve above it.</p>
<p>ŏ : Type "o", plus a "q" to produce the "o" with an inverted breve above it.</p>
<p>ŭ : Type "u", plus a "q" to produce the "u" with an inverted breve above it.</p>
<br>
<p>â̆ : Type "â", plus a "q" to produce the "â" with an inverted breve above it.</p>
<p>ê̆ : Type "ê", plus a "q" to produce the "ê" with an inverted breve above it.</p>
<p>ô̆ : Type "ô", plus a "q" to produce the "ô" with an inverted breve above it.</p>
<br>
<p>à : Type "a", plus a "f" to produce the "a" with a grave mark above it.</p>
<p>è : Type "e", plus a "f" to produce the "e" with a grave mark above it.</p>
<p>ì : Type "i", plus a "f" to produce the "i" with a grave mark above it.</p>
<p>ò : Type "o", plus a "f" to produce the "o" with a grave mark above it.</p>
<p>ù : Type "u", plus a "f" to produce the "u" with a grave mark above it.</p>
<br>
<p>ơ, ư :  Type "o" or "u" followed by a "w" to produce the "ơ" or "ư".</p>
<p>ươ: Type "uo" followed by a "w" to produce "ươ".</p>