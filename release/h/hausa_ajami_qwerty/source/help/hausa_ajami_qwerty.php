<p>
    This keyboard is for typing Ajami in the Hausa language of Nigeria using either the Hafs or Warsh orthography.
</p>

<p>This keyboard makes use of rotas for a few characters. For example: typing » twice will produce ›. Doing this works with «, ”, and “. It also works with turning <i>fatha</i>, <i>kasra</i>, and <i>damma</i> into <i>fathatan</i>, <i>kasratan</i>, and <i>dammatan</i> respectively.</p>
<p>It may be difficult to tell, but kashida is on the SHIFT+7 key, and the hyphen is available on the SHIFT+- key. -- becomes a long dash.</p>
<p>This keyboard uses many new-to-Unicode characters that were encoded for the Hausa language in the Warsh style. Not many fonts or applications will support them yet. However, Alkalami, Ruwudu, Harmattan and Scheherazade New do support the new characters. LibreOffice will support the rendering for these new characters. The base characters on the RALT layer are for the Warsh orthography that is sometimes used for Hausa.</p>

