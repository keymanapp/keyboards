    A disposição de teclado hcesar, também chamado "teclado nacional", foi utilizada por máquinas de escrever vendidas em Portugal desde 1937 até aos anos 80. Foi concebida para colocar o máximo
de teclas normalmente utilizadas no meio do layout.</p>
<p>The hcesar keyboard layout, also called "teclado nacional", was used by typewriters sold in Portugal from 1937 until the 1980s. It was designed to put the most
commonly used keys in the middle of the layout. 
</p>

<h2>Desktop Keyboard layout</h2>

<p>This keyboard makes use of deadkeys. Typing the deadkey before the base character will output the combined character. For example:</p>
<p>´a will produce á (available on aAeEiIoOuUyY).</p>
<p>`a will produce à (available on aAeEiIoOuU).</p>
<p>¨a will produce ä (available on aAeEiIoOuUy).</p>
<p>~a will produce ã (available on aAnNoO).</p>
<p>^a will produce â (available on aAeEiIoOuU).</p>

