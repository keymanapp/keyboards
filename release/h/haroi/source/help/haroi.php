<h2>Tone Marks</h2>

<p>Press any vowel key and then any numeral 1-5 to get the vowel with a tone mark. </p>
<p>For example:</p>
<ul style="font-family:'Charist SIL';">
<li>a + 1 = á </li> 
<li>a + 2 = à </li>
<li>a + 3 = ả </li>
<li>a + 4 = ã </li>
<li>a + 5 = ạ </li>
</ul>
