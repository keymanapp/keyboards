<h2><span style='mso-fareast-font-family:"Times New Roman"'>Introduction<o:p></o:p></span></h2>

<p>This keyboard is designed for the Kashmiri language spoken in Jammun and Kashmir, India and Pakistan, in its
standard Perso-Arabic script, as used in Pakistan, Kashmir and India. It is based on phonetic
keyboard for Kashmiri Language developed by Pakistani linguist, author, Scientist,
Inventor researcher and Software Developer Mr.Rehmat Aziz Chitrali for Khowar
Academy(an literary Association working on the language and culture
documentation in Northern Pakistan) used in Windows 8.1 and above as Kashmiri
Keyboard. </p>

<p>This is the first Kashmiri language keyboard created by Rehmat Aziz Chitrali,
Pakistani linguist, Scientist, Inventor, Software Developer and Researcher.
This keyboard is for Kashmiri Language based on the keyboard layout as developed
by Mr.Rehmat Aziz Chitrali and approved by Khowar Academy of Pakistan as
standard keyboard for Kashmiri language.</p>

<p>The Kashmiri Keyboard Project(KKP) was supervised and developed by Rehmat Aziz
Chitrali, who heads the Khowar Academy Chitral Pakistan.</p>

<p>Khowar Academy Chitral was established in April 1996 specifically to promote
the endangered languages of Chitral Pakistan. The Academy has worked
extensively on Kashmiri linguistics, standardization for computing, and modeling
of script, speech and language. Research is being conducted in all aspects of Kashmiri,
including acoustic phonetics, phonology, morphology, syntax, grammar and
semantics.</p>

<h2><span style='mso-fareast-font-family:"Times New Roman"'>Font<o:p></o:p></span></h2>

<p style='text-align:justify'>The Rachitrali-Kashmiri Keyman keyboard is designed
by Rehmat Aziz Chitrali to work with any extended Arabic script font. </p>

<h2><span style='mso-fareast-font-family:"Times New Roman"'>Keyboard<o:p></o:p></span></h2>

<p>This keyboard layout is designed by Rehmat Aziz Chitrali linguist and
researcher at Khowar Academy Chitral Khyber Pakhtunkhwa Pakistan for the Kashmiri
language. It includes an on screen keyboard which can be viewed by clicking on
the Keyman icon and selecting the On Screen Keyboard menu item. </p>

