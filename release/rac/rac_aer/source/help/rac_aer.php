آئر کلیدی تختی کے متعلق

آئر کلیدی تختہ آئر زبان کا پہلا سافٹ ویر ہے جسے کھوار اکیڈمی چترال کے رحمت عزیز چترالی نے ایجاد کیا ہے اس کلیدی تختی میں ڈاکومینٹیشن کے زریعے کیبورڈ کے استعمال کے طریقہء کار کی وضاحت کی گئی ہے، اس کیبورڈ کے تین ورژن ہیں پہلا ورژن'' بیس ورژن'' کہلاتا ہے دوسرا ورژن ''شفٹ ورژن'' ہے اور تیسرا ورژن ''کنٹرول آلٹر ورژن'' کہلاتا ہے، رحمت عزیز چترالی نے یہ آئر کلیدی تختہ اور آئر سافٹ ویر کھوار اکیڈمی کے قیام کے وقت ۱۹۹۶ میں ایجاد کیا تھا لیکن اسے کسی اچھے کمپنی کی تلاش تھی اس سلسلے میں انہوں نے بڑی بڑی سافٹ ویر کمبنیز اور اداروں سے رابطہ کیا لیکن آئر زبان کے مخصوص حروف یونی کوڈز کو سپورٹ نہیں کرتے تھے اس لیے یہ کام التوا کا شکار رہا اس کے بعد رحمت عزیز چترالی نے آئر زبان کے مخصوص حروف کے لیے یونی کوڈز ایجاد کرنے کے ساتھ ساتھ ان حروف کے لیے اپنے ایجاد کردہ سافٹ ویر کا استعمال کرتے ہویے یونی کوڈ شدہ ان حروف کو کھوار اکیڈمی کے پلیٹ فارم سے یونی کوڈ کنسورشیم میں رجسٹر کروایا جوکہ آپ کی سندھ کی معدوم ہونے والی زبان آئر کے لیے ایک بہت بڑے کارنامے اور احسان سے کم نہیں، رحمت عزیز چترالی نے آئر زبان کے اس سافٹ ویر کو مایکروسافٹ کی ممبر کمپنی ٹاول سافٹ اور کیمین کے ساتھ رجسٹر اور ریلیز کروایا اور  اس آئر سافٹ ویر کو استفادہء عام کے لیے کھوار اکیڈمی نے ٹاول سافٹ اور کیمین کے تعاون و اشتراک سے ریلیز کردیا ہے امید ہے کہ یہ سافٹ ویر آپ کو پسند آیے گا۔۔۔۔۔

