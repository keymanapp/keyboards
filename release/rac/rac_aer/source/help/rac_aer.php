<h2>Introduction</h2>
<p>This keyboard is designed for the Aer language spoken in the province  of Sindh Pakistan, in its standard Perso-Arabic script, 
as used in Sindh Pakistan. It is based on phonetic keyboard for Aer Language 
developed by Pakistani linguist, author, researcher and Software Developer Mr.Rehmat Aziz Chitrali for 
use by SIL International, and later approved by Khowar Academy (a literary Association working on the 
language and culture documentation in Northern Pakistan) used in Windows 8.1 and above as Aer Keyboard</p>

<p>This is the first Aer language keyboard created by Rehmat Aziz Chitrali, 
	Pakistani linguist, Software Developer and Researcher. This keyboard is for 
	Aer Language based on the keyboard layout as developed by Mr. Rehmat Aziz 
	Chitrali and approved by Khowar Academy of Pakistan as standard keyboard for 
Aer language.</p>

<p>The Aer Keyboard Project (AKP) was supervised and developed by Rehmat 
Aziz Chitrali, who heads the Khowar Academy Chitral Pakistan. </p>

<p>Khowar Academy Chitral was established in April 1996 specifically to promote the
	endangered languages of Chitral Pakistan. The Academy has worked extensively on 
	Aer linguistics, standardization for computing, and modeling of script, speech 
	and language. Research is being conducted in all aspects of Aer, including 
	acoustic phonetics, phonology, morphology, syntax, grammar and semantics.</p>

