<p>This keyboard is designed for the Dogri language spoken in Pakistan, Kashmir and India, in its standard Perso-Arabic script, as used in Asia. It is based on phonetic keyboard for Dogri Language developed by Pakistani linguist, author, Scientist,  Inventor researcher and Software Developer Mr.Rehmat Aziz Chitrali for Khowar Academy(an literary Association working on the language and culture documentation in Northern Pakistan) used in Windows 8.1 and above as Dogri Keyboard.
</p>

<p>This is the first Dogri language keyboard created by Rehmat Aziz Chitrali, Pakistani linguist, Scientist, Inventor, Software Developer and Researcher. This keyboard is for Dogri Language based on the keyboard layout as developed by Mr.Rehmat Aziz Chitrali and approved by Khowar Academy of Pakistan as standard keyboard for Dogri language.</p>

<p>The Dogri Keyboard Project(DKP) was supervised and developed by Rehmat Aziz Chitrali, who heads the Khowar Academy Chitral Pakistan.</p>

<p>Khowar Academy Chitral was established in April 1996 specifically to promote the endangered  languages of Chitral Pakistan. The Academy has worked extensively on Dogri linguistics, standardization for computing, and modeling of script, speech and language. Research is being conducted in all aspects of Dogri, including acoustic phonetics, phonology, morphology, syntax, grammar and semantics.</p>

