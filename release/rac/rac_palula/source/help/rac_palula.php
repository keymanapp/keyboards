<h2>Introduction</h2>
<p>This keyboard is designed for the Palula language spoken in Chitral Pakistan in its standard Perso-Arabic script, as used Pakistan. It is based on phonetic keyboard for Palula Language developed by Pakistani linguist, author, researcher, scientist,  Inventor and Software Developer Mr.Rehmat Aziz Chitrali for Summer Institute of Linguistics(SIL), and later approved by Khowar Academy(an literary Association working on the language and culture documentation in Northern Pakistan) used in Windows 8.1 and above as Palula Keyboard.
</p>

<p>This is the first Palula language keyboard created by Rehmat Aziz Chitrali, Pakistani linguist, Scientist,  Inventor,  Software Developer and Researcher. This keyboard is for Palula Language based on the keyboard layout as developed by Mr.Rehmat Aziz Chitrali and approved by Khowar Academy of Pakistan as standard keyboard for Palula language.</p>

<p>The Palula Keyboard Project(PKP) was supervised and developed by Rehmat Aziz Chitrali, who heads the Khowar Academy Chitral Pakistan.</p>

<p>Khowar Academy Chitral was established in April 1996 specifically to promote the endangered  languages of Chitral Pakistan. The Academy has worked extensively on Palula linguistics, standardization for computing, and modeling of script, speech and language. Research is being conducted in all aspects of Palula, including acoustic phonetics, phonology, morphology, syntax, grammar and semantics.</p>

