<h2>Introduction</h2>
<p>This keyboard is designed for the Parkari Koli language spoken in Sindh Province of Pakistan, in its standard Perso-Arabic script, as used in Pakistan. It is based on phonetic keyboard for Parkari Koli Language developed by Pakistani linguist, author, scientist, inventor, researcher and software developer Mr.Rehmat Aziz Chitrali for Khowar Academy(an literary Association working on the language and culture documentation in Northern Pakistan) used in Windows 8.1 and above as Parkari Koli Keyboard.
</p>

<p>This is the first Parkari Koli language keyboard created by Rehmat Aziz Chitrali, Pakistani linguist, scientist, inventor, researcher and software developer. This keyboard is for Parkari Koli Language based on the keyboard layout as developed by Mr.Rehmat Aziz Chitrali and approved by Khowar Academy of Pakistan as standard keyboard for Parkari Koli language.</p>

<p>The Parkari Koli Keyboard Project(PKKP) was supervised and developed by Rehmat Aziz Chitrali, who heads the Khowar Academy Chitral Pakistan.</p>

<p>Khowar Academy Chitral was established in April 1996 specifically to promote the endangered  languages of Chitral Pakistan. The Academy has worked extensively on Parkari Koli linguistics, standardization for computing, and modeling of script, speech and language. Research is being conducted in all aspects of Parkari Koli, including acoustic phonetics, phonology, morphology, syntax, grammar and semantics.</p>

