<h2>Introduction</h2>
<p>This keyboard is designed for the Kalasha language spoken in Kalash Valleys of  Chitral, Rumbur Valley, 
Birir Valley, and Bumburet Valley of Khyber Pakhtunkhwa Pakistan, in its standard Perso-Arabic script, 
as used in Kalash Valleys of Chitral Pakistan. It is based on phonetic keyboard for Kalasha Language 
developed by Pakistani linguist, author, researcher and Software Developer Mr.Rehmat Aziz Chitrali for 
use by SIL International, and later approved by Khowar Academy (a literary Association working on the 
language and culture documentation in Northern Pakistan) used in Windows 8.1 and above as Kalasha Keyboard</p>

<p>This is the first Kalasha language keyboard created by Rehmat Aziz Chitrali, 
	Pakistani linguist, Software Developer and Researcher. This keyboard is for 
	Kalasha Language based on the keyboard layout as developed by Mr. Rehmat Aziz 
	Chitrali and approved by Khowar Academy of Pakistan as standard keyboard for 
Kalasha language.</p>

<p>The Kalasha Keyboard Project (KKP) was supervised and developed by Rehmat 
Aziz Chitrali, who heads the Khowar Academy Chitral Pakistan. </p>

<p>Khowar Academy Chitral was established in April 1996 specifically to promote the
	endangered languages of Chitral Pakistan. The Academy has worked extensively on 
	Kalasha linguistics, standardization for computing, and modeling of script, speech 
	and language. Research is being conducted in all aspects of Kalasha, including 
	acoustic phonetics, phonology, morphology, syntax, grammar and semantics.</p>

