<h2>Introduction</h2>
<p>This keyboard is designed by Rehmat Aziz Chitrali Linguist and Researcher at Khowar Academy Chitral, Khyber Pakhtunkhwa of Pakistan for the Urdu language spoken in Pakistan. 


It is based on phonetic keyboard for Urdu Language 
developed by Pakistani linguist, author, researcher and Software Developer Mr.Rehmat Aziz Chitrali for 
use by SIL International, Keyman, Tavultesoft and later approved by Khowar Academy (a literary Association working on the 
language and culture documentation in Northern Pakistan) used in Windows 8.1 and above as Urdu Keyboard</p>

<p>This is the first Urdu language keyboard created by Rehmat Aziz Chitrali, 
	Pakistani linguist, Software Developer and Researcher. This keyboard is for 
	Urdu Language based on the keyboard layout as developed by Mr. Rehmat Aziz 
	Chitrali and approved by Khowar Academy of Pakistan as standard keyboard for 
Urdu language.</p>

<p>The Urdu Keyboard Project (UKP) was supervised and developed by Rehmat 
Aziz Chitrali, who heads the Khowar Academy Chitral Pakistan. </p>

<p>Khowar Academy Chitral was established in April 1996 specifically to promote the
	endangered languages of Chitral Pakistan. The Academy has worked extensively on 
	Urdu linguistics, standardization for computing, and modeling of script, speech 
	and language. Research is being conducted in all aspects of Urdu, including 
	acoustic phonetics, phonology, morphology, syntax, grammar and semantics.</p>

