<p>This keyboard is designed for the Pashai language spoken in Pakistan and Afghanistan, in its standard Perso-Arabic script, as used in Pakistan and Afghanistan. It is based on phonetic keyboard for Pashai Language developed by Pakistani linguist, author, Scientist,  Inventor researcher and Software Developer Mr.Rehmat Aziz Chitrali for Khowar Academy(an literary Association working on the language and culture documentation in Northern Pakistan) used in Windows 8.1 and above as Pashai Keyboard.</p>

<p>This is the first Pashai language keyboard created by Rehmat Aziz Chitrali, Pakistani linguist, Scientist, Inventor, Software Developer and Researcher. This keyboard is for Pashai Language based on the keyboard layout as developed by Mr.Rehmat Aziz Chitrali and approved by Khowar Academy of Pakistan as standard keyboard for Pashai language.</p>

<p>The Pashai Keyboard Project(PKP) was supervised and developed by Rehmat Aziz Chitrali, who heads the Khowar Academy Chitral Pakistan.</p>

<p>Khowar Academy Chitral was established in April 1996 specifically to promote the endangered  languages of Chitral Pakistan. The Academy has worked extensively on Pashai linguistics, standardization for computing, and modeling of script, speech and language. Research is being conducted in all aspects of Pashai, including acoustic phonetics, phonology, morphology, syntax, grammar and semantics.</p>

