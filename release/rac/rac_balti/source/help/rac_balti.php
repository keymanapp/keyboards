<h2>Introduction</h2>
<p>This keyboard is designed for the Balti language spoken in Gilgit-Baltistan of Northern Pakistan in its standard Perso-Arabic script, as used Pakistan. It is based on phonetic keyboard for Balti Language developed by Pakistani linguist, author, researcher, scientist,  Inventor and Software Developer Mr.Rehmat Aziz Chitrali for Summer Institute of Linguistics(SIL), and later approved by Khowar Academy(an literary Association working on the language and culture documentation in Northern Pakistan) used in Windows 8.1 and above as Balti Keyboard.
</p>

<p>This is the first Balti language keyboard created by Rehmat Aziz Chitrali, Pakistani linguist, Scientist,  Inventor,  Software Developer and Researcher. This keyboard is for Balti Language based on the keyboard layout as developed by Mr.Rehmat Aziz Chitrali and approved by Khowar Academy of Pakistan as standard keyboard for Balti language.</p>

<p>The Balti Keyboard Project(BKP) was supervised and developed by Rehmat Aziz Chitrali, who heads the Khowar Academy Chitral Pakistan.</p>

<p>Khowar Academy Chitral was established in April 1996 specifically to promote the endangered  languages of Chitral Pakistan. The Academy has worked extensively on Balti linguistics, standardization for computing, and modeling of script, speech and language. Research is being conducted in all aspects of Balti, including acoustic phonetics, phonology, morphology, syntax, grammar and semantics.</p>

