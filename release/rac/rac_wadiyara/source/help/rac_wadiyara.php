<h2><span style='mso-fareast-font-family:"Times New Roman"'>Introduction<o:p></o:p></span></h2>

<p>This keyboard is designed for the Wadiyara language spoken in Sindh Pakistan, in its
standard Perso-Arabic script, as used in Pakistan. It is based on phonetic
keyboard for Wadiyara Language developed by Pakistani linguist, author, Scientist,
Inventor researcher and Software Developer Mr.Rehmat Aziz Chitrali for Khowar
Academy(an literary Association working on the language and culture
documentation in Northern Pakistan) used in Windows 8.1 and above as Wadiyara
Keyboard. </p>

<p>This is the first Wadiyara language keyboard created by Rehmat Aziz Chitrali,
Pakistani linguist, Scientist, Inventor, Software Developer and Researcher.
This keyboard is for Wadiyara Language based on the keyboard layout as developed
by Mr.Rehmat Aziz Chitrali and approved by Khowar Academy of Pakistan as
standard keyboard for Wadiyara language.</p>

<p>The Wadiyara Keyboard Project(WKP) was supervised and developed by Rehmat Aziz
Chitrali, who heads the Khowar Academy Chitral Pakistan.</p>

<p>Khowar Academy Chitral was established in April 1996 specifically to promote
the endangered languages of Chitral Pakistan. The Academy has worked
extensively on Wadiyara linguistics, standardization for computing, and modeling
of script, speech and language. Research is being conducted in all aspects of Wadiyara,
including acoustic phonetics, phonology, morphology, syntax, grammar and
semantics.</p>

