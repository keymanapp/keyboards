<p style="text-align: right; dir=rtl; font-size:150%">براهوئی زبان پاکستان کے صوبہ بلوچستان میں بولی جاتی ہے اس زبان میں جہاں رحمت عزیز چترالی کا براهوئی قاعدہ، براهوئی حروف تہجی، براهوئی خوشخطی، براهوئی سافٹ ویر، براهوئی اردو ذخیرہء الفاظ اور براهوئی ٹیکسٹ ایڈیٹر منظر عام پہ آ چکے ہیں وہاں براهوئی زبان بولنے والے نوجوانوں نے بھی اپنی مادری زبان براهوئی میں کتابیں لکھنے کا عزم ظاہر کیا ہے۔ کھوار اکیڈمی کے قیام کے بعد رحمت عزیز چترالی نے براهوئی زبان کے ساتھ ساتھ دیگر پاکستانی زبانوں کو کمپیوٹر پر تحریر کے قابل بنانے کے لیے کیبورڈ بنانے کا سلسلہ شروع کیا اور پاکستان کی چالیس زبانوں کو ایک ہی کیبورڈ کے زریعے تحریر کے قابل بناکر عالمی ریکارڈ قائم کرکے پاکستان کا نام عالمی سطح پر روشن کیا اور پاکستان میں بولی جانے والی زبان براهوئی کے لیے کیبورڈ بھی ایجاد کرنے کا سہرہ آپ ہی کے سر جاتا ہے اور آپ کے ایجاد کردہ کلیدی تختی کی مدد سے براهوئی زبان میں تحریر کا سلسلہ جاری ہے اور رحمت عزیز چترالی کا یہ کام براهوئی زبان کی تاریخ میں ایک سنگ میل کی حیثیت رکھتا ہے، پاکستانی زبانوں کے لیے بے لوث خدمات انجام دینے پر  ہم حکومت پاکستان سے رحمت عزیز چترالی کے لیے ''ایوارڈ برائے حسن کارکردگی'' کی سفارش کرتے ہیں امید ہے کہ حکومت پاکستان رحمت عزیز چترالی اور ان جیسے دیگر  پاکستانی ماہرین لسانیات اور ادیبوں  کے لیے ایوارڈو اعزازات کا اعلان کرے گی، رحمت عزیز چترالی نے پہلی بار کھوار زبان اور براهوئی زبان کو گوگل کیبورڈ اور جی بورڈ میں شامل کرکے ان معدومیت کا شکار زبانوں کو بچانے کے لیے اہم کردار ادا کیا جو کہ زبانوں کی تاریخ میں سنہرے حروف سے لکھا جائے گا

