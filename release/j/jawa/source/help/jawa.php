<h1 id="2">Usage Notes</h1>
<p>How to use the keyboard (for user), or read the codes (for developer)</p>
<ul>
    <li><b>Adeg-adeg</b> (sentence openings). Type the pipe symbol (|) to get 'adeg-adeg' ꧋. Type \ to get ꧊. 'Rerenggan kiwa/tengen' ꧁ and ꧂ can be typed using { and }.</li>
    <li><b>Nglegena.</b> This is the basic 20 letters (consonantal syllables), and also the 16 <b>murda</b> letters and <b>mahaprana</b> letters. They can stand alone by themselves, and by default assigned the voice of 'a' /ɔ/.</li>
    <li><b>Sandhangan</b> (vowels). This is the basic 4 vowels ('wulu', 'suku', 'taling', 'pepet'), and 6 long vowels (including 'tarung', see <b>double vowels</b>). They cannot stand alone by themselves, and must be joined by a <b>nglegena</b>. Also for the sake of category, I would include here the 8 'aksara swara' vowels (A/E/I/O/U/Ai/Ii/II), even though these vowels can stand by themselves, without nglegena. To type standalone sandhangan<ul>
        <li>'qe' - 'taling' ꦺ</li>
        <li>'qi' - 'wulu' ꦶ</li>
        <li>'qo' - 'taling tarung' ꦺꦴ</li>
        <li>'qu' - 'suku' ꦸ</li>
        <li>'qx' - 'pepet' ꦼ</li>
    </ul></li>
    <li><b>Letters.</b> When you type a letter, if it's a vowel (a/e/i/o/u/x), then you get the character 'ha' with the appropriate 'sandhangan' immediately (one-to-two mapping, or in the case of 'o', one-to-three). If you type a consonant, you will get the '<b>nglegena</b>' form with the <b>pangkon</b> attached (one-to-two, or one-to-three in the cases of 'f','v','z').</li>
    <li><b>X-Q rule.</b> Typing 'x' will give you ('ha'+)'pepet'. Typing 'q' will give you '<b>pangkon</b>'. These are the only arbitrary placement that you need to memorize. It just happened to fit nicely in QWERTY keyboard, because X and Q are unused letters (while 'f', 'v', and 'z', even though not found in native Javanese language, they are 'aksara <b>rekan</b>', and frequently used in words borrowed from Indonesian)</li>
    <li><b>Pangkon</b> (virama). So, why the '<b>nglegena</b>' was attached with 'pangkon' by default? Well, it's because if you just type a consonant, it could be anywhere, in the start, middle, or end of a syllable. If it's in the start, you just need to add a vowel, and the pangkon will be replaced by the appropriate '<b>sandhangan</b>' (minus the 'ha', they're not needed anymore), if it's in the middle, it will transform to '<b>wyanjana</b>', if it's 'r' or 'y'. If it's in the end, it would transform to '<b>sesigeg</b>' if it's 'r', 'h', or 'ng'<ul>
        <li><em>Rule 1: Pangkon + Vowel will remove the 'pangkon' and add a 'sandhangan' where appropriate.</em></li>
        <li><em>Rule 2: Pangkon + Consonant will turn the consonant into 'pasangan', followed by another 'pangkon'</em></li>
        <li><em>Rule 3: Pangkon + Consonant + Pangkon + Consonant will put a Zero-Width Joiner after first pangkon, so that it won't try to form a double subscript glyphs.</em></li></ul>
    </li>
    <li><b>Sesigeg</b> (final consonants). If you type an 'r', an 'h', or an 'n'-followed-by-'g', you will get a 'sesigeg' ('layar', 'wignyan', or 'cecak'). If it is immediately followed by a '<b>wyanjana</b>' and/or a vowel, they will transform to 'ra', 'ha', and 'nga' automatically (+ applicable '<b>sandhangan</b>'), because 'layar', 'wignyan', or 'cecak' are basically 'ra pangkon', 'ha pangkon', and 'nga pangkon'. So all rules regarding '<b>pangkon</b>' + '<b>letter</b>' would apply to them as well. To type them independently, you can use:<ul>
        <li>'R' - 'layar'</li>
        <li>'H' - 'wignyan'</li>
        <li>'Y' - 'cecak'</li></ul></li>
    <li><b>Double consonants.</b> If you type these two/three letters in sequence, you would get different character (replacing the first one)<ul>
        <li>'ny' - 'nya pangkon' ꦚ꧀ (replacing 'na pangkon' ꦤ꧀)</li>
        <li>'ng' - 'cecak' &nbsp; ꦁ (see <b>sesigeg</b>)</li>
        <li>'nn' - 'na murda' ꦟ꧀</li>
        <li>'th' - 'tha pangkon' ꦛ꧀ (replacing 'ta pangkon' ꦠ꧀)</li>
        <li>'dh' - 'dha pangkon' ꦝ꧀ (replacing 'da pangkon' ꦢ꧀)</li>
        <li>'nj' - 'nya pangkon ja pangkon' ꦚ꧀ꦗ꧀ (nasal sound)</li>
        <li>'nc' - 'nya pangkon ca pangkon' ꦚ꧀ꦕ꧀ (nasal sound)</li>
    </ul></li>
    <li><b>Double vowels.</b> If you type these two/three letters in sequence, you would get different character (replacing the first one)<ul>
        <li>'aa' - 'tarung' ꦴ</li>
        <li>'ai' - 'dirga mure' ꦻ</li>
        <li>'au' - 'dirga mure + tarung' ꦻꦴ</li>
        <li>'ii' - 'wulu melik' ꦷ</li>
        <li>'i'/'e' + any other vowel - instead of 'ha' in the middle, you get 'ya'</li>
        <li>'uu' - 'suku mendhut' ꦹ</li>
        <li>'u'/'o' + any other vowel - instead of 'ha' in the middle, you get 'wa'</li>
    </ul></li>
    <li><b>Wyanjana</b> (medial consonants). If you type a consonant followed by an 'r' or a 'y', you will get 'wyanjana' ('cakra' or 'pengkal'), followed by a '<b>pangkon</b>'*. You need to type a vowel afterwards, lest you would have an invalid 'wyanjana'+'pangkon' combination. If you type a consonant followed by an 'r' followed by an 'x', you will get a 'cakra keret' instead of 'cakra + pepet'. To type them independently, you can use:<ul>
        <li>'&' (SHIFT+7) or (ALT+7) - 'cakra keret'</li>
        <li>'(' (SHIFT+8) or (ALT+8) - 'pengkal'</li>
        <li>')' (SHIFT+9) or (ALT+9) - 'cakra'</li>
        <li>*Now, I know it's highly unusual to see 'wyanjana' with 'pangkon', but I decided for the sake of code brevity. Otherwise I have to make separate rules for 'wyanjana'. Instead, since they ends with 'pangkon', all the rules for 'pangkon' also applies for them. In one way, it makes sense, because if you only type 'br' for example, it doesn't have a vowel yet. If you leave it like that, of course it's wrong. Only after you type a vowel would the 'pangkon' get replaced by applicable '<b>sandhangan</b>'</li></ul></li>
    <li><b>Rekan.</b> They're supplementing the letters that aren't found in Javanese language, namely 'f/v/z'. Some sources equate 'f' and 'v' as 'pa + cecak telu', while some others differentiate them. I subscribe to the second group: 'f' as 'pa + cecak telu'(+'pangkon') and 'v' as 'wa + cecak telu'(+'pangkon'). 'z' is 'ja + cecak telu'(+'pangkon'). Writing 'f/v/z' would also ends with '<b>pangkon</b>'. For other rekan that uses 'cecak telu':<ul>
<li> ꦒ꦳ GA + CECAK TELU = غ (γa).
</li><li> ꦗ꦳ JA + CECAK TELU = ز (za).
</li><li> ꦢ꦳ DA + CECAK TELU = ذ (dza).
</li><li> ꦦ꦳ PA MURDA + CECAK TELU = ف (fa).
</li><li> ꦔ꦳ NGA + CECAK TELU = ع ('a).
</li><li> ꦱ꦳ SA + CECAK TELU = ش (ša).
</li><li> ꦲ꦳ HA + CECAK TELU = ح (ḥa).</li></ul>
For these letters there are no shortcuts, you have to be creative, and found a way to write it your own with the available means. For example, to type 'dza', you can type 'da'+'~' (tilde), 'dzi' = 'da'+'~'+'q'+'	i'. <br />For Chinese 'rekan', I couldn't find any reliable source regarding their history, list, and usage (pronounciation), only from <a href='https://en.wikipedia.org/w/index.php?title=Javanese_script&oldid=880704499#Additional_Aksara'>Wikipedia</a>, and they're archaic/not proper pinyin/mandarin and not suitable for modern usage. I'm not listing them here for now, because I'm planning to build a systematic index of every Chinese syllable and their Javanese script counterpart (<a href="https://id.wiktionary.org/wiki/Indeks:Pinyin-Aksara_Jawa">Indeks:Pinyin-Aksara Jawa</a>).</li>
    <li><b>Murda</b> and Mahaprana. I store them in the capital Latin consonant letters, but it doesn't necessarily mean they're Javanese capital letter, it's just for convenience and mnemonic. Their number is only 10 single-letter Murda ('KGCJTDNPBS') and 3 multi-letters Murda. Below are some <b>double consonant</b> combinations for murda letters.<ul>
        <li>'Th' - 'tta mahaprana pangkon' ꦜ꧀ (replacing 'Ta pangkon' ꦡ꧀)</li>
        <li>'Dh' - 'dda mahaprana pangkon' ꦞ꧀ (replacing 'Da pangkon' ꦣ꧀)</li>
        <li>'jny' - 'nya murda pangkon' ꦘ꧀</li>
        <li>the 10 murda above can also be written by typing the 10 corresponding nglegena with 'h'. So 'kh' = "K", 'gh' = 'G', etc. <em>Except</em> 'dh' and 'th'.</li>
    </ul>
The other 10 capital Latin consonant letters (QZHRY) and (FVMLW) doesn't store 'murda' letters. I used Q for 'Ka Sasak', Z for 'Sa Mahaprana' (to differentiate from S, 'Sa Murda'), and H, R, Y for '<b>wyanjana</b>'. If you typed a murda it will also attached by '<b>pangkon</b>' and the above 'pangkon' rules apply. (FVMLW) are currently unused. The six capital Latin vowel letters and x (AEIOUX) are used for 'aksara swara' (see <b>sandhangan</b> above). Some special 'aksara swara' (long vowels):<ul>
        <li>'Aa' - 'A tarung' ꦄꦴ (long-A)</li>
        <li>'Ai' - 'Ai' ꦍ</li>
        <li>'Ii' - 'Ii' ꦇ (long-I)</li>
        <li>'II' - 'I Kawi' ꦅ</li>
        <li>'Uu' - 'U tarung' ꦈꦴ (long-U)</li>
        <li>'Oo' - 'O tarung' 'Au' ꦎꦴ (long-O)</li>
</ul></li>
    <li><b>Special</b> letters. 'Aksara ganten'<ul>
        <li>'rx' - 'pa cerek' ꦉ (replacing 'ra pepet' ꦫꦼ)</li>
        <li>'lx' - 'nga lelet' ꦊ</li>
        <li>'RR' - 'ra agung + pangkon' ꦬ꧀</li>
    </ul></li>
    <li><b>Numbers.</b> For numbers, you just typed it normally. The only difference with normal numbers is that Javanese numbers are always enclosed/surrounded by 'pada pangkat' ꧇ (looks like a semicolon) by typing a semicolon ':'. Rule: <ul>
        <li>when a non-number followed by number, it's separated by 'pada pangkat'</li>
        <li>when a number followed by non-number, it's separated by 'pada pangkat' as well</li>
        <li>if the number is in the beginning, it's started with 'pada pangkat'</li>
        <li>if a number is at the end, followed by period ('pada lungsi')</li>
        <li>a number will be ended with 'pada pangkat' before the 'pada lungsi' automatically, except if it's at the very end, and there's no more character to follow, then you have to manually close the 'pada pangkat' by typing semicolon ':'.</li></ul></li>
    <li><b>Special</b> characters. See the visual mapping of the keyboard for reference, or the table above. They might move in the future. I'm trying to make it as intuitive as possible. Some special combinations:<ul>
        <li>꧅꧉ꦧ꧀ꦖ꧉꧅or ꧅ꦧ꧀ꦖ꧅or ꧅ꦧ꧀ꦕ꧅'purwapada' - to introduce a poem. <br /><br />It can be typed as such: '/.bcha./' or '/bcha/' or '/bCa/' or '/bca/'</li>
        <li>꧅꧉ꦟ꧀ꦢꦿ꧉꧅or ꧅ꦟ꧀ꦢꦿ꧅'madyapada' - to indicates a new song within a poem. <br /><br />It can be typed as such: '/.Ndra./' or '/Ndra/'</li>
        <li>꧅꧉ꦆ꧉꧅or ꧅ꦆ꧅'wasanapada' - to end a poem. <br /><br />It can be typed as such: '/.I./' or '/I/'</li>
    </ul></li>
    <li><b>Invalid</b> combinations. I have tried to reduce the possibility of invalid combinations as minimal as possible.<ul>
        <li>Combination of a '<b>pangkon</b>', '<b>sesigeg</b>', '<b>wyanjana</b>', '<b>panyangga</b>' and themselves is not allowed (eg. q+q)</li>
        <li>Combination of a 'number' and number's 'pada pangkat', and a 'pangkon', 'sesigeg', 'wyanjana', 'panyangga' is not allowed. (But the reverse is allowed)</li>
        <li>Combination of a 'pada pangkat' and another 'pada pangkat' is not allowed</li>
    </ul></li>
</ul>

