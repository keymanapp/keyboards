<h1>Sobre Latinoamérica Habla</h1>
<p><b><a href="http://latinoamericahabla.com/">Latinoamérica Habla</a></b> es una organización de la sociedad civil, donde trabajamos por la protección, revitalización y digitalización las lenguas indígenas de latinoamérica. Promovemos la inclusión de las comunidades originarias, respetando su cosmovisión e igualando oportunidades. Utilizamos la tecnología como herramienta fundamental para la preservación de las lenguas en peligro de extinción.<br> 
El teclado QOM, es una estrategia más en la tarea de revitalizar y promover los idiomas indígenas entre sus hablantes. Llevamos la lengua a los soportes tecnológicos que utilizan los usuarios para promover su uso, pero también a la sociedad en general para darle visibilidad.
Para el diseño del teclado, contamos con la participación activa de la comunidad QOM de Chaco, Argentina quienes propusieron el proyecto y formaron parte del desarrollo del mismo.<br>
