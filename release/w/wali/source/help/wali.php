    <h2>Introducing the Wali (GH) Language Keyboard</h2>
    <p>
        Welcome to the new keyboard designed for the Wali (Waalɩɩ) language! This tool makes it easier than ever to type in Wali on all your devices.
    </p>

    <h2>Who is this for?</h2>
    <p>
        This keyboard is for anyone who wants to type in Wali on their Android smartphone, iPhone, iPad, Windows, Mac, or other touch devices. 
        Wali, with the ISO 639-3 language code wlx, is spoken in and around Wa, the capital of Ghana's Upper West Region. It's also a major trading language that many people in the region use and understand.
    </p>

    <h2>Why use this keyboard?</h2>
    <p>
        With this keyboard, you can now write in Wali across all your favorite platforms. 
        Whether you're sending emails, chatting on WhatsApp, posting on Facebook, or contributing to Wikipedia and other blogs, 
        you can express yourself in your native language.
    </p>

    <h2>How does it work?</h2>
    <p>
        The keyboard is based on the familiar English QWERTY layout, but it gives you easy access to all the unique Wali characters. 
        The special vowels <kbd>ɛ</kbd>, <kbd>ʋ</kbd>, <kbd>ɩ</kbd>, <kbd>ɔ</kbd>, and the letter <kbd>ŋ</kbd> are all available with a single touch.
    </p>

    <p>
        These layouts follows the official Wali orthography as proposed by Abdul Moomin, Abdul-Aziz in his 2015 Master's Thesis, "Aspects of Waali phonology," from the University of Ghana.
    </p>   

    <h2>Desktop Keyboard layouts</h2>
    <ul>To get the following vowels (a,e,i,o,u) with the acute, grave or tilde accent, type:
        <li><kbd>'</kbd> + <kbd>[vowel]</kbd> = <kbd>Vowel with Accute</kbd></li>
        <li><kbd>`</kbd> + <kbd>[vowel]</kbd> = <kbd>Vowel with Grave</kbd></li>
        <li><kbd>~</kbd> + <kbd>[vowel]</kbd> = <kbd>Vowel with Tilde</kbd></li>
    </ul>

    <div id='osk' data-states='default shift rightalt rightalt-shift'>
