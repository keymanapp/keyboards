<p class=MsoNormal>This package includes one keyboard file, Greek Polytonic
Plus.&nbsp; It does not include any fonts.&nbsp; Most computers already have fonts
installed that supply almost all the characters covered by this keyboard. Times
New Roman and Calibri have them all except the angle brackets, which are found
in Cambria Math; these fonts are typically installed with Windows.&nbsp; On Mac OS, Times New Roman is perhaps the best choice; it lacks only capital Jot, capital Kai ligature, some of the archaic letters and the angle brackets. If you
need additional fonts, <a
href="https://classicalstudies.org/publications-and-research/nau-download">New
Athena Unicode</a> is complete, as the <a
href="https://brill.com/page/BrillFont/brill-typeface">Brill Typeface</a> (it
omits the Coptic letters).&nbsp; There are many other polytonic Greek fonts
available which can easily be found on line.&nbsp; </p>

<p class=MsoNormal>&nbsp;</p>

<p class=MsoNormal>After installing, click the Keyman icon and choose Greek
Polytonic Plus.&nbsp; You can also use <span style='font-size:11.0pt;
font-family:"Calibri",sans-serif'>Windows Key</span><span style='font-size:
11.0pt'>-</span><span style='font-size:11.0pt;font-family:"Calibri",sans-serif'>Spacebar</span>
to move among installed keyboards; <span style='font-size:11.0pt;font-family:
"Calibri",sans-serif'>Windows Key-Ctrl-Spacebar</span> takes you back to the
keyboard that was previously in use. &nbsp;On a Mac, use <span
style='font-size:11.0pt;font-family:"Calibri",sans-serif'>Control-Spacebar</span>
to select the previous input source, or <span style='font-size:11.0pt;
font-family:"Calibri",sans-serif'>Control-Option-Spacebar</span> for the next
input source in the Input menu.  To access the on-screen keyboard in Windows,
type <span style='font-size:11.0pt;font-family:"Calibri",sans-serif'>Alt-Shift-K</span>;
on Mac OS, choose On-Screen Keyboard under Keyman in the Input Source menu.</p>

