<p>It is sometimes necessary in the Gondi language to use a halant
part way through a word, especially on a suffix boundary. In order
to do this, one must press the <b><span style='color:blue'>x</span></b> key <i>twice</i>
(to add the Zero-Width Non Joiner U+200C).</p>

<p>If the Gunjala digits are required, these can be typed by pressing the <span style='color:blue'>`</span> key (back-tick or tilde ~ key which is to the left of digit 1) prior to the digit.</p>

