<h2>Mobile Keyboard Typing</h2>
<p>In mobile keyboard, the user must frequently toggle to different layers (lower, upper, numeric) to type some of the Geez letters. This toggling makes it inconvenient and slows down the flow of typing speed. This issue has been resolved in GeezWord Mobile.</p>

<h3>GeezWord Mobile Keyboard Typing</h3>
<p>We have enhanced GeezWord mobile keyboard layout for speed and convenience. Two main enhancements were made. In the first enhancement, you <strong>Press Twice</strong> a key, instead of switching to the Shift layer to type a letter or instead of selecting a letter from popup screen.</p>

<h3>Speed Typing (“Press Twice”)</h3>
