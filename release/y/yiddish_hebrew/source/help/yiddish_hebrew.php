<p>This layout, useful for both Yiddish and Hebrew, is based on a design created by Mark David for UYIP which was based on the standard Hebrew keyboard used in Israel (which itself was created from earlier Yiddish typewriter keyboard layouts). A retrofited design which is of particular ease of use for those who are already proficient with a standard Israeli keyboard. This version was implemented for Keyman by Gyula Zsigri.</p>

<ul>
    <li>A <em>geresh</em> typed after a <em>geresh</em> turns the geresh
into <em>gershayim</em>.</li>
<li>A <em>hyphen</em> typed after a <em>hyphen</em> turns the hyphen
into an <em>en dash,</em> and a hyphen typed after an en dash turns the
en dash into an <em>em dash</em>.</li>
</ul>

<p>
<strong>Tip.</strong>&nbsp; If you ever need two consecutive hyphens
then type hyphen, space and hyphen again and delete the space.</p>

