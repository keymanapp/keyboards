<p>
    Keyboard for the Unicode Deseret Alphabet used in the mid to late 1800's by The Church of Jesus Christ of Latter-day Saints.
</p>
<p>This keyboard is designed to enable simple input of all the Deseret Unicode characters.</p>
<p>The small letters are shown in the table below, for capitals use the corresponding capital keys. All other keys have their normal function so that numbers, etc. are not affected by this encoding.</p>
	<p><a href="deseret_chart1.png"><img class="keyboard" src="deseret_chart1.png" /></a></p>

<p>The letters change as you type as needed; for example, when you type the first i it turns into 𐐮 and then when you type the second i the 𐐮 turns into 𐐨. The \ key can be used to prevent the above from happening and force a, e, g, h, i, o, u, w, y encoding as a separate letter (see table below for examples).</p>
	<p><a href="deseret_chart2.png"><img class="keyboard" src="deseret_chart2.png" /></a></p>

