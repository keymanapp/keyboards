<p>
  This keyboard is designed to allow typing à la the Navajo font, but producing Unicode characters that are viewable in most modern fonts.
</p>
<p>
  The font downloaded from <a href="https://www.dinecollege.edu/wp-content/uploads/2020/10/Timenrn_.ttf">Diné College</a> allows
  typing in Navajo, but uses Unicode characters that are not correct. This keyboard will allow the user to type as though using that
  font, but producing the correct Unicode characters for better compatibility with other software and users.
</p>
<p>
  A good discussion of why this is needed is given at the <a href="https://navajonow.wordpress.com/2011/07/14/navajo-font-vs-navajo-keyboard">NavajoNow website.</a>
</p>
<p>
  Several other keyboards have already been created for Navajo, including one at <a href="https://languagegeek.com/2024lang/nav.html">LanguageGeek</a>,
  one already on <a href="https://keyman.com/keyboards/fv_dine_bizaad?bcp47=nv-latn">Keyman</a>, and one you can install in Windows. However,
  the one from LanguageGeek is the only one of these that actually seems to support Diné Bizaad, and it is somewhat difficult to use, requiring the
  use of "dead keys" to create the letter forms that are unique to Navajo (like ą́).
</p>
<p>
  This keyboard was created in partnership with the Navajo community, fulfilling a desire to type their language using a method
  that was already familiar to them. By putting the correct Unicode letters in the places of the characters previously used by the
  font, people who have previously used the font can now type in Unicode without learning something new. By using the "alt" and "shift"
  keys to add to the keyboard, all of the characters produced by a normal North American keyboard can still be typed, while
  making it easier to type Diné Bizaad.
</p>

