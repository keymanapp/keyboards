<h1>The Dazaga Gourane Karra keyboard for Keyman</h1>

<p>This keyboard layout is designed for the Dazaga Gourane Karra Language and was developed with 
    the help of the <a href=https://typlab.linguistik.hu-berlin.de/>Working Group General Linguistics (Typ*Ex Lab) at Humboldt-University of Berlin</a>. 
    <br>It includes both a touch layout for smartphones and a layout for physical keyboards. 
    <br>The keyboard will work with Times New Roman or most other Unicode encoded fonts.</p>

<h2>The smartphone keyboard</h2>

<p><a href="phone.jpg"><img class="keyboard" width="300" height=auto src="phone.jpg"/></a></p>

<h2>Usage on physical keyboards</h2>

<p>The acute and grave accents are typed just like using standard latin keyboards, by typing the accent followed by the desired vowel:</p>
<ul>
<li>` + aeiou = àèìòù</li>
<li>´ + aeiou = áéíóú</li>
</ul>
<p>For keyboards without the ´-key, the apostrophe (') can alternatively be used. The semicolon is used in the same way for the other characters not included in an English layout:</p>
<ul>
<li>; + a = ä</li>
<li>; + e = ë</li>
<li>; + o = ö</li>
<li>; + g = ŋ</li>
<li>; + y = ɲ</li>
<li>; + n = ṅ</li>
<li>; + s = ṡ</li>
<li>; + r = ṙ</li>
</ul>
<p>The tonal markers are typed after vowels:</p>
<ul>
<li>a + ^ = â</li>
<li>a + | = ā</li>
<li>a + ~ = ã</li>
</ul>
<p>This way tonal markers can be combined with non-standard characters easily:</p>
<ul><li>; + a + | = ǟ</li></ul>

<p>The on-screen keyboard sadly is not working as of yet, unless windows is set to an English keyboard layout.</p>
