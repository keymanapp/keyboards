<h1>Die Dazaga Gourane Karra Tastatur für Keyman</h1>

<p>Dies ist eine Tastaturbelegung für die Dazaga-Gourane-Karra-Sprache, die mit der Hilfe der 
    <a href=https://typlab.linguistik.hu-berlin.de/>Arbeitsgruppe Allgemeine Sprachwissenschaft (Typ*Ex Lab) der Humboldt-Universität zu Berlin</a> entwickelt wurde. 
    Sie beinhaltet sowohl eine Handytastatur als auch eine Belegung für physische Tastaturen.
    <br>Die Tastatur funktioniert mit Times New Roman oder den meisten anderen Unicode-Schriftarten.</p>

<h2>Die Handy-Tastatur</h2>

<p><a href="phone.jpg"><img class="keyboard" width="300" height=auto src="phone.jpg"/></a></p>

<h2>Verwendung für physische Tastaturen</h2>

<p>Akut und Gravis werden wie auf anderen lateinischen Tastaturen getippt, indem der Akzent gefolgt von dem gewünschten Vokal eingegeben wird:</p>
<ul>
<li>` + aeiou = àèìòù</li>
<li>´ + aeiou = áéíóú</li>
</ul>
<p>Für Tastaturen ohne eine ´-Taste kann alternativ das Apostroph (') genutzt werden. Das Semikolon wird in gleicher Weise für die anderen nicht standardmäßig auf der Tastatur vorhandenen Zeichen verwendet:</p>
<ul>
<li>; + a = ä</li>
<li>; + e = ë</li>
<li>; + o = ö</li>
<li>; + g = ŋ</li>
<li>; + y = ɲ</li>
<li>; + n = ṅ</li>
<li>; + s = ṡ</li>
<li>; + r = ṙ</li>
</ul>
<p>Die Tonmarker werden nach den jeweiligen Buchstaben getippt:</p>
<ul>
<li>a + ^ = â</li>
<li>a + | = ā</li>
<li>a + ~ = ã</li>
</ul>
<p>So können sie auch einfach mit den durch Semikolon erstellten Zeichen kombiniert werden, beispielsweise:</p>
<ul><li>; + a + | = ǟ</li></ul>

<p>Die Bildschirmtastatur funktioniert zur Zeit leider noch nicht, außer die Tastatursprache von Windows ist auf Englisch eingestellt.</p>
