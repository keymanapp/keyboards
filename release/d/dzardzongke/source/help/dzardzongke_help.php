<h1>Keyboard Layout</h1>

<h2>Desktop Keyboard Layout</h2>
<p> On a desktop keyboard, the letters 'á Á é É í Í ó Ó ú Ú' do not have their own keys, but are made by pressing ';' and then the vowel. For example, pressing ';' and then 'a' makes 'á', pressing ';' and then 'A' makes 'Á'.  To type ';' by itself, you need to press the ';' key twice. </p>
