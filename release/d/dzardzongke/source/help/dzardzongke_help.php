<h2>Mobile Keyboard Layout</h2>
<p> On a phone or tablet, you can see and select 'á Á é É í Í ó Ó ú Ú' if you press and hold on a vowel. For example, pressing and holding 'a' will show the option to type 'á', pressing and holding 'A' will show the option to type 'Á'. </p>
