<p>The keyboard layout is the same as that of the English keyboard; but, as you type, certain key sequences would output certain characters. For example, if you type a semicolon ";" and then key "A", the output would be <b>ƒÅ</b>.</p>
<p>See <a href="balochi_scientific.pdf">this document</a> for all the key combinations.</p>

<p>For Android and iOS, you may use either long press or key combinations. Long press on "a" and more related characters will show up.</p>

