	<h1>Start Using Bukawa</h1>
	<p><b>1. Desktop/laptop keyboards</b></p>
	
		<p>The
		Bukawa keyboard utilizes the 'deadkey' function of Keyman. The square bracket
		&quot;[&quot; is the designated deadkey.</p>
		
		<p>To
		input any of the special characters type the &quot;[&quot; followed by the relevant
		base key (see below).</p>
		
		<p><b>2. Phone and Tablet keyboards</b></p>
		
		<p>The
		Keyman keyboard for tablets and phones uses a long press on the relevant key to
		bring up a pop-up text box from which the character can be selected. The same
		base keys as used for computer keyboards have been used for the long press
		function.</p>
		
			
		<h2>Keyboard Layout</h2>
		
		<p>NOTE: for phone and
		tablet keyboards, just use the ‘long press’ on the same base keys for each
		special character.</p>
		
		<p><b>1. Vowels</b></p>
		
		<p><b><span style='font-family:"Charis"'>ê</span></b> or <b><span style='font-family:"Charis"'>Ê</span></b> --– type “[“ followed by “e” or
		“E”<br>
		<b><span style='font-family:"Charis"'>ô</span></b> or <b><span style='font-family:"Charis"'>Ô</span></b> --- [ + o/O<br>
		<b><span style='font-family:"Charis"'>ö</span></b> or <b><span style='font-family:"Charis"'>Ö</span></b> --- [ + q/Q    (Used in some Bukawa dialects)</span></p>
		
		<p><b>2. Eng symbol</b></p>
		
		<p><b><span
		style='font-family:"Charis"'>&#331;</span></b> or <b><span style='font-family:"Charis"'>&#330;</span></b> --– long press  n/N (also works with j/J)</p>
		
		<p><b>3. Tones</span></b></p>
		
		<p>High tone --- [ +<b> h</b>  (or<b>
		/</b> on computer keyboards)<br>
		Low tone  --- [ +<b> l</b>   (or<b> \</b> on computer keyboards)</p>
		
		<p><b>NOTES:</b></p>
		
		<p>1. To ‘over-ride’ the deadkey
		function and type the left square bracket “<b>[</b>“ on a computer keyboard
		just type it twice.</p>
		
		<p>2. With many fonts the upper
		case ‘eng’ (<b>&#330;</b>) will display as the upper case N with a tail. If it
		is preferred to display it in as a larger form of the lower case <b><span style='font-family:"Charis"'>&#331;</span></b>
		you may have to change fonts. <b>Charis</b> is one font that will display
		it as <b><span style='font-family:"Charis"'>&#330;</span></b>.</span></p>
		

