      the Burushaski&nbsp; language. This particular orthography has been
      proposed and agreed upon by representatives of all three varieties of
      Burushaski&nbsp; of Hunza, Yasin and Nagar. The unique characters of
      Burushaski, mostly retro-flexes and diacritic vowels have been explained
      in this document. </p>
