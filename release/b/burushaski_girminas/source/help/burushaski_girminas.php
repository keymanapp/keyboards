<h1 class="style11">Overview</h1>
<p>Burushaski orthography encompasses the spelling and punctuations of the Burushaski language. This particular orthography
  has been proposed and agreed upon by representatives of all three varieties of Burushaski of 
Hunza, Yasin and Nagar. The unique characters of Burushaski, mostly retroflexes and diacritic vowels have been colored in this document. </p>
<p>Burushaski Girminas is latin/Roman based keyboard developed for Buruśo Mahraka, and it is available for free.</p>
