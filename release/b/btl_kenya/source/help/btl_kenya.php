<p>
This keyboard was developed for use by BTL (Bible Translation & Literacy) for language teams in Kenya.
</p>

<p>This keyboard makes use of modifier keys. Typing the modifier key before the base character will output the new or combined character. For example:</p>
<p>;a will produce ɑ (available on aA).</p>
<p>'a will produce á (available on aAɑⱭeEiIoOuU).</p>
<p>`a will produce à (available on aAɑⱭeEiIoOuU).</p>
<p>:a will produce ä (available on aAɑⱭeEiIoOuU).</p>
<p>^i will produce î (available on iIuU).</p>
<p>=a will produce ā (available on aAɑⱭeEiIoOuU).</p>
<p>.b will produce ḅ (available on bBdD).</p>
<p>]d will produce d̯ (available on dDtT).</p>
<p>_v will produce v̱ (available on vV).</p>

