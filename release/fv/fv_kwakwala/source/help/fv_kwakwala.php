<h2>Keyboard Layout </h2>
<ul>
    <li>The special Characters of Kwak̕wala have been assigned to various punctuation keys</li>
    <li>All accents are typed after the base character: <strong>a̱</strong> is typed <strong>a</strong> then
        <strong>semicolon ;</strong>
        <ul>
            <li>To type the glottalized accent when it appears over a single letter use the <strong>slash key
                    /</strong><br />
                <strong>k̓</strong> is typed <strong>k</strong> then <strong>/</strong>
            </li>
            <li>To type the glottalized accent when it appears between two letters use the <strong>right bracket key
                    ]</strong><br /> <strong>k̕w</strong> is typed <strong>k</strong> then <strong>]</strong> then
                <strong>w</strong>
            </li>
            <li>To type the underline accent use the <strong>semicolon key ;</strong><br /> <strong>ḵ</strong> is typed
                <strong>k</strong> then <strong>;</strong>
            </li>
        </ul>
    </li>
    <li>To type the slash - ł use the <strong>left bracket key [</strong></li>
    <li>All changed punctuation can type their original value by holding down the Right-Alt or Option key. To get the
        original value of the left-bracket [ type <strong>Right-Alt</strong>+<strong>[</strong> (Windows)
        <strong>Option</strong>+<strong>[</strong> (Mac).
    </li>
    <ul>
        <li><b>NOTE</b>: To type / while using Google Drive, use
            <strong>Right-Alt</strong>+<strong>Shift</strong>+<strong>/</strong> (Windows) /
            <strong>Option</strong>+<strong>Shift</strong>+<strong>/</strong> (Mac)
        </li>
    </ul>
    </li>
    <li> Opening and closing quotes. For Mac users, Right-Alt is either of the Option Keys.
        <ul>
            <li>single: ‘ Backquote — ’ Shift+Right Bracket</li>
            <li>double: “ Shift+Hyphen — ” Shift+Equals</li>
            <li>single ‹ Right-Alt+Shift+9 — › Right-Alt+Shift+0 </li>
            <li> double « Right-Alt+9 — » Right-Alt+0</li>
        </ul>
    </li>
</ul>
