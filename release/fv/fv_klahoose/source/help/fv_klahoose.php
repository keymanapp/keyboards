<h2>Desktop Keyboard Layout</h2>
<div id='osk' data-states='default'></div>

<ul>
	<li>The "Default" keymap above shows how to type the following characters in Éy7á7juuthem.</li>
		<ul>
			<li>ƛ	—	[ <em>left bracket on English layout</em></li>
			<li>θ	—	] <em>right bracket</em></li>
			<li>ə	—	; <em>semicolon</em></em></li>
			<li>ʔ	—	/ <em>slash</em></li>
		</ul>
	<li>To type a <strong>combining accent</strong>, first type the base alphabet character (such as <strong>a</strong>), and then type the accent key as listed below.</li>
	<ul>
		<li>Acute accent (◌́)	—	Backtick ` (next to 1)</li>
			<ul><li>Example: e + ` = é </li></ul>
		<li>Combining glottal apostrophe (◌̓)	—	Apostrophe '</li>
			<ul><li>Example: m + ' = m̓ </li></ul>
		<li>Caron (◌̌)	—	Backslash \ </li>
			<ul><li>Example: c + \ = č</li></ul>
	</ul>
	<li>You can type the original English value of the keys by holding down the Right-Alt or Option key.</li>
		<ul><li>Example: The left bracket [ is typed Right-Alt+[ (Windows) or Option+[ (Mac).</li></ul>
</ul>

<div id='osk' data-states='shift'></div>

<ul>
	<li>The "Shifted" keymap above shows characters in Éy7á7juuthem that can be typed with SHIFT key combinations.</li>
		<ul>
			<li>ɛ	— SHIFT + e</li>
			<li>ʊ	—	SHIFT + u</li>
			<li>I	—	SHIFT + i</li>
			<li>ɔ	— SHIFT + e</li>
			<li>æ	—	SHIFT + a</li>
			<li>ɬ	— SHIFT + l</li>
			<li>χ	— SHIFT + x</li>
			<li>w̥	— SHIFT + v</li>
		</ul>
	<li>You can type the original English value of the keys by holding down the Right-Alt or Option key.</li>
		<ul><li>Example: Capital A is typed RightAlt + a (Windows) or Option + a (Mac).</li></ul>
</ul>
	
<h3>Typing raised characters</h3>
	<li>To type a <strong>raised character</strong>, first type the base alphabet character (such as <strong>y</strong>, and then type the Caret ^ (Shift + 6).</li>
		<ul>
		<li>Raised ᶿ: θ + ^ <em>or</em> t + ^</li> 
		<li>Raised ʷ: w + ^</li>
		<li>Raised ʸ: y + ^</li>
		</ul>
		
</ul> 

