<h2>Desktop keyboard</h2>

<p>To type in SENĆOŦEN on Mac or PC, turn <kbd>CAPS LOCK</kbd> on. To type a special character, type the base character and then type an accenting key. (Ṯ is typed <kbd>T</kbd> then Semicolon <kbd>;</kbd>)</p>

<img src="Sencoten.png" />

<ul>
	<li>To type the <b>acute accent ´</b> use the Apostrophe key <kbd>'</kbd> — Ć is typed <kbd>C</kbd> then Apostrophe <kbd>'</kbd></li>
	<li>To type the <b>slash accent</b> use the Slash key <kbd>/</kbd> — Ⱥ is typed <kbd>A</kbd> then Slash <kbd>/</kbd></li>
	<li>To type the <b>dash accent</b> use the Hyphen key <kbd>-</kbd> — Ŧ is typed <kbd>T</kbd> then Hyphen <kbd>-</kbd></li>
	<li>There are two options to type the <b>low line accent</b>:
		<ul>
			<li>Use the Semicolon key <kbd>;</kbd> — Ṉ is typed <kbd>N</kbd> then Semicolon <kbd>;</kbd></li>
			<li>Use the Underscore key <kbd>_</kbd> — Ṉ is typed <kbd>N</kbd> then <kbd>Shift</kbd>+<kbd>_</kbd> Underscore</li>
		</ul>
	</li>
	<li>To type cedilla (glottal stop) press <kbd>Shift</kbd>+<kbd>,</kbd> (Comma) OR turn on <kbd>Caps Lock</kbd> and type Comma <kbd>,</kbd> (Cedilla is treated as capitalized comma.)</li>
	<li>Opening and closing quotes. For Mac users, <kbd>Right-Alt</kbd> is either of the <kbd>Option</kbd> Keys.
		<ul>
			<li>double: « Left-Bracket <kbd>[</kbd> — » Right-Bracket <kbd>]</kbd></li>
			<li>single: ‹ <kbd>Shift</kbd>+<kbd>[</kbd> Left-Bracket — › <kbd>Shift</kbd>+<kbd>]</kbd> Right-Bracket</li>
		</ul>
	</li>
	<li>The original English punctuation keys can always be typed by holding down the <kbd>Right-Alt</kbd> key (Windows) or <kbd>Option</kbd> key (Mac).
		<ul>
			<li>The slash / is typed <kbd>Right-Alt</kbd>+<kbd>/</kbd> (Windows) or <kbd>Option</kbd>+<kbd>/</kbd> (Mac).</li>
		</ul>
	</li>
</ul>

