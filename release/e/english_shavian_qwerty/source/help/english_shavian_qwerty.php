  <h2>Phone and tablet layouts</h2>
  <p>The layouts for phone and tablet are substantially similar to each other, and are based on the desktop layout with some tweaks to avoid the keys becoming too small. Unlike the desktop keyboard, additional letters are produced by holding down (long hold) keys.</p>
  <p>Pressing the [123] key will bring up the numbers and a range of symbols, arranged in a similar layout to the standard iOS keyboard.</p>
  <p>Pressing the [abc] key will bring up a standard US English QWERTY layout keyboard to allow typing in the Latin alphabet. Long hold keys allow the entry of a selection of diacritics, common International Phonetic Alphabet symbols used for English, as well as Esperanto letters (for those interested in Shavian for Esperanto, the Ŝava alfabeto).</p>
  
