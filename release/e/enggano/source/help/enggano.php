<h1>Using the desktop/laptop keyboard for the Enggano language</h1>
<ul>
    <li> For nasalized vowels (ã/Ã/ẽ/Ẽ/ĩ/Ĩ/õ/Õ/ũ/Ũ) type the vowel followed by tilde (~).
    <li> For e/E/u/U with a dot (ė/Ė/u̇/U̇), type e or u followed by star (*).
    <li> For nasalized central vowels (ė̃/Ė̃/u̇̃/U̇̃), type e/u/E/U followed by star (*), followed by tilde (~).
</ul>

<h1>Menggunakan keyboard desktop/laptop untuk bahasa Enggano</h1>
<ul>
    <li> Untuk vokal bernasal (ã/Ã/ẽ/Ẽ/ĩ/Ĩ/õ/Õ/ũ/Ũ) ketikkan vokal diikuti dengan tilde (~).
    <li> Untuk e/E/u/U dengan titik (ė/Ė/u̇/U̇), ketik e atau u diikuti dengan bintang (*).
    <li> Untuk vokal pusat bernasal (ė̃/Ė̃/u̇̃/U̇̃), ketik e/u/E/U diikuti bintang (*), lalu diikuti tilde (~).
</ul>

<h1>Using the mobile phone keyboard for the Enggano language</h1>

<p>
Mobile keyboard layout for mobile phones: long press on a/A/i/I/o/O  brings up a menu with the characters with tilde; long press on e/E/u/U brings up a menu with the characters with tilde, dot, or tilde and dot.
</p>

<h1>Menggunakan keyboard ponsel untuk bahasa Enggano</h1> 
<p>
Tata letak keyboard seluler untuk ponsel: tekan lama pada a/A/i/I/o/O akan menampilkan menu dengan karakter dengan tilde; tekan lama pada untuk e/E/u/U akan menampilkan menu dengan karakter dengan tilde, dot, atau tilde dan dot.
</p>
