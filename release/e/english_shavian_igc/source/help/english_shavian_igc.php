  <h2>Phone and tablet layouts</h2>
  <p>The layouts for phone and tablet are substantially similar to each other, and are based on the desktop layout with some tweaks to avoid the keys becoming too small.</p>
  <p>The compound letters (and 𐑠) may be found by holding down (long hold) one of the keys with the dot in the top right. Holding down:</p>
  <ul>
    <li>𐑭 will display 𐑸</li>
    <li>𐑷 will display 𐑹</li>
    <li>𐑵 will display 𐑿</li>
    <li>𐑱 will display 𐑺</li>
    <li>𐑳 will display 𐑻</li>
    <li>𐑦 will display 𐑾 and 𐑽</li>
    <li>𐑩 will display 𐑼</li>
    <li>𐑖 will display 𐑠</li>
  </ul>
  <p>Pressing the [123] key will bring up the numbers and a range of symbols, arranged in a similar layout to the standard iOS keyboard. Long hold keys include additional symbols.</p>
  <p>Pressing the [abc] key will bring up a standard US English QWERTY layout keyboard to allow typing in the Latin alphabet. Long hold keys allow the entry of a selection of diacritics, common International Phonetic Alphabet symbols used for English, as well as Esperanto letters (for those interested in Shavian for Esperanto, the Ŝava alfabeto).</p>
  
