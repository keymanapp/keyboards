<h1>Keyboard Layout</h1>

<h2>Desktop and onscreen layouts</h2>

<p>The desktop and onscreen layouts are identical, and are based on the <a href="https://oztypewriter.blogspot.com/2014/04/history-of-shavian-keyboard-imperial.html">Shavian Imperial Good Companion</a> typewriter layout, used by by Kingsley Read in the 1960s. Shavian IGC is named in honour of the Shavian Imperial Good Companion typewriter.</p>
<p>The compound letters (𐑸, 𐑹, 𐑺, 𐑻, 𐑼, 𐑽, & 𐑿), and a range of symbols, are entered by holding down SHIFT.</p>
<p>Numbers may be entered by holding down ALT.</p>

