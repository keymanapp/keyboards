  <h2>Desktop Keyboard layout</h2>
<p>The keyboard layout is demonstrated in the keyboard images below.</p>
<p>Half forms and full consonant letters are sometimes present in same key (on default and shift states). For example  क and क्‍ are placed on same key.</p>
<p>Some consonant letters do not have dedicated keys for half letters. To get half letter we need to type halant ( ्) after full consonent letter. For example there is no key for typing छ्‍. we have to type छ and then ् to get छ्; </p>
<p>The are no dedicated full consonant letters for some consonants. For example there is no dedicated key for  भ, so you cannot type  भ with just one single key. You need to type  भ्‍ and then  ा to get  भ .</p>
<p>To get  ई first you need to type the letter  इ then you have to type  र् (SHIFT+Z).</p>

