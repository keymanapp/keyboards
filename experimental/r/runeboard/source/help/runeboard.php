<h1>Keyboard Layout</h1>

<p>Watch <a href=https://www.youtube.com/channel/UCWLmY2EMdJxE_ch2BTvwl1g>Rune Revival on YouTube</a>.</p>

<h3>Notes:</h3>
<p>1) If you have installed the Runeboard keyboard by downloading it from Keyman, Ogham and Latin characters will be missing. This is because Keyman will not distribute mixed script keyboards. Nonetheless, all runic characters will still be available to use. If you desire access the full layout with the extra characters, please contact Rune Revival via the link above. Thank you.</p>
<p>2) Before use, ensure none of your other software uses any conflicting hotkeys.</p>
<p>3) The K_Z key is the Zero Width Joiner [ZWJ]. In fonts which support bindrunes/ligatures, type it after another character and before another to create a supported bindrune.</p>

