<p>
This is a keyboard for typing sitelen pona, a logographic writing system
for the popular constructed language toki pona. This keyboard uses glyph
components (radicals) to input sitelen pona logographs similarly to how
they would be written by hand.
</p>

<p>
This keyboard is based on the
<a href="https://sona.pona.la/wiki/Wakalito">Wakalito</a> input method for
<a href="https://apps.apple.com/us/app/nasin-sitelen-wakalito/id1569543076">iOS</a> and
<a href="https://archive.org/details/nasin_sitelen_Wakalito">Espanso</a>
by jan Likipi, kala pona Tonyu, and jan Tepo.
However, there are several major differences.
Unlike the original implementation, this keyboard outputs sitelen pona
characters instead of toki pona words in Latin script. Additionally,
the set of available punctuation and special characters is different,
and several non-pu words which are supported by the original but
which are not present in the UCSUR encoding are not supported.
</p>

<p>
The sitelen pona logographs produced by this keyboard use the
<a href="https://www.kreativekorp.com/ucsur/charts/sitelen.html">UCSUR encoding</a>.
A sitelen pona font supporting this encoding is required for text to appear correctly.
(The Keyman mobile app uses a font included in this package.)
</p>

