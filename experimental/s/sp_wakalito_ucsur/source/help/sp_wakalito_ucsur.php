<script>
  function prep() {
    var tables = document.getElementsByClassName('typing');
    for (var i = 0; i < tables.length; i++) {
      var rows = tables[i].getElementsByTagName('tr');
      for (var j = 0; j < rows.length; j++) {
        rows[j].className = 'hidden';
      }
      var f = function(rows, k, n) {
        rows[k].className = 'hidden';
        k++; if (k >= n) k = 0;
        rows[k].className = '';
        var g = function() { f(rows, k, n); };
        var h = (k == n - 2) ? 1500 : 750;
        setTimeout(g, h + Math.floor(Math.random() * h));
      };
      f(rows, rows.length-2, rows.length);
    }
  }
  prep();
</script>
