<p>蘇州안ᄋᆞ有七種音調：陰平、陽平、陰上、陽上、陰去、陰入로陽入，陰平로入聲用勿着標音調，陰去要標音調符號 〮，陽平聲調个音節假使쓰ㅁ ㄴ ㄹ聲母个，出現垃拉單詞个前頭，극윽要標記音調符號 〮，其他條件ᄋᆞ〮ᄄᆕ个陽上音節用勿着標音調。陰上로陽上要標音調符號 〯。ㅇ聲母个音節讀陽聲調，ㆆ聲母个音節讀陰聲調。多音節詞語假使第一个音節쓰陽聲，극믁只有第一个音節需要標記音調。</p>

