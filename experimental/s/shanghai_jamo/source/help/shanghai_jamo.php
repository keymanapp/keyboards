<p>Shanghai Dialect has 4 tones, high level tone, falling tone, rising tone, entering tone. The high level tone and entering tone don't need to be marked, The falling tone should be marked  with 〮, the rising tone syllable with ㅁ ㄴ ㄹ consonant should be marked with 〮 in the beginning of a word, otherwise the syllable will be pronounced as high level tone, the rising sound syllable with other circumstance don't need to be marked. syllable with ㅇ consonant in the front will be rising tone. Syllable with ㆆ consonants without tone mark will be the high level tone. Syllable with ㆆ consonants with tone mark will be the falling tone. In word with multiple syllables, the tone marks of the sylllables behind the first syllale can be omitted</p>
