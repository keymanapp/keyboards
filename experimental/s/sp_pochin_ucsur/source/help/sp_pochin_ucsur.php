<p>
The eight keys in light green (1, Q, A, Z, 2, W, S, X) are called
<b>word keys</b> and output logographs directly.
</p>

<p>
The fourteen keys in dark blue (3, 7, E, U, 8, I, 9, 0, O, P, J, K, L, M)
are called <b>special keys</b> and output punctuation or special characters.
</p>

<p>
The fourteen keys in light blue (4, 5, 6, R, T, Y, D, F, G, H, C, V, B, N)
are called <b>letter keys</b> and are associated with each of the fourteen
Latin letters used in toki pona. Logographs not entered using the word keys
are entered using two or three keystrokes of these keys. The first keystroke
for a word is always its first letter. For most words, other keystrokes
are letters within that word. (Some non-pu words added in this implementation
don't quite follow this rule.)
</p>

<p>
Non-alphanumeric and shifted keys output the same punctuation and uppercase
Latin letters as on a standard ASCII keyboard layout.
</p>

