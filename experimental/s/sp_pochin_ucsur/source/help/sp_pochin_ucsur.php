<p>
This is a keyboard for typing sitelen pona, a logographic writing system for
the popular constructed language toki pona. This keyboard uses sequences of
one to three keystrokes to enter sitelen pona logographs.
</p>

<p>
This keyboard is based on the
<a href="https://github.com/timeopochin/tokiponakeyboard">Toki Pona Keyboard app by Timeo Pochin</a>.
However, there are several major differences.
Unlike the original implementation, this keyboard does not support gestures for compound glyphs.
Also, this keyboard outputs sitelen pona characters instead of toki pona words in Latin script.
Finally, this keyboard can output additional characters the original did not support.
</p>

<p>
The sitelen pona logographs produced by this keyboard use the
<a href="https://www.kreativekorp.com/ucsur/charts/sitelen.html">UCSUR encoding</a>.
A sitelen pona font supporting this encoding is required for text to appear correctly.
(The Keyman mobile app uses a font included in this package.)
</p>

