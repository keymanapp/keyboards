<p>
The eight keys in light green are called <b>word keys</b> and output logographs directly.
In this implementation, long pressing on some of these keys will bring up additional logographs.
</p>

<p>
The ten keys in dark blue are called <b>special keys</b> and either output punctuation
or perform a special function such as space, backspace, enter, or switching layouts.
Unlike in the original implementation, these do not automatically switch between opening
and closing punctuation. Also, the original quotation mark, period, and question mark
are replaced with CJK quotation marks and the sitelen pona interpunct and colon.
Long pressing on some of these keys will bring up additional punctuation characters,
including those supported by the original implementation.
</p>

<p>
The fourteen keys in light blue are called <b>letter keys</b> and are associated with
each of the fourteen Latin letters used in toki pona. If the logograph displayed on
the key is green, pressing that key will switch to another layer with logographs for
words starting with the same Latin letter. If the logograph displayed on the key is
white, pressing that key will output that logograph and return to the default layer.
The basic rules for these keys are:
</p>

<ul>
<li>Pressing any white logograph once will output the displayed logograph.</li>
<li>Pressing any green logograph twice will output the displayed logograph.</li>
<li>The first keypress for a word is always its first letter.</li>
<li>For most words, other keypresses are letters within that word.
(Some non-pu words added in this implementation don't quite follow this rule.)</li>
</ul>

