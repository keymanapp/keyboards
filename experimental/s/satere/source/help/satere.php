<p>
    The keyboard supports the Satere language of Brazil. The layout is based on a standard US keyboard layout.
</p>

<h2>Desktop Keyboard Layout</h2>
<p>This keyboard makes use of deadkeys. Typing the deadkey before the base character will output the combined character. For example:</p>

<p>~a produces ã (available on aAeEiIoOuUyY). These are also available on the RALT key. For example RALT+a produces ã (available on aAeEiIoOuUyY).</p>

<p>~~ will output a "~"</p>
<p>' outputs a "saltillo" glottal character (U+A78C). Two in a row will output the normal single quote.</p>

<h2>Mobile Keyboard Layout</h2>

<p>Due to the size and number of keys, some characters are hidden in the long press. 
	Press and hold on the key with a little dot on the top right to reveal and use them.</p>