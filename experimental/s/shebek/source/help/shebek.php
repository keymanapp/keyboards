<p>
  This keyboard is designed for typing Shebek in the Arabic script. The orthography is still in development. Some characters from a standard Arabic keyboard that are not required by Shebek have been added to the right-alt keyboard layers.
</p>
<p>The font distributed with this keyboard is named <b>Scheherazade New Kurd</b>. It has specific glyph variants for Kurdish languages.</p>


<h2>Desktop Keyboard Layout</h2>

  <p>There are a few keys which are only available through two keystrokes:</p>
<ul>
  <li><b>SHIFT+M</b> produces &#x2018;, and pressing <b>SHIFT+M</b> a second time will produce &#x201C;</li>
  <li><b>SHIFT+U</b> produces &#x2019;, and pressing <b>SHIFT+U</b> a second time will produce &#x201D;</li>
</ul>  

