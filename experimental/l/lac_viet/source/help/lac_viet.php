 <p>The consonant [k] is spelled as the letter "k" when it appears before the vowel letters "e, ê, i, y". When it appears before the vowel letters "â, ă, u, ư, ô, ơ"， it is spelled as the letter "c".</p>
 <p>The consonant [ɣ] is spelled as the letter "gh" when it appears before the vowel letters "e, ê, i, y". When it appears before the vowel letters "â, ă, u, ư, ô, ơ"， it is spelled as the letter "g".</p>
 <p>The consonant [ŋ] is spelled as the letter "ngh" when it appears before the vowel letters "e, ê, i, y". When it appears before the vowel letters "â, ă, u, ư, ô, ơ"， it is spelled as the letter "ng".</p>
<p>The consonants in yellow background exist in Zhuang dialects other than Nàm-Nình dialect.</p>
