<h1>Jamo Generation</h1>
<p>
  The generating rules for old hangul is similar with typing with normal jamo keyboard. 
</p>
  <p>Beside the ordinary Jamo and those letters which delivered a single key. You can combine each jamo letters or jamo strokes togeter to generate jamo letters and syllables. The generating orders are all from left to right and from up to down.
</p>
<p>Adding a space then press backspace to delete the space will end generation of the current syllable and start the next syllable.
</p>
<p>
This keyboard use uppercase "J" to generate tones. Pressing uppercase "J" key once is for the falling tone sign 〮, then press the J key again for generating rising tone sign 〯.
</p>
