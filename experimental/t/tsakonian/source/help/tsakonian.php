<h2>Ελληνική</h2>
<p>Το τσακώνικο πληκτρολόγιο έχει σχεδιαστεί για την πληκτρολόγηση της τσακώνικης γλώσσας. Περιλαμβάνει επίσης διάφορους πρόσθετους ελληνικούς χαρακτήρες και σύμβολα για την ευκολία χρήσης των Αρχαίων Ελληνικών.</p>
<p>Περιλαμβάνει ένα πληκτρολόγιο οθόνης, το οποίο μπορείτε να δείτε κάνοντας κλικ στο εικονίδιο Keyman και επιλέγοντας το στοιχείο μενού On Screen Keyboard. Παρόμοια πληκτρολόγια είναι επίσης διαθέσιμα για άλλες πλατφόρμες γραφείου και κινητών τηλεφώνων.</p>

<h2>English</h2>
<p>The Tsakonian keyboard is designed for typing the Tsakonian language. It includes additional Greek symbols and characters for convenience.</p>

<p>It includes an on screen keyboard which can be viewed by clicking on the Keyman icon and selecting the On Screen Keyboard menu item. Similar keyboards are also available for other desktop and mobile platforms.</p>

	
