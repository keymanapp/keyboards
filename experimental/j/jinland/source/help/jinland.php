<h2>tone rules 音調輸入規則</h2>
 <p> 陰平不用打，上聲打x，陰去和陰入聲打s，陽平打f，陽去和陽入打j <p>
 <p> high level for nothing, tone 2 = x, tone 3 =s, tone 4 = f, tone 6 = j <p>
