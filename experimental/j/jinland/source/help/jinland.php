  <h2>Punctuation and Capitalization標點和大小寫</h2>
  <p>A space " " should be added betweem each words or between each syllable of cantonese origin or sino-cantonese origin words.</p>
  <p>每個詞語或者每個晉語詞的每個音節之間應該添加空格" "</p>
  <p>The first letter in one sentence should be capitalized</p>
  <p>每個句子的第一個字母要大寫</p>
  <p>The first letter in a proper noun such as the name of a particular person and place should be capitalized</p>
  <p>專有名詞比如人和地方的名字的第一個字母要大寫</p>
  <p>The first letter in each syllable of a Cantonese origin or sino-cantonese origin proper noun such as the name of a particular person and place should be capitalized</p>
  <p>晉語的專有名詞的每一個音節的第一個字母要大寫</p>
  <p>In an acronym, the first letter of each syllable should be capitalized, and a period "." can be added behind each capitalized letter</p>
  <p>在縮寫中，每個音節的第一個字母要大寫，在每個大寫字母的後面可以加上一個句號"."</p>
  <p>In a multi-syllable single word, the space " " should be added between each syllable.</p>
  <p>在一個多音字單詞裏面，每個音節之間可以加入一個連字符"-"</p>
  <p>In a multi-syllable combined word or a Cantonese origin or sino-cantonese origin word, the hyphen "-" can be added between each syllable for avoid confusion</p>
  <p>在一個多音節的組合詞或者晉語詞的每一個音節之間，可以加入一個連字符"-"避免混淆</p>
  <p>Other punctuation works similar with punctuations in English and Vietnamese</p>
  <p>其他標點的作用與英文和越南文的類似</p>
  <p></p>


<p> 雙擊音調鍵或者特殊轉化鍵，取消轉化，變回原本的拼寫 <p>
 <p> doubletaping leads to generate original letters:  <p>
   <p>
     If more than one tone marking key is pressed, the last one will be used. For example, typing <code>asf</code> will return "à". To write a tone marking key as a normal character, one has to press it twice: <code>hex</code> becomes <code>hẻ</code>, while <code>hexx</code> becomes <code>hex</code>.
   </p>
 <p> 使用Z鍵取消音調轉換 <p>
 <p> tap Z key to remove tonations  <p>

