<p>
  A keyboard for typing in Jèrriais, a dialect of Norman and the native language of Jersey in the Channel Islands.
</p>

<h2>Desktop</h2>

<p>The desktop version is used with a UK QWERTY keyboard, and adds key combinations for diacritics.

<ul>
  <li>A vowel followed by two single quotes ('') creates an acute accent: e'' = é</li>
  <li>A vowel followed by two backticks (``) creates a grave accent: a`` = à</li>
  <li>A vowel (or "y") followed by two carets (^^) creates a circumflex: o^^ = ô</li>
  <li>A vowel followed by two double quotes ("") creates a diaresis: i"" = ï</li>
  <li>A "c" followed by two commas (,,) creates a cedilla: c,, = ç</li>
</ul>

<h2>Mobile/Tablet</h1>

<p>The mobile keyboard is a QWERTY layout. The vowels and some other letters with dots in the top right corner can be long-pressed for accent options, and long pressing the full-stop and apostrophe keys gives further punctuation.
</p>

<p>You can also use flicking gestures for commonly used accents:</p>
<ul>
  <li>Flicking up on a vowel gives a circumflex</li>
  <li>Flicking up and to the right on an appropriate vowel gives an acute accent</li>
  <li>Flicking down and to the right on an appropriate vowel gives a grave accent</li>
  <li>Flicking to the left gives a diaresis</li>
  <li>Flicking down on the c key gives a cedilla</li>
  <li>Flicking left on the full stop gives a comma</li>
  <li>Flicking up on the full stop gives an exclamation mark</li>
  <li>Flicking up on the apostrophe gives a double quote</li>
</ul>

<p>Other punctuation can be found by pressing the 123 key.</p>

