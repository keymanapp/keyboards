<h1>Keyboard Layout</h1>

<p>This keyboard followings the standard US QWERTY layout. To access the "ŋ" character, type "ng". To access the "Ŋ" character, type "NG" or "Ng". If backspace is typed after "ŋ" it will turn back to "ng". If backspace is typed after "Ŋ" it will turn back to "Ng".</p>
