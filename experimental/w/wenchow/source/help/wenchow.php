<p>
    wenchow for üe-tsiu-‘ó.溫州話羅馬字
</p>
<p> typing specific letters by doubletapping and combination 雙擊和字母組合輸入特殊字母</p>
<p> examples: oo > ö, uu > û, ii > ï, ae > æ, aa > æ, uw > ü, mm > m̆, nn > n̆, ll > l̆, ngg > n̆g </p>
<p> adding aspiration marks "‘" by adding h after consonants 在輔音後輸入h字母，添加送氣音符號"‘"</p>
<p> examples: ph > p‘, th > t‘, kh > k‘, tsh > ts‘, chh > ch‘, kwh > kw‘, hh > ‘ </p>
<p> doubletapping h, to generating voiced sound h "‘", 雙擊h，產生濁音h "‘"</p>
<p> examples: hh > ‘ </p>
<p> typing tone marks by adding "q" and "x" 通過添加字母q和x輸入音調符號 </p>
<p> examples: aq > á, ax > à. </p>
<p> cancelling letter tranformation and tone marks by typing the tranformation key again</p>
<p> 再次點擊轉化鍵，取消字母轉化和音調符號 </p>
<p> examples: aqq > aq, axx > ax, ooo > oo. </p>
