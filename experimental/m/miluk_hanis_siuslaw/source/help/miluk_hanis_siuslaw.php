<p>
    This keyboard is created for the miluk, hanis, shaʼyuushtlʼa, quuiich, and alsea languages. It includes upper and lower case versions of all characters. In addition to your keys responding when pressed on their own, all keys have additional outputs when holding down the shift key. These represent the different sounds that are found in these three languages. Notably, labialized consonants, ejectives, and geminant consonants are accessed via the shift key.

</p>

