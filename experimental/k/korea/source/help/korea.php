<h1>Jamo Generating</h1>
<p>
  The generating rules for old hangul is similar with typing with normal jamo keyboard. You can combine each jamo letters or jamo strokes to generate combined consonants, combined vowels, and jamo syllable. The generating happens from left to right and from up to down.
</p>
<p>
For example:
</p>
<p>
ㅣ + ㅇ > ㆁ
</p>
<p>
ㄹ + ㅇ > ᄛ
</p>
<p>
ㅂ + ㅇ > ㅸ
</p>
<p>
ㅍ + ㅇ > ㆄ
</p>
<p>
ㅕ + ㅣ > ㅖ
</p>
<p>
ㅑ + ㅣ > ㅒ
</p>
<p>
얘 + ㅣ > ᅌퟀ
</p>
<p>
이 + ㅣ > ᄋퟄ
</p>
<p>Typing space while pressing shift to end generating of one syllable for start next syllable.</p>
<p>Adding a space then deleting the space will have the same effect.
</p>
<p>This keyboard can automatically progress syllables.
