<h2>Empieza usar este teclado para la lengua koreguaje</h1>

<p>
    Este es teclado Koreguaje v. 0.2. Las posiciones de las letras son basadas en las del teclado español.
</p>

<h2>Diseño del Teclado</h1>

<p>
    La tecla [ñ] del teclado español aquí se convierte en tecla especial para añadir un tilde (~) en cima de un vocal 
    para indicar nasalización o en cima de la 'n' para hacer la 'ñ'.
    Tocar la [ñ] y después el espacio para hacer un tilde solito.
</p>

<p>
    La tecla [-] también se convierte en tecla especial para añadir una barra en medio de una 'u' o 'i'. 
    Para hacer la letra 'ʉ̃', se toca las teclas [~] y [-] en cualquier orden, y después se toca la [u].
</p>

<p>
    La tecla [´] sirve igual que en el teclado de español, para añadir un acento en cima de un vocal.
    Junto con [shift], hace el dieresis (¨) para hacer la letra 'ü'. 
    En este teclado, no se puede añadir el acento en vocales que llevan tilde ni barra.
</p>

