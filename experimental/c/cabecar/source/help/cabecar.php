<h1>Diseño del Teclado</h1>

<h2>Diseño del Teclado de Escritorio</h2>
<div id='osk' data-states='default shift shift-ctrl'>

<p>Agregando letras con diacríticos

Para crear vocales con tilde (á, por ejemplo), presione la tecla de la tilde seguida de la vocal deseada. Para una mayúscula tildada (Á, por ejemplo), presione la tecla de la tilde y luego SHIFT + vocal.

Para agregar la diéresis (ä, por ejemplo), presione la tecla “[” seguida de la vocal deseada. Para una mayúscula (Ä, por ejemplo), presione esta misma tecla seguida de SHIFT + vocal.

Para agregar la raya de nasalidad (a̱, por ejemplo), presione la tecla del guion seguida de la vocal deseada. Para una mayúscula (A̱, por ejemplo), presione esta misma tecla seguida de SHIFT + vocal.

Para agregar la combinación de diéresis más tilde (ä́, por ejemplo), presione SHIFT + la tecla “[” seguida de la vocal deseada. De forma similar, para la mayúscula (Ä́, por ejemplo), presione SHIFT + la tecla “[” y luego SHIFT + vocal.

Para agregar la combinación de raya de nasalidad más tilde (á̱, por ejemplo), presione SHIFT + la tecla del guion seguida de la vocal deseada. Para una mayúscula (Á̱, por ejemplo), presione SHIFT + la tecla del guion y luego SHIFT + vocal.

No es necesario mantener presionadas ambas combinaciones de teclas para obtener las marcas diacríticas. Pero si lo desea, puede hacerlo, pues funcionará de todos modos. </p>
</div>

