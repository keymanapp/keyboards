<p>
  This keyboard is designed for the Chuvash language. It is loosely based on the Russian (Basic) layout, but it includes the characters required for Chuvash.
</p>
<p>Acute accents can be typed immediately after a vowel. For example, typing "и" following by acute accent will produce "и́".</p>

