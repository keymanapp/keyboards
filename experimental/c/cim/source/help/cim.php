<h1>Keyboard Layout</h1>

<h2>Desktop Keyboard Layout</h2>
<div id='osk' data-states='default shift'>
</div>
<p> To access vowels with macrons, please use the keystroke Alt-vowel.  For capitalized vowels with macrons, use Shift-Alt-vowel.
To access the Saltillo, use Alt-semicolon, or Shift-Alt-semicolon for a capitalied Saltillo.
To access the 'okina (works better with some fonts/programs), use Alt-quote or Shift-Alt-quote.</p>

<h2>Mobile/Tablet Keyboard Layout</h2>
<div id='osk-phone' data-states='default shift numeric'>
</div>
<p>Due to the size and number of keys, some characters are hidden in the long press.
Press and hold on the key with a little dot on the top right to reveal them.
Macrons can be accessed through their respective vowels. Saltillo is the far right
key, next to the letter "L", and the 'okina can be accessed with longpress on that key.</p>